def ch1_interview_framework():
    title("第1章: システム設計面接フレームワーク")

    p("""
    まず全体像:
    システム設計面接は「唯一の正解」を当てる試験ではない。
    曖昧な課題を分解し、要件を引き出し、制約下で妥当な設計を組み立てる
    思考プロセスそのものが評価対象になる。

    面接官が見ている主な観点:
    - 問題設定の明確化（何を作るか、何を作らないか）
    - 規模感の把握（QPS、データ量、遅延要件）
    - トレードオフ説明（一貫性/可用性、コスト/性能）
    - リスク認識（障害時の挙動、運用性、将来拡張）

    たとえ話:
    完成した料理の正解を当てる試験ではなく、
    限られた材料と時間でメニューを設計する料理長の判断を見る試験。
    """)

    # --- Step 1 ---
    heading("Step 1: 要件定義 (最初の5分)")

    p("""
    この節の狙い: 「Step 1: 要件定義 (最初の5分)」を単語暗記ではなく、なぜ必要かまでつながる形で理解する。
    なぜ最初の5分が合否を分けるか？
    → 面接官が見ているのは「曖昧な問題を構造化する力」。
    いきなり設計を始める候補者は「要件を確認しない = 実務で暴走する人」
    と判断される。

    確認すべき2種類の要件:
      1. Functional Requirements (FR): ユーザーが何をできるか
         例) URLを短縮する、短縮URLでリダイレクトする
      2. Non-Functional Requirements (NFR): 品質属性
         例) DAU 1億、レイテンシ < 100ms、可用性 99.99%

    質問テンプレート:
      「ユーザー規模はどのくらいですか？」
      「読み取りと書き込みの比率は？」
      「データの保持期間は？」
      「一貫性と可用性、どちらを優先しますか？」
    """)

    misconception(
        "要件は面接官が全部教えてくれる",
        "自分から質問して要件を引き出すこと自体が評価ポイント"
    )

    # --- Step 2 ---
    heading("Step 2: 概算 (Back-of-Envelope Estimation, 3分)")

    p("""
    まず全体像: この節では「Step 2: 概算 (Back-of-Envelope Estimation, 3分)」を、定義 -> 仕組み -> 実務上の判断の順で整理する。
    概算の目的は「どの規模のインフラが必要か」を把握すること。
    正確な数値は不要。桁数(オーダー)が合っていればよい。

    計算の流れ:
      DAU → 1日のリクエスト数 → QPS → ピークQPS
      → ストレージ(5年分) → 帯域幅 → サーバー台数

    覚えるべき定数:
      1日 = 86,400秒 ≈ 10万秒
      1MB = 10^6 bytes, 1GB = 10^9, 1TB = 10^12
      1サーバー ≈ 10,000 QPS (Webサーバー目安)
    """)

    # --- Step 3 ---
    heading("Step 3: 高レベル設計 (10分)")

    p("""
    先に結論: 「Step 3: 高レベル設計 (10分)」は実装と設計判断に直結する。背景とトレードオフを押さえる。
    ブロック図を描く。データの流れ(Write Path / Read Path)を矢印で示す。

    典型的な構成:
      Client → LB → App Server → Cache / DB / MQ

    ポイント:
      - まず最もシンプルな構成を描く
      - 面接官の同意を得てから詳細に入る
      - 「この段階ではシングルサーバーですが、後でスケールします」でOK
    """)

    # --- Step 4 ---
    heading("Step 4: 詳細設計 (20分)")

    p("""
    先に結論: 「Step 4: 詳細設計 (20分)」は実装と設計判断に直結する。背景とトレードオフを押さえる。
    面接官が深掘りしたいポイントに集中する。全部を語る必要はない。

    よく聞かれる深掘りポイント:
      - データモデル（テーブル設計、インデックス）
      - スケーリング（シャーディング戦略）
      - キャッシュ戦略
      - 障害対策（レプリカ、フェイルオーバー）
      - 特殊なアルゴリズム（Consistent Hashing, Trie等）
    """)

    # --- Step 5 ---
    heading("Step 5: ボトルネックとトレードオフ (5分)")

    p("""
    この節の狙い: 「Step 5: ボトルネックとトレードオフ (5分)」を単語暗記ではなく、なぜ必要かまでつながる形で理解する。
    「この設計のボトルネックはどこか？」と自ら提起する。
    これができると「シニアレベル」の評価になる。

    トレードオフの軸:
      一貫性 vs 可用性 (CAP定理)
      レイテンシ vs スループット
      コスト vs 信頼性
      シンプルさ vs スケーラビリティ
    """)

    interview("""
    5ステップを必ず順に踏む。特にStep 1の要件定義を飛ばさない。
    「まず要件を確認させてください」と切り出すのが黄金パターン。
    時間配分: 5分 → 3分 → 10分 → 20分 → 5分 = 43分
    """)


