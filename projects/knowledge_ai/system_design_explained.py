def ch2_common_patterns():
    title("第2章: 共通パターン (Building Blocks)")

    # --- Load Balancer ---
    heading("2.1 ロードバランサー (LB)")

    p("""
    先に結論: 「2.1 ロードバランサー (LB)」は実装と設計判断に直結する。背景とトレードオフを押さえる。
    ロードバランサーとは何か:
      複数サーバーにリクエストを分散する「交通整理係」。
      1台のサーバーに負荷が集中するのを防ぐ。

    L4 (トランスポート層) vs L7 (アプリケーション層):
      L4: TCP/UDPレベルでパケットを転送。高速だがHTTPの中身は見ない。
          → 用途: 大量接続の分散、TCP接続の振り分け
      L7: HTTP/HTTPSの中身(URL, Cookie, Header)を見て判断。
          → 用途: /api/* は APIサーバー、/static/* は CDN へルーティング

    アルゴリズム:
      Round Robin: 順番に振り分け。最もシンプル
      Least Connections: 接続数が最少のサーバーへ
      IP Hash: クライアントIPで固定サーバーへ(セッション維持)
      Weighted: サーバーのスペックに応じて重み付け
    """)

    misconception(
        "ロードバランサーは1台でよい",
        "LB自体が単一障害点になる。Active-Passive構成で冗長化する"
    )

    # --- Cache ---
    heading("2.2 キャッシュ戦略")

    p("""
    まず全体像: この節では「2.2 キャッシュ戦略」を、定義 -> 仕組み -> 実務上の判断の順で整理する。
    キャッシュとは何か:
      頻繁にアクセスされるデータを高速なストレージ(メモリ)に置くこと。
      レストランの「本日のおすすめ」が壁に書いてあるようなもの。
      メニュー全体を見なくても素早く注文できる。

    Cache-Aside (Lazy Loading):
      1. アプリがキャッシュを見る
      2. ヒット → キャッシュから返す
      3. ミス → DBから取得 → キャッシュに書き込み → 返す
      長所: 必要なデータだけキャッシュされる
      短所: 初回アクセスは必ずミス(コールドスタート)

    Write-Through:
      1. 書き込み時にキャッシュとDBを同時に更新
      長所: キャッシュとDBが常に一致
      短所: 書き込みレイテンシが増加

    Write-Behind (Write-Back):
      1. キャッシュにだけ書き込み
      2. 非同期でDBに反映
      長所: 書き込みが超高速
      短所: キャッシュ障害でデータ消失リスク

    TTL (Time To Live):
      キャッシュの有効期限。短すぎるとヒット率低下、長すぎると古いデータ。
    """)

    p("""
    キャッシュ無効化 (Invalidation):
      「コンピュータサイエンスで最も難しい2つのこと:
       キャッシュ無効化と命名」 — Phil Karlton

      戦略:
        1. TTLベース: 一定時間で自動失効
        2. イベントベース: 更新時に明示的に削除
        3. バージョンベース: キーにバージョンを含める
    """)

    interview("""
    「Cache-Asideを採用します。理由は読み取り頻度が高く、
    書き込みは少ないためです。TTLは5分に設定し、
    データ更新時にはキャッシュを明示的に削除します」
    と、戦略 + 理由 + 具体的なパラメータをセットで語る。
    """)

    # --- DB Scaling ---
    heading("2.3 データベーススケーリング")

    p("""
    まず全体像: この節では「2.3 データベーススケーリング」を、定義 -> 仕組み -> 実務上の判断の順で整理する。
    垂直スケーリング (Scale Up):
      サーバーのCPU/メモリを増強。シンプルだが限界がある。

    水平スケーリング (Scale Out):
      サーバーを増やしてデータを分散。

    レプリケーション:
      Master-Slave: 書き込みはMaster、読み取りはSlaveに分散
      Master-Master: 両方で書き込み可能だがコンフリクトの問題

    シャーディング (Sharding):
      データを複数DBに分割する。「図書館の本棚を部屋ごとに分ける」イメージ。
      - Hash Sharding: hash(key) % N でシャードを決定
      - Range Sharding: 日付やIDの範囲で分割
      問題: リシャーディング、ホットスポット、JOIN困難
    """)

    misconception(
        "シャーディングは最初から導入すべき",
        "複雑性が爆増する。まずレプリケーション+キャッシュで粘り、"
        "それでも足りなくなったらシャーディングを検討"
    )

    # --- Message Queue ---
    heading("2.4 メッセージキュー (MQ)")

    p("""
    まず全体像: この節では「2.4 メッセージキュー (MQ)」を、定義 -> 仕組み -> 実務上の判断の順で整理する。
    メッセージキューとは何か:
      Producer(送信者)とConsumer(受信者)を非同期に繋ぐ「郵便ポスト」。
      送り手は手紙を投函したら待たずに帰れる。

    なぜ必要か:
      1. 非同期処理: 重い処理をバックグラウンドで実行
      2. 負荷平準化: リクエストの急増を吸収
      3. 疎結合: サービス間の依存を減らす

    代表的なMQ:
      Kafka: 高スループット、ログベース、順序保証(パーティション内)
      RabbitMQ: 柔軟なルーティング、メッセージ確認応答
      SQS: AWSマネージド、運用不要

    注意点:
      - メッセージの重複配信 → 冪等性(Idempotency)で対処
      - 順序保証 → 同一パーティションキーで保証
      - メッセージ消失 → ACK + 永続化で対策
    """)

    # --- CDN ---
    heading("2.5 CDN (Content Delivery Network)")

    p("""
    まず全体像: この節では「2.5 CDN (Content Delivery Network)」を、定義 -> 仕組み -> 実務上の判断の順で整理する。
    CDNとは何か:
      世界中にエッジサーバーを配置し、ユーザーの近くからコンテンツを配信。
      東京のユーザーにUS-Eastのサーバーから配信すると200ms。
      東京のエッジサーバーからなら10ms。

    Push CDN vs Pull CDN:
      Push: コンテンツを事前にCDNにアップロード
            → 更新頻度が低い静的コンテンツ向き
      Pull: 最初のリクエスト時にオリジンから取得してキャッシュ
            → トラフィック量が多いサイト向き
    """)

    # --- API Design ---
    heading("2.6 API設計")

    p("""
    まず全体像: この節では「2.6 API設計」を、定義 -> 仕組み -> 実務上の判断の順で整理する。
    REST vs GraphQL vs gRPC:
      REST: リソースベース、HTTPメソッド(GET/POST/PUT/DELETE)
            → 用途: 公開API、シンプルなCRUD
      GraphQL: クライアントが必要なデータだけ指定
            → 用途: フロントエンド駆動、複数リソースの一括取得
      gRPC: Protocol Buffers、双方向ストリーミング
            → 用途: マイクロサービス間通信、低レイテンシ

    API設計のポイント:
      - バージョニング: /api/v1/, /api/v2/
      - ページネーション: cursor-based (offset-basedは大量データで非効率)
      - レート制限: 429 Too Many Requests を返す
      - 冪等性: PUT/DELETEは冪等、POSTは冪等キーで対応
    """)

    interview("""
    Building Blocksは「引き出し」。問題に応じて適切なものを選ぶ。
    面接では「なぜこのコンポーネントを選んだか」を必ず説明する。
    例: 「読み書き比率が100:1なので、Cache-Aside + Read Replica で
    読み取り性能を確保します」
    """)


