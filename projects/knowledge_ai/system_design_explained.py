def ch4_design_problems_2():
    title("第4章: 設計問題 - Rate Limiter / KV Store / 検索")

    # --- Rate Limiter ---
    heading("4.1 Rate Limiter (レート制限)")

    p("""
    先に結論: 「4.1 Rate Limiter (レート制限)」は実装と設計判断に直結する。背景とトレードオフを押さえる。
    問題: APIへのリクエストを制限し、不正利用やDDoSを防ぐ。

    なぜ必要か:
      - サーバーの過負荷防止
      - コスト管理 (API課金)
      - 公平なリソース分配
      - セキュリティ (ブルートフォース防止)

    主要アルゴリズム:

    1. Token Bucket:
       バケツにトークンが一定速度で補充される。
       リクエスト1回につきトークン1個消費。トークンがなければ拒否。
       水道の蛇口から水が溜まるイメージ。
       長所: バースト対応可能、メモリ効率良い
       短所: パラメータ調整が必要(容量, 補充レート)

    2. Sliding Window Log:
       各リクエストのタイムスタンプをログに記録。
       ウィンドウ内のログ数がしきい値を超えたら拒否。
       長所: 正確
       短所: メモリ消費大(全タイムスタンプ保持)

    3. Sliding Window Counter:
       前ウィンドウと現ウィンドウのカウントを重み付き平均。
       長所: メモリ効率と精度のバランスが良い
       短所: 近似値

    4. Fixed Window Counter:
       時間ウィンドウごとにカウンター。最もシンプル。
       短所: ウィンドウ境界で2倍のリクエストが通る可能性
    """)

    p("""
    コード例 - Token Bucket:
    """)
    print("""
    class TokenBucket:
        def __init__(self, capacity, refill_rate):
            self.capacity = capacity       # バケツの容量
            self.tokens = capacity         # 現在のトークン数
            self.refill_rate = refill_rate  # 秒あたり補充数
            self.last_refill = time.monotonic()

        def allow(self):
            now = time.monotonic()
            elapsed = now - self.last_refill
            self.tokens = min(self.capacity,
                            self.tokens + elapsed * self.refill_rate)
            self.last_refill = now
            if self.tokens >= 1:
                self.tokens -= 1
                return True
            return False
    """)

    p("""
    分散環境での課題:
      複数サーバーがRate Limiterを持つと、各サーバーのカウントが独立。
      解決: Redis(中央集権型)でカウンターを共有。
      Redisが落ちたら？ → ローカルにフォールバック + 後でRedisと同期。
    """)

    interview("""
    「Token Bucketを採用します。理由はバーストトラフィックに対応でき、
    メモリ効率が良いためです。分散環境ではRedisでカウンターを共有し、
    LuaスクリプトでアトミックにRead-Modify-Writeします。
    Redisダウン時はローカルカウンターにフォールバックします」
    """)

    # --- KV Store ---
    heading("4.2 分散KVストア (Key-Value Store)")

    p("""
    先に結論: 「4.2 分散KVストア (Key-Value Store)」は実装と設計判断に直結する。背景とトレードオフを押さえる。
    問題: 高可用性・高スケーラビリティなKey-Valueストアを設計する。

    CAP定理:
      分散システムは以下の3つを同時に満たせない:
        C (Consistency): 全ノードが同じデータを返す
        A (Availability): 全リクエストに応答する
        P (Partition Tolerance): ネットワーク分断に耐える

      Pは避けられない(ネットワークは必ず壊れる)。
      よって実質的にはCP or APの選択。

      CP (例: HBase, ZooKeeper):
        分断時にデータが古い可能性があれば応答しない。
        → 用途: 銀行、在庫管理

      AP (例: Cassandra, DynamoDB):
        分断時も応答する。データが一時的に古い可能性あり。
        → 用途: SNS、ショッピングカート
    """)

    misconception(
        "CAP定理でCAを選べる",
        "ネットワーク分断(P)は必ず発生する。Pを捨てることはできない。"
        "よって選択肢はCPかAPの二択"
    )

    p("""
    Consistent Hashing:
      なぜ必要か: 通常のhash(key) % N はNが変わると全データ再配置。
      Consistent Hashingはリング上にノードを配置し、
      ノード追加/削除時に影響を受けるデータはK/N (K=データ数, N=ノード数)。

      仮想ノード: 1物理ノードに複数の仮想ノードを割り当て、
      データの偏りを軽減する。仮想ノード数は100〜200が目安。

    データレプリケーション:
      リング上で次のN個のユニーク物理ノードにレプリカを配置。
      N=3が一般的 (3台まで同時に壊れても耐える)。

    書き込み一貫性 (Quorum):
      W + R > N ならば強整合性を保証できる。
      W=2, R=2, N=3 → 強整合性
      W=1, R=1, N=3 → 高可用性(結果整合性)
    """)

    # --- Autocomplete ---
    heading("4.3 検索オートコンプリート")

    p("""
    先に結論: 「4.3 検索オートコンプリート」は実装と設計判断に直結する。背景とトレードオフを押さえる。
    問題: 検索ボックスにユーザーが文字を入力すると、候補を表示する。

    データ構造 - Trie (トライ木):
      各ノードが1文字を表す木構造。
      「cat」「car」「card」は先頭の「ca」を共有する。

      なぜTrieか:
        プレフィックス検索に最適化されている。
        「ca」と入力 → Trie上で「ca」ノードまで辿り、
        その配下の全単語が候補。
        時間計算量: O(p + n) (p=プレフィックス長, n=候補数)

    スケーリング:
      Trieはメモリに乗らないほど大きくなる場合:
        1. プレフィックスで分割 (a-m: サーバー1, n-z: サーバー2)
        2. 各ノードに上位K件だけ保持 (全候補は不要)
        3. CDNで頻出プレフィックスの結果をキャッシュ

    データ更新:
      リアルタイム更新は非効率。
      → バッチ処理: 検索ログを集計 → 定期的にTrie再構築
      → 人気度は時間減衰 (最近の検索ほど重み高い)
    """)

    interview("""
    「Trieをベースにし、各ノードにTop-K候補をキャッシュします。
    データ更新はリアルタイムではなくMapReduceバッチで1時間ごとに
    Trieを再構築。プレフィックスの先頭文字でシャーディングし、
    各シャードは異なるサーバーで処理します」
    """)


