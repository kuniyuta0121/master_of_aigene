def ch7_database():
    title("第7章: データベース選定")

    heading("7.1 SQL vs NoSQL")

    p("""
    先に結論: 「7.1 SQL vs NoSQL」は実装と設計判断に直結する。背景とトレードオフを押さえる。
    SQL (リレーショナルDB):
      MySQL, PostgreSQL, Oracle
      特徴: ACID保証、JOIN、スキーマ固定、正規化
      向いている用途: 複雑なクエリ、トランザクション、データ整合性が重要

    NoSQL:
      4種類に分かれる:

      1. Key-Value: Redis, DynamoDB
         → セッション管理、キャッシュ、シンプルなルックアップ

      2. Document: MongoDB, Couchbase
         → スキーマが柔軟、JSONドキュメント、CMS

      3. Column-Family: Cassandra, HBase
         → 大量の書き込み、時系列データ、IoT

      4. Graph: Neo4j, Neptune
         → ソーシャルネットワーク、推薦エンジン、関係性の探索
    """)

    p("""
    選定基準 (面接での回答フレームワーク):

    SQLを選ぶ場合:
      - データ間のリレーションが複雑 (JOINが多い)
      - ACIDトランザクションが必須 (決済、在庫)
      - データの構造が固定 (スキーマが安定)

    NoSQLを選ぶ場合:
      - スキーマが頻繁に変わる
      - 超大量の書き込み (数十万QPS)
      - 水平スケーリングが必須
      - データ間のリレーションが少ない
    """)

    misconception(
        "NoSQLはSQLより優れている",
        "優劣ではなくトレードオフ。NoSQLはJOINが苦手で、"
        "ACIDが弱い場合が多い。用途に応じて使い分ける"
    )

    heading("7.2 シャーディング戦略")

    p("""
    この節の狙い: 「7.2 シャーディング戦略」を単語暗記ではなく、なぜ必要かまでつながる形で理解する。
    シャーディングとは:
      データを複数のDBに分散配置する手法。
      1台のDBでは限界に達したときのスケーリング手段。

    Hash Sharding:
      shard_id = hash(key) % shard_count
      長所: データが均等に分散
      短所: シャード数変更時に大規模データ移動

    Range Sharding:
      例: user_id 1-100万 → shard1, 100万-200万 → shard2
      長所: 範囲クエリが効率的
      短所: ホットスポット (人気のある範囲に偏る)

    Consistent Hashing:
      リング上にノードを配置し、キーのハッシュ値で最寄りのノードに格納。
      長所: ノード追加/削除の影響が最小
      短所: 実装が複雑

    シャーディングキーの選び方:
      - カーディナリティが高い (値の種類が多い) ← 必須
      - アクセスパターンに合う (よく一緒にクエリされるデータは同一シャード)
      - ホットスポットを避ける
      悪い例: 日付でシャーディング → 今日のシャードだけ高負荷
      良い例: user_id でシャーディング → ユーザーのデータは同一シャード
    """)

    heading("7.3 レプリケーション")

    p("""
    まず全体像: この節では「7.3 レプリケーション」を、定義 -> 仕組み -> 実務上の判断の順で整理する。
    レプリケーションとは:
      同じデータのコピーを複数のサーバーに持つこと。
      目的: 可用性向上 + 読み取り性能向上

    同期レプリケーション:
      Masterが全Slaveへの書き込み完了を待つ。
      長所: データ一貫性が保証される
      短所: レイテンシが高い (最も遅いSlaveに律速)

    非同期レプリケーション:
      Masterは書き込み後すぐに応答。Slaveへは後で伝播。
      長所: 低レイテンシ
      短所: Master障害時にデータ消失の可能性

    半同期レプリケーション:
      N台中M台への書き込み完了で応答 (Quorum)。
      バランスの良い選択。W+R > N で一貫性保証。
    """)

    interview("""
    「このシステムはユーザーデータを扱うので、
    user_idでHash Shardingします。
    レプリケーションはW=2, R=2, N=3のQuorum構成で、
    強整合性を保証しつつ1台の障害に耐えます。
    シャード追加時のリバランシングにはConsistent Hashingを使います」
    """)


