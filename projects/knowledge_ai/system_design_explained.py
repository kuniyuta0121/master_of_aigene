def ch3_design_problems_1():
    title("第3章: 設計問題 - URL短縮 / Twitter / チャット")

    # --- URL Shortener ---
    heading("3.1 URL短縮サービス (TinyURL)")

    p("""
    先に結論: 「3.1 URL短縮サービス (TinyURL)」は実装と設計判断に直結する。背景とトレードオフを押さえる。
    問題: 長いURLを短縮し、短縮URLにアクセスすると元URLにリダイレクトする。

    Step 1 - 要件:
      FR: URL短縮、リダイレクト、(オプション)カスタムエイリアス
      NFR: DAU 1億、読み書き比率 100:1、低レイテンシ、高可用性

    Step 2 - 概算:
      書き込み: 1億 * 1 / 86400 ≈ 1,200 QPS
      読み取り: 1,200 * 100 = 120,000 QPS
      ストレージ(5年): 1億/日 * 365 * 5 * 500B ≈ 90TB

    Step 3 - 高レベル設計:
      Client → LB → App Server → DB (短縮URL→元URL)
                                  → Cache (Redis)

    Step 4 - 詳細設計:
      短縮コード生成方法:
        a) Base62エンコード (0-9, a-z, A-Z の62文字)
           7文字で 62^7 ≈ 3.5兆 通り。十分。
        b) MD5ハッシュの先頭7文字: 衝突の可能性あり
        c) 分散ID生成 (Snowflake) → Base62変換

      DB設計:
        テーブル: urls(short_code PK, long_url, created_at, expires_at)
        インデックス: short_code (主キー), long_url (重複チェック用)

      リダイレクト:
        301 (Permanent): ブラウザがキャッシュ → Analytics不正確
        302 (Temporary): 毎回サーバーに来る → Analytics正確
    """)

    p("""
    コード例 - Base62エンコード:
    """)
    print("""
    CHARS = "0123456789abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ"
    def to_base62(num):
        if num == 0: return CHARS[0]
        result = []
        while num > 0:
            result.append(CHARS[num % 62])
            num //= 62
        return "".join(reversed(result))
    # to_base62(123456789) → "8M0kX"
    """)

    misconception(
        "ハッシュ関数で短縮コードを作ればよい",
        "MD5は衝突が起きうる。衝突時のリトライロジックが必要。"
        "Auto-increment ID + Base62変換の方がシンプルで衝突ゼロ"
    )

    # --- Twitter Timeline ---
    heading("3.2 Twitter タイムライン (ニュースフィード)")

    p("""
    まず全体像: この節では「3.2 Twitter タイムライン (ニュースフィード)」を、定義 -> 仕組み -> 実務上の判断の順で整理する。
    問題: ユーザーがフォローしている人の投稿をタイムラインに表示する。

    2つのアプローチ:

    Fan-out on Write (Push モデル):
      投稿時にフォロワー全員のタイムラインキャッシュに書き込む。
      長所: 読み取りが超高速 (事前計算済み)
      短所: フォロワーが多い人(セレブ)の投稿で書き込み爆発
            1000万フォロワー → 1投稿で1000万回書き込み

    Fan-out on Read (Pull モデル):
      タイムライン表示時にフォロー中ユーザーの投稿をリアルタイム集約。
      長所: 書き込みは1回で済む
      短所: 読み取り時のレイテンシが高い

    ハイブリッド戦略 (Twitterの実際の設計):
      一般ユーザー → Fan-out on Write (Push)
      セレブ(フォロワー1万人以上) → Fan-out on Read (Pull)
      タイムライン表示時に両方をマージ

    データモデル:
      tweets: (tweet_id, user_id, content, created_at)
      followers: (follower_id, followee_id)
      timeline_cache: (user_id, tweet_id, score) ← Redisのソートセット
    """)

    interview("""
    「一般ユーザーにはPushモデルで事前書き込みし、
    フォロワー1万人以上のユーザーはPullモデルに切り替えます。
    これにより書き込み爆発を防ぎつつ、読み取りレイテンシを
    95%のケースで50ms以下に抑えられます」
    → 「なぜ1万人？」と聞かれたら「実測ベースで調整する閾値」と答える。
    """)

    # --- Chat System ---
    heading("3.3 チャットシステム (WhatsApp/Slack)")

    p("""
    先に結論: 「3.3 チャットシステム (WhatsApp/Slack)」は実装と設計判断に直結する。背景とトレードオフを押さえる。
    問題: 1対1チャットとグループチャットを支えるリアルタイムシステム。

    プロトコル選択:
      HTTP Polling: クライアントが定期的にサーバーに問い合わせ → 非効率
      Long Polling: サーバーが新メッセージまで接続を保持 → まだ非効率
      WebSocket: 双方向の常時接続 → チャットに最適
      SSE: サーバーからクライアントへの一方向 → 通知向き

    なぜWebSocketか:
      チャットは「サーバーからクライアントへのプッシュ」が必須。
      HTTPは「クライアントから聞かないと答えない」。
      WebSocketは一度接続すれば双方向にデータを送れる。
      電話のように「回線が繋がっている」イメージ。

    アーキテクチャ:
      WebSocket Server: 接続管理、メッセージ配信
      Chat Service: メッセージ保存、グループ管理
      Presence Service: オンライン状態管理
      Notification Service: オフラインユーザーへのプッシュ通知
      MQ (Kafka): サービス間のメッセージ連携

    メッセージ順序保証:
      同一チャットルーム → 同一Kafkaパーティション → 順序保証
      クライアント側でもタイムスタンプでソート
    """)

    misconception(
        "WebSocketは全通信に使うべき",
        "WebSocketはチャットのリアルタイム配信に使う。"
        "ログイン、プロフィール更新等は通常のHTTP APIでよい"
    )


