#!/usr/bin/env python3
"""
System Design Interview - 深掘り解説
"""
import textwrap

SEP = "=" * 70
THIN = "-" * 70

def title(text):
    print(f"\n{SEP}\n  {text}\n{SEP}")

def heading(text):
    print(f"\n{THIN}\n  {text}\n{THIN}")

def p(text):
    for line in textwrap.dedent(text).strip().split("\n"):
        print(f"  {line}")

def interview(text):
    print()
    print("  【面接向けに整理すると】")
    for line in textwrap.dedent(text).strip().split("\n"):
        print(f"  {line}")

def misconception(wrong, right):
    print(f"\n  ⚠ よくある誤解: {wrong}")
    print(f"  → 正確には: {right}")


