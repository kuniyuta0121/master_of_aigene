def ch6_estimation():
    title("第6章: 概算 (Back-of-Envelope Estimation)")

    p("""
    概算は面接の「数字で語る力」を見せる武器。
    電卓なしで、ホワイトボードに素早く書ける計算力が問われる。
    正確な値は不要。桁が合っていれば十分。
    """)

    heading("6.1 QPS (Queries Per Second) 計算")

    p("""
    まず全体像: この節では「6.1 QPS (Queries Per Second) 計算」を、定義 -> 仕組み -> 実務上の判断の順で整理する。
    基本公式:
      QPS = DAU × (1ユーザーあたりのリクエスト数) / 86,400
      ピークQPS = 平均QPS × 2〜3 (経験則)

    例 - Twitter:
      DAU = 3億
      平均ツイート = 2/日, タイムライン閲覧 = 20/日
      書き込みQPS = 3億 × 2 / 86,400 ≈ 7,000
      読み取りQPS = 3億 × 20 / 86,400 ≈ 70,000
      ピーク読み取りQPS ≈ 140,000
    """)

    heading("6.2 ストレージ見積もり")

    p("""
    まず全体像: この節では「6.2 ストレージ見積もり」を、定義 -> 仕組み -> 実務上の判断の順で整理する。
    公式:
      1日のデータ量 = 書き込みQPS × 86,400 × 1レコードのサイズ
      5年分 = 1日のデータ量 × 365 × 5

    例 - URL短縮サービス:
      書き込みQPS = 1,200
      1レコード = 500 bytes (URL + メタデータ)
      1日 = 1,200 × 86,400 × 500 ≈ 50 GB
      5年 = 50 × 365 × 5 ≈ 90 TB
    """)

    heading("6.3 帯域幅")

    p("""
    先に結論: 「6.3 帯域幅」は実装と設計判断に直結する。背景とトレードオフを押さえる。
    公式:
      帯域幅 = QPS × 平均レスポンスサイズ

    例 - 動画配信:
      同時視聴者 = 100万
      平均ビットレート = 5 Mbps
      帯域幅 = 100万 × 5 Mbps = 5 Tbps
      → CDNで分散しないと不可能な規模
    """)

    heading("6.4 サーバー台数")

    p("""
    まず全体像: この節では「6.4 サーバー台数」を、定義 -> 仕組み -> 実務上の判断の順で整理する。
    公式:
      サーバー台数 = ピークQPS / 1サーバーあたりQPS

    目安:
      Webサーバー (Node.js/Go): 10,000〜50,000 QPS
      APIサーバー (Python/Java): 1,000〜10,000 QPS
      DBサーバー (MySQL): 1,000〜5,000 QPS

    例:
      ピークQPS = 140,000
      1サーバー = 10,000 QPS
      → 14台 + 冗長分30% = 約20台
    """)

    p("""
    コード例 - 概算計算テンプレート:
    """)
    print("""
    def estimate(dau, req_per_user, data_bytes, rw_ratio=10):
        daily_req = dau * req_per_user
        avg_qps = daily_req / 86400
        peak_qps = avg_qps * 2
        write_qps = avg_qps / (1 + rw_ratio)
        read_qps = avg_qps - write_qps
        storage_5y = daily_req * data_bytes * 365 * 5
        print(f"平均QPS: {avg_qps:,.0f}")
        print(f"ピークQPS: {peak_qps:,.0f}")
        print(f"5年ストレージ: {storage_5y / 1e12:.1f} TB")

    # URL Shortener: DAU 1億, 1リクエスト/日, 500B, 読み書き100:1
    estimate(100_000_000, 1, 500, 100)
    """)

    interview("""
    概算は30秒〜1分で終わらせる。桁だけ合えばよい。
    面接官に「ストレージは5年で約100TBです。
    シャーディングが必要になります」と結論を先に言い、
    その後で計算過程を見せる。
    """)


