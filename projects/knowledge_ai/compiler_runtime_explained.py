def chapter7_jit():
    title("第7章: JITコンパイル")

    heading("7.1 JITとは何か")
    p("""
    この節の狙い: 「7.1 JITとは何か」を単語暗記ではなく、なぜ必要かまでつながる形で理解する。
    JIT（Just-In-Time）コンパイル = 実行時にコードをコンパイルする技術。

    AOT（Ahead-Of-Time）との対比:
      AOT: ソース → コンパイル → 実行ファイル → 実行
           例: C, C++, Rust, Go
      JIT: ソース → バイトコード → 実行しながらホットコードをコンパイル
           例: Java (HotSpot), JavaScript (V8), Python (PyPy)

    なぜJITが強力か:
    1. プロファイル情報を使える（この型が99%来る → 特殊化）
    2. 実行時に分かることで最適化できる（仮想呼び出しの解除等）
    3. プラットフォーム固有の最適化が自動（AVX命令の利用等）
    """)

    heading("7.2 ティアードコンパイル")
    p("""
    まず全体像: この節では「7.2 ティアードコンパイル」を、定義 -> 仕組み -> 実務上の判断の順で整理する。
    JVM HotSpotの4段階:

    Tier 0: インタプリタ
      → バイトコードを1命令ずつ実行。プロファイルデータを収集。

    Tier 1-3: C1コンパイラ（ベースライン）
      → 高速にネイティブコードを生成。軽い最適化のみ。

    Tier 4: C2コンパイラ（最適化）
      → 時間をかけて高度な最適化。インライン展開、ループ展開等。

    遷移の判断基準:
    - メソッド呼び出し回数（invocation counter）
    - ループの反復回数（backedge counter）

    閾値の例:
      Tier 0 → Tier 1: 約1,500回の呼び出し
      Tier 1 → Tier 4: 約10,000回の呼び出し

    V8 (JavaScript):
      Ignition (インタプリタ) → TurboFan (最適化JIT)
      2段階のシンプルな構成。
    """)

    heading("7.3 トレーシングJIT")
    p("""
    まず全体像: この節では「7.3 トレーシングJIT」を、定義 -> 仕組み -> 実務上の判断の順で整理する。
    メソッドJIT: メソッド単位でコンパイルする（HotSpot, V8）
    トレーシングJIT: ホットなループの「実行パス」を記録してコンパイル

    トレーシングJITの仕組み:
    1. ホットループを検出
    2. 1回の実行を「トレース」（分岐結果も含めて記録）
    3. トレースをネイティブコードにコンパイル
    4. 次回実行時、同じパスならネイティブコードを使う
    5. 違うパスに入ったら「ガード失敗」→ フォールバック

    長所: ループに特化した最適化が可能、動的言語に強い
    短所: パスが多いと多数のトレースが必要

    採用: PyPy（Python）, LuaJIT（最速のスクリプト言語実装の1つ）
    """)

    heading("7.4 ウォームアップ問題")
    p("""
    まず全体像: この節では「7.4 ウォームアップ問題」を、定義 -> 仕組み -> 実務上の判断の順で整理する。
    JITは「最初は遅い」問題がある。

    コールドスタート:
      起動直後はインタプリタで実行 → 遅い
      プロファイルデータが溜まるまで待つ → JITコンパイル → 速くなる

    ウォームアップ時間の例（概算）:
      JVM HotSpot: 数秒〜数十秒
      V8:          100ms〜数秒
      PyPy:        数秒

    対策:
    1. AOT + JIT のハイブリッド（GraalVM Native Image）
    2. プロファイルの事前収集（JVM の -XX:+UseSharedSpaces）
    3. 定常状態の性能を計測する（マイクロベンチマークの注意点）

    サーバレス（AWS Lambda等）ではウォームアップが特に問題。
    → GraalVM Native Image や Go/Rust の採用が増えている理由の1つ。
    """)

    heading("7.5 脱最適化（Deoptimization）")
    p("""
    まず全体像: この節では「7.5 脱最適化（Deoptimization）」を、定義 -> 仕組み -> 実務上の判断の順で整理する。
    JITは「仮定」に基づいて最適化する。仮定が崩れたら？

    例:
      function add(a, b) { return a + b; }
      // 99%の呼び出しで a, b は整数
      // JITは「整数加算」に特殊化

      add("hello", "world")  // 文字列が来た！仮定が崩れた

    脱最適化のステップ:
    1. 最適化コード内の「ガードチェック」が失敗
    2. 最適化コードのスタックフレームをインタプリタのフレームに変換
    3. インタプリタで実行を継続
    4. 新しいプロファイルを収集して再コンパイル

    注意: 脱最適化が頻発するとJITの恩恵が消える。
    実務での対策:
    - 型を混ぜない（数値とオブジェクトを同じ変数に入れない）
    - 多態的なコールサイトを避ける（monomorphicが最速）
    """)

    heading("7.6 インラインキャッシュ")
    p("""
    この節の狙い: 「7.6 インラインキャッシュ」を単語暗記ではなく、なぜ必要かまでつながる形で理解する。
    動的言語の最大のボトルネック: メソッド解決のオーバーヘッド。

    obj.method() を呼ぶとき:
      1. obj の型を調べる
      2. 型のメソッドテーブルを検索
      3. メソッドを呼び出す
    → 毎回これをやると遅い

    インラインキャッシュ:
      コールサイトに「前回の型とメソッド」をキャッシュ。

    状態遷移:
      未初期化 → モノモルフィック → ポリモルフィック → メガモルフィック

    モノモルフィック (1型):  型チェック1回 → 直接呼び出し（最速）
    ポリモルフィック (2-4型): 線形探索でキャッシュ確認
    メガモルフィック (5型以上): キャッシュ諦め → 毎回フル検索（最遅）

    実務的な影響:
      // モノモルフィック（高速）
      shapes.forEach(s => s.draw())  // shapes が全部 Circle なら

      // メガモルフィック（低速）
      shapes.forEach(s => s.draw())  // Circle, Square, Triangle, ...
    """)

    misconception(
        "JITがあればAOTコンパイラより必ず速い",
        "JITはプロファイル情報で有利だが、ウォームアップコスト、"
        "メモリ消費（コンパイラ自体がメモリを使う）、コンパイル時間の"
        "オーバーヘッドがある。数値計算ならC/Rust(AOT)がまだ速い。"
        "定常状態のサーバアプリならJITが有利な場合もある。"
    )

    interview("""
    Q: V8はJavaScriptをどう実行する？
    A: Parse → AST → Ignition（バイトコードインタプリタ、型フィードバック収集）
       → TurboFan（最適化JIT、Sea of Nodes IRを使用）
       → 型仮定が崩れたらDeoptimize（インタプリタに戻る）。
       プロパティアクセスやメソッド呼び出しにはインラインキャッシュを使用。
       Hidden Class（隠しクラス）でオブジェクトのプロパティレイアウトを最適化。
    """)


