def chapter2_parser():
    title("第2章: パーサー（構文解析）")

    heading("2.1 文脈自由文法とBNF")
    p("""
    この節の狙い: 「2.1 文脈自由文法とBNF」を単語暗記ではなく、なぜ必要かまでつながる形で理解する。
    パーサーはトークン列を木構造（構文木/AST）に変換する。
    そのルールを記述するのが「文脈自由文法（CFG）」。

    BNF（バッカス・ナウア記法）で書くとこうなる:

      <expr>   ::= <term> (('+' | '-') <term>)*
      <term>   ::= <factor> (('*' | '/') <factor>)*
      <factor> ::= NUMBER | '(' <expr> ')' | IDENT

    この文法が表現していること:
    - 掛け算は足し算より優先される（termが先にグループ化）
    - 括弧で優先順位を変えられる
    - 再帰的な構造（exprの中にexprが入る）

    たとえば "1 + 2 * 3" は:
      expr → term(1) + term(2*3)
      → 1 + (2 * 3) = 7   （2 * 3 が先に計算される）
    """)

    heading("2.2 再帰下降パーサー")
    p("""
    この節の狙い: 「2.2 再帰下降パーサー」を単語暗記ではなく、なぜ必要かまでつながる形で理解する。
    最もシンプルなパーサー。文法規則1つ = 関数1つ。

    例: <expr> ::= <term> (('+' | '-') <term>)*

      def parse_expr():
          left = parse_term()
          while current_token in ('+', '-'):
              op = consume()
              right = parse_term()
              left = BinaryOp(op, left, right)
          return left

    長所: 読みやすい、デバッグしやすい、エラーメッセージを出しやすい
    短所: 優先順位レベルごとに関数が必要（10段階なら10関数）
    採用: GCC(C), CPython, TypeScript, Go

    注意: 左再帰のある文法はそのまま書くと無限ループになる。
      <expr> ::= <expr> '+' <term>   ← これは左再帰（NG）
      <expr> ::= <term> ('+' <term>)* ← こう書き換える
    """)

    heading("2.3 Prattパーサー（演算子順位解析）")
    p("""
    まず全体像: この節では「2.3 Prattパーサー（演算子順位解析）」を、定義 -> 仕組み -> 実務上の判断の順で整理する。
    Prattパーサー（TDOP: Top-Down Operator Precedence）は
    再帰下降の弱点を解消する洗練された手法。

    核心的なアイデア:
    - 各トークンに「結合力」（binding power = 優先順位の数値）を持たせる
    - nud: トークンが「先頭」に来た時の処理（リテラル、前置演算子）
    - led: トークンが「中置」に来た時の処理（二項演算子）

      def parse_expr(min_prec=0):
          left = nud()              # 先頭トークンを処理
          while prec(peek()) > min_prec:
              op = advance()
              right = parse_expr(prec(op))  # 右辺を再帰的にパース
              left = BinaryOp(op, left, right)
          return left

    長所: 演算子の追加 = 1行追加するだけ。文法規則の書き換え不要。
    短所: 慣れないと直感的でない
    採用: V8(JavaScript), Lua, rustc(部分的)
    """)

    heading("2.4 LL(k) と LR(k)")
    p("""
    まず全体像: この節では「2.4 LL(k) と LR(k)」を、定義 -> 仕組み -> 実務上の判断の順で整理する。
    パーサーの理論的な分類:

    LL(k): Left-to-right, Leftmost derivation, k tokens lookahead
      - トップダウン（再帰下降はLL(1)の手書き実装）
      - ANTLR が生成する（LL(*)）
      - 直感的だが、左再帰を処理できない

    LR(k): Left-to-right, Rightmost derivation, k tokens lookahead
      - ボトムアップ（shift-reduce）
      - yacc/bison が生成する（LALR(1)）
      - 左再帰OK、より広い文法クラスを受理できる
      - だがエラーメッセージが分かりにくい

    実務での選択:
      手書き再帰下降 → ほとんどのプロダクションコンパイラ
      PEG (Parsing Expression Grammar) → 新しい言語で人気
      Pratt → 式のパースに特化
      LR → レガシーツール（yacc系）
    """)

    misconception(
        "パーサージェネレータ（yacc等）を使うのが標準",
        "現代のメジャーなコンパイラはほぼ全て手書きパーサーを採用。"
        "理由はエラーメッセージの品質とインクリメンタルパースへの対応。"
    )

    interview("""
    Q: ASTとCST（具象構文木）の違いは？
    A: CSTはソースコードの文法構造をそのまま反映する（括弧等も含む）。
       ASTは意味的に不要なノードを除去した「抽象的な」木。
       コンパイラはAST上で型チェックや最適化を行う。
       Prettier等のフォーマッタはCSTを使う（空白情報が必要）。
    """)


