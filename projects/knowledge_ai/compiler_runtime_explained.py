def chapter5_codegen():
    title("第5章: コード生成")

    heading("5.1 レジスタ割り当て")
    p("""
    先に結論: 「5.1 レジスタ割り当て」は実装と設計判断に直結する。背景とトレードオフを押さえる。
    IR上では仮想レジスタは無制限に使えるが、
    実際のCPUのレジスタ数は有限（x86-64で16個の汎用レジスタ）。

    レジスタ割り当て = 仮想レジスタを物理レジスタに対応づける問題。
    これはグラフ彩色問題と等価であり、NP困難。

    2つの主要アルゴリズム:

    【グラフ彩色法】(Chaitin, 1981)
    1. 干渉グラフを作る: 同時に生きている変数間に辺を引く
    2. 簡約: 次数 < K（レジスタ数）のノードをスタックに積む
    3. 着色: スタックから取り出して色（レジスタ）を割り当てる
    4. スピル: 着色できなければメモリ（スタック）に退避

    例: a, b, c が同時に生きている → 3色必要
        レジスタが2個しかなければ1つはスピル（メモリに格納）

    【線形スキャン法】(Poletto, 1999)
    1. 各変数の生存区間（start, end）を計算
    2. 開始時刻順にソート
    3. 左から右にスキャンし、貪欲にレジスタを割り当て
    4. レジスタ不足時は最も遅く終わる変数をスピル

    使い分け:
      グラフ彩色 → AOTコンパイラ (GCC, LLVM) - 品質重視
      線形スキャン → JITコンパイラ (V8, HotSpot C1) - 速度重視
    """)

    heading("5.2 命令選択")
    p("""
    まず全体像: この節では「5.2 命令選択」を、定義 -> 仕組み -> 実務上の判断の順で整理する。
    IR命令を実際のCPU命令にマッピングする。

    例: t1 = a + b

    x86-64の場合:
      mov rax, [rbp-8]     ; a をレジスタにロード
      add rax, [rbp-16]    ; b を加算
      mov [rbp-24], rax    ; 結果を t1 に格納

    ARM64の場合:
      ldr x0, [sp, #8]     ; a をロード
      ldr x1, [sp, #16]    ; b をロード
      add x0, x0, x1       ; 加算
      str x0, [sp, #24]    ; 結果を格納

    単純なパターンマッチングから、ツリーパターンマッチング（BURS）、
    さらにはスーパーオプティマイザ（最短の命令列を探索）まで
    さまざまな手法がある。

    CISC（x86）: 1命令で複雑な操作が可能 → 命令選択が重要
    RISC（ARM）: 命令が単純で均一 → 命令選択は比較的容易
    """)

    heading("5.3 命令スケジューリング")
    p("""
    先に結論: 「5.3 命令スケジューリング」は実装と設計判断に直結する。背景とトレードオフを押さえる。
    現代のCPUはパイプライン化されており、命令の順序が性能に影響する。

    問題: メモリアクセス後すぐにその結果を使うと「ストール」する

      ldr x0, [mem]       ; メモリロード（数サイクルかかる）
      add x1, x0, #1      ; ← x0 の結果を待つ必要がある（ストール）

    解決: 独立した命令を間に挟む

      ldr x0, [mem_a]      ; a をロード
      ldr x2, [mem_b]      ; b をロード（a と独立）
      add x1, x0, #1       ; a の結果を使う（ロード完了済み）
      add x3, x2, #2       ; b の結果を使う

    コンパイラの命令スケジューラが依存関係グラフを作り、
    ストールを最小化する命令順序を計算する。
    """)

    misconception(
        "レジスタ割り当ては最適解を求めている",
        "NP困難なのでヒューリスティクスを使う。実用上はグラフ彩色法で"
        "十分良い結果が得られる。JITでは品質より速度が重要なので"
        "線形スキャンを使い、最適性は諦める。"
    )

    interview("""
    Q: スピル（spill）とは？
    A: レジスタが足りない時に変数をメモリ（スタック）に退避すること。
       レジスタアクセスは1サイクル、メモリアクセスは100サイクル以上
       かかるので、スピルは性能に大きく影響する。
       良いレジスタ割り当ては、頻繁にアクセスする変数をスピルしない。
    """)


