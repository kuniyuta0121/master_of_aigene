def chapter1_lexer():
    title("第1章: レキサー（字句解析）")

    heading("1.1 字句解析とは何か")
    p("""
    まず全体像:
    コンパイラは、ソースコードをいきなり意味解析しない。
    最初に「文字列」を「意味のある最小単位」に分解する工程が必要になる。
    それが字句解析（lexical analysis）。

    たとえば人間が文章を読むときも、まず単語に区切ってから意味を取る。
    レキサーはこの「単語分割」をプログラミング言語向けに厳密化したもの。

    例:
      入力: "let x = 42 + y"
      出力: [LET, IDENT("x"), EQ, NUMBER(42), PLUS, IDENT("y")]

    トークンは通常、次の3点を持つ:
    - 種類（IDENT, NUMBER, PLUS など）
    - 値（"x", 42 など）
    - 位置（行・列・オフセット）

    なぜ位置が重要か:
    エラー報告で「どこが悪いか」を正確に示せるから。
    良いコンパイラは、正しいコードを通すだけでなく、
    誤ったコードに対して修正しやすい診断を返せる。
    """)

    heading("1.2 なぜ正規表現を使うのか")
    p("""
    先に結論: 「1.2 なぜ正規表現を使うのか」は実装と設計判断に直結する。背景とトレードオフを押さえる。
    トークンのパターンは「正規言語」で表現できる。
    正規言語 = 有限オートマトン（DFA/NFA）で認識できる言語。

    つまり:
      数値リテラル: \\d+(\\.\\d+)?     → 42, 3.14
      識別子:       [a-zA-Z_]\\w*      → foo, _bar, x1
      文字列:       "([^"\\\\]|\\\\.)*"  → "hello", "a\\nb"
      演算子:       ==|!=|<=|>=|\\+|-   → ==, +, -

    正規表現 → NFA → DFA の変換は自動化できる。
    lex/flexなどのツールがこれを行う。

    重要: 正規表現では「括弧の対応」は表現できない。
    それはパーサーの仕事（文脈自由文法が必要）。
    """)

    heading("1.3 有限オートマトン（DFA/NFA）")
    p("""
    この節の狙い: 「1.3 有限オートマトン（DFA/NFA）」を単語暗記ではなく、なぜ必要かまでつながる形で理解する。
    有限オートマトンは「状態」と「遷移」で構成される機械。

    NFA（非決定性）: 1つの入力に対して複数の遷移先がありうる
    DFA（決定性）:   1つの入力に対して遷移先は常に1つ

    例: 数値の認識DFA

      状態:  [開始] --数字--> [整数部] --'.'-->  [小数点]  --数字--> [小数部]
                                  |                                    |
                                  +--- (受理: 整数)            (受理: 小数)

    Thompson構成法: 正規表現 → NFA
    部分集合構成法: NFA → DFA
    DFA最小化:     DFA → 最小DFA

    実用上のポイント:
    - DFAは高速（O(n)）だがメモリ消費が大きくなりうる
    - NFAはメモリ効率的だがバックトラックでO(2^n)になりうる
    - Pythonのreモジュールは内部的にNFAベース
    """)

    heading("1.4 実装のポイント: 最長一致と優先順位")
    p("""
    この節の狙い: 「1.4 実装のポイント: 最長一致と優先順位」を単語暗記ではなく、なぜ必要かまでつながる形で理解する。
    レキサーの2つの重要なルール:

    1. 最長一致（Maximal Munch）:
       "==" を見たら "=" + "=" ではなく "==" として認識する。
       だからパターンの定義順序が重要:
         "==" を "=" より先に定義する必要がある。

    2. キーワードと識別子の区別:
       "if" は識別子？キーワード？
       → まず識別子としてマッチし、キーワードテーブルで後から判定する。
       これが最もシンプルで拡張しやすい方法。

    実際のソースでの実装:
      TOKEN_SPEC = [
          (r'==',  TokenType.EQEQ),    # == を = より先に
          (r'=',   TokenType.EQ),
          ...
          (r'[a-zA-Z_]\\w*', TokenType.IDENTIFIER),  # 後でキーワード判定
      ]
    """)

    misconception(
        "レキサーは正規表現だけで実装する",
        "実際のコンパイラ（GCC, Clang, V8）は手書きレキサーが多い。"
        "理由: (1)エラーメッセージの品質 (2)パフォーマンス (3)文脈依存の処理"
    )

    heading("1.5 実世界のレキサー")
    p("""
    この節の狙い: 「1.5 実世界のレキサー」を単語暗記ではなく、なぜ必要かまでつながる形で理解する。
    言語ごとの特殊事情:

    Python: インデントが意味を持つ → レキサーがINDENT/DEDENTトークンを生成
            (他の言語にはないユニークな設計)

    JavaScript: 正規表現リテラル /pattern/ と除算 a/b の区別
                → パーサーの状態をレキサーが参照する必要がある

    Rust: ライフタイム 'a とchar 'x' の区別
          raw string r#"..."# のネスト

    C/C++: プリプロセッサ(#include, #define)がレキサーの前に動く
           → 実質2段階の字句解析
    """)

    interview("""
    Q: レキサーとパーサーの違いは？
    A: レキサーは「文字列→トークン列」（正規言語）。
       パーサーは「トークン列→構文木」（文脈自由言語）。
       レキサーは「単語」を切り出し、パーサーは「文法構造」を解析する。
       分離する理由は (1)関心の分離 (2)パフォーマンス (3)エラー処理。
    """)


