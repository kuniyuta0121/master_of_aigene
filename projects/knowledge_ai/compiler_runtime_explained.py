def chapter3_type_inference():
    title("第3章: AST & 型推論")

    heading("3.1 抽象構文木（AST）")
    p("""
    先に結論: 「3.1 抽象構文木（AST）」は実装と設計判断に直結する。背景とトレードオフを押さえる。
    ASTはソースコードの「意味的な構造」を表す木。

    例: "let x = 1 + 2 * 3"

      LetBinding("x")
        └─ BinaryOp("+")
             ├─ Number(1)
             └─ BinaryOp("*")
                  ├─ Number(2)
                  └─ Number(3)

    ASTの用途はコンパイルだけではない:
    - リンター (ESLint, Pylint): ASTパターンマッチでバグ検出
    - フォーマッタ (Prettier, Black): ASTから整形済みコードを生成
    - トランスパイラ (Babel, TypeScript): AST変換 → コード生成
    - IDE機能: 「定義にジャンプ」「リネーム」はAST解析
    - コードレビューツール: AST差分で意味的な変更を検出
    """)

    heading("3.2 型システムの分類")
    p("""
    先に結論: 「3.2 型システムの分類」は実装と設計判断に直結する。背景とトレードオフを押さえる。
    型システムは「プログラムが実行前に安全か検査する」仕組み。

    軸1: 静的型付け vs 動的型付け
      静的: コンパイル時に型エラーを検出（Java, Rust, TypeScript）
      動的: 実行時に型チェック（Python, JavaScript, Ruby）

    軸2: 構造的型付け vs 名前的型付け
      構造的: 「形」が同じなら同じ型（TypeScript, Go interface）
      名前的: 「名前」が同じなら同じ型（Java, C#）

      // TypeScript（構造的）
      interface A { x: number }
      interface B { x: number }
      let a: A = { x: 1 };
      let b: B = a;  // OK! 構造が同じだから

      // Java（名前的）
      class A { int x; }
      class B { int x; }
      A a = new A();
      B b = a;  // エラー! 名前が違うから

    軸3: 漸進的型付け（Gradual Typing）
      型ありと型なしのコードを混在させる（Python + mypy, TypeScript）
    """)

    heading("3.3 Hindley-Milner型推論")
    p("""
    先に結論: 「3.3 Hindley-Milner型推論」は実装と設計判断に直結する。背景とトレードオフを押さえる。
    Hindley-Milner（HM）は「型注釈なしで型を推論する」アルゴリズム。
    ML, Haskell, Rust（部分的）, TypeScript（部分的）で使われる。

    核心的なアイデア: 「制約を集めて、統一的に解く」

    Algorithm W の手順:
    1. 未知の型に「型変数」(t1, t2, ...)を割り当てる
    2. ASTを走査して型制約を生成する
    3. 単一化（unification）で制約を解く

    例: "let f = fn(x) => x + 1"
      - x の型は不明 → t1 とする
      - 1 は Int
      - x + 1 の + は (Int, Int) -> Int を要求
      - よって t1 = Int （制約から推論）
      - f の型は Int -> Int
    """)

    heading("3.4 単一化（Unification）")
    p("""
    この節の狙い: 「3.4 単一化（Unification）」を単語暗記ではなく、なぜ必要かまでつながる形で理解する。
    単一化は「2つの型を同じにする代入（置換）を見つける」アルゴリズム。

    例:
      unify(t1, Int)           → {t1 := Int}
      unify(t1 -> t2, Int -> Bool) → {t1 := Int, t2 := Bool}
      unify(Int, Bool)         → エラー!（型の不一致）
      unify(t1, List[t1])      → エラー!（occurs check: 無限型の防止）

    Robinsonの単一化アルゴリズム:
      1. 型変数 vs 何か → その変数を「何か」に束縛（occurs checkを忘れずに）
      2. 同じ型構築子 → 引数を再帰的に単一化
      3. 異なる型構築子 → エラー

    occurs check（出現検査）が必要な理由:
      t1 = List[t1] だと t1 = List[List[List[...]]] と無限になる。
      これを検出して拒否する。
    """)

    misconception(
        "型推論があれば型注釈は不要",
        "実際にはRustでも関数シグネチャには型注釈が必要。"
        "理由: (1)ドキュメントとして機能 (2)推論の限界 (3)コンパイル速度。"
        "HMは「関数本体内」の推論に使い、境界は明示するのがベストプラクティス。"
    )

    heading("3.5 各言語の型推論レベル")
    p("""
    まず全体像: この節では「3.5 各言語の型推論レベル」を、定義 -> 仕組み -> 実務上の判断の順で整理する。
    言語          型システム         推論の範囲
    ──────────────────────────────────────────────
    Python        動的(gradual)     なし (mypy は外部ツール)
    JavaScript    動的              なし
    TypeScript    構造的            ローカル + 文脈的
    Java/C#       名前的            限定 (var, ダイヤモンド<>)
    Go            構造的(interface) 限定 (:= 短縮宣言)
    Rust          名前的+trait      ローカル (関数本体内)
    Haskell/ML    HM+拡張           グローバル (プログラム全体)
    """)

    interview("""
    Q: TypeScriptの型システムの特徴は？
    A: 構造的型付け + 漸進的型付け。型の互換性は「形」で判断する。
       Union型, Intersection型, Conditional型など表現力が非常に高い。
       型推論はローカル（関数引数の型は推論しない）。
       any型で型チェックを部分的にオフにできる（漸進的）。
    """)


