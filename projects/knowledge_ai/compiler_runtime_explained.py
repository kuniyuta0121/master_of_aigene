def chapter6_gc():
    title("第6章: GCアルゴリズム")

    heading("6.1 なぜGCが必要か")
    p("""
    先に結論: 「6.1 なぜGCが必要か」は実装と設計判断に直結する。背景とトレードオフを押さえる。
    手動メモリ管理（malloc/free, new/delete）の問題:
    - メモリリーク: freeし忘れ
    - ダングリングポインタ: free後にアクセス
    - 二重解放: 同じメモリを2回free

    解決策は3つ:
    1. GC（自動解放）: Java, Python, Go, JavaScript
    2. 所有権システム: Rust（コンパイル時に解放タイミングを決定）
    3. 手動管理: C, C++（プログラマの責任）

    GCの基本原理: 「到達可能なオブジェクトは生きている」
    GCルート（スタック変数、グローバル変数、レジスタ）から
    参照をたどって到達できないオブジェクトはゴミ。
    """)

    heading("6.2 Mark-Sweep")
    p("""
    先に結論: 「6.2 Mark-Sweep」は実装と設計判断に直結する。背景とトレードオフを押さえる。
    最もシンプルなGCアルゴリズム。2フェーズ:

    マークフェーズ:
      GCルートから深さ優先探索で全ての到達可能オブジェクトに印をつける。

    スイープフェーズ:
      ヒープ全体をスキャンし、印のないオブジェクトを解放する。

    ルート → A → B → C
                         D（到達不可 → 解放）

    長所: サイクル（循環参照）を処理できる、シンプル
    短所: Stop-the-World（GC中はアプリ停止）、断片化
    """)

    heading("6.3 Copying GC（コピーGC）")
    p("""
    先に結論: 「6.3 Copying GC（コピーGC）」は実装と設計判断に直結する。背景とトレードオフを押さえる。
    ヒープを2つの半空間（from-space / to-space）に分ける。

    1. from-space にオブジェクトを割り当てる
    2. from-space が満杯になったら:
       - 生きているオブジェクトを to-space にコピー
       - from と to を交換

    Cheneyのアルゴリズム: BFS（幅優先）でコピー。
    スキャンポインタとコピーポインタの2つだけで実装できる。

    長所:
    - 断片化しない（コピー時にコンパクション）
    - 割り当てが超高速（ポインタバンプ: ptr += size）
    - 生きているオブジェクトだけ処理（死んだオブジェクトはノーコスト）

    短所:
    - ヒープの50%しか使えない
    - 生存率が高いと大量コピーが発生
    """)

    heading("6.4 世代別GC（Generational GC）")
    p("""
    この節の狙い: 「6.4 世代別GC（Generational GC）」を単語暗記ではなく、なぜ必要かまでつながる形で理解する。
    「世代仮説」: ほとんどのオブジェクトは若くして死ぬ。

    この観察に基づき、ヒープを世代に分ける:
      Young世代（Nursery）: 小さい、頻繁にGC（Minor GC）
      Old世代（Tenured）:   大きい、たまにGC（Major GC）

    オブジェクトのライフサイクル:
      1. Young世代に割り当て
      2. Minor GCで生き残ったら「生存カウント」を増やす
      3. N回生き残ったらOld世代に「昇格（promote）」
      4. Old世代はMajor GC（Full GC）で回収

    ライトバリア（Write Barrier）:
      Old世代のオブジェクトがYoung世代を参照する場合、
      Minor GCでその参照を見逃さないように「記憶集合」に記録する。
      → Minor GCのルートに記憶集合を追加する。
    """)

    heading("6.5 G1 GC")
    p("""
    先に結論: 「6.5 G1 GC」は実装と設計判断に直結する。背景とトレードオフを押さえる。
    G1（Garbage First）: JVM のデフォルトGC（Java 9以降）。

    特徴:
    - ヒープを等サイズの「リージョン」に分割（連続でなくてよい）
    - 各リージョンは Eden / Survivor / Old / Humongous のどれか
    - ゴミの多いリージョンから優先的に回収（Garbage First）
    - 停止時間の目標を設定可能（-XX:MaxGCPauseMillis=200）

    動作:
    1. Young GC: Edenリージョンを回収（Stop-the-World）
    2. 並行マーキング: Old領域をバックグラウンドでマーク
    3. Mixed GC: Youngの一部 + Oldの一部を同時に回収

    改善点: 従来の世代別GCより停止時間が予測可能。
    """)

    heading("6.6 ZGC と Shenandoah")
    p("""
    先に結論: 「6.6 ZGC と Shenandoah」は実装と設計判断に直結する。背景とトレードオフを押さえる。
    超低レイテンシGC。停止時間 < 10ms（ヒープサイズに依存しない）。

    【ZGC】
    - カラードポインタ: ポインタのビットにGCメタデータを埋め込む
      (marked0, marked1, remapped, finalizable)
    - ロードバリア: 参照を読み取る時にポインタを修正
    - 全工程が並行: マーキング、リロケーション、参照処理
    - リージョンベース: Small(2MB), Medium(32MB), Large(N*2MB)

    【Shenandoah】
    - Brooks転送ポインタ: オブジェクトごとに1ワード追加
    - 並行コンパクション: 転送ポインタ経由でリダイレクト
    - ZGCと同等の停止時間、異なるメカニズム

    比較:
      GC         並行マーク  並行コンパクト  バリア種類
      ─────────────────────────────────────────────────
      G1         Yes        部分的          Write(SATB)
      ZGC        Yes        Yes            Load
      Shenandoah Yes        Yes            Load+Write
      Serial/CMS CMSのみ    No             Write
    """)

    heading("6.7 参照カウント（Reference Counting）")
    p("""
    この節の狙い: 「6.7 参照カウント（Reference Counting）」を単語暗記ではなく、なぜ必要かまでつながる形で理解する。
    各オブジェクトが「何箇所から参照されているか」をカウント。
    カウントが0になったら即座に解放。

    CPythonのメモリ管理:
      参照カウント（メイン）+ サイクル検出器（補助）

    参照カウントの長所:
    - 即座に解放（予測可能な解放タイミング）
    - Stop-the-Worldがない
    - 実装がシンプル

    参照カウントの短所:
    - 循環参照を処理できない
       A → B → A （参照カウントが0にならない）
    - 代入ごとにカウント操作（オーバーヘッド）
    - マルチスレッドでアトミック操作が必要

    CPythonのサイクル検出:
      1. コンテナオブジェクトの参照先のカウントを仮減算
      2. カウント > 0 のオブジェクトは外部参照あり（ルート）
      3. ルートから到達不可なオブジェクトは循環ゴミ → 回収
    """)

    misconception(
        "GCがあればメモリリークは起きない",
        "GCは「到達不可能な」オブジェクトしか回収しない。"
        "リストに追加し続けて参照を保持すればリークする。"
        "イベントリスナーやキャッシュの解放忘れは典型的なGC環境でのリーク。"
    )

    interview("""
    Q: Java/Python/Goのメモリ管理の違いは？
    A: Java = 世代別GC（G1がデフォルト）。ZGCで<10ms停止も可能。
       Python = 参照カウント + 世代別サイクル検出器。GILの影響大。
       Go = 並行Mark-Sweep（tri-color marking）。世代分けなし。
       停止時間は Go < Java(ZGC) < Java(G1) < Python の傾向。
       RustはGCなし（所有権システムでコンパイル時に管理）。
    """)


