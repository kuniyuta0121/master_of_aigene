def chapter4_ir_optimization():
    title("第4章: 中間表現（IR）& 最適化")

    heading("4.1 なぜ中間表現が必要か")
    p("""
    この節の狙い: 「4.1 なぜ中間表現が必要か」を単語暗記ではなく、なぜ必要かまでつながる形で理解する。
    ASTは「ソース言語の構造」を反映している。
    機械語は「CPU命令の列」。この間のギャップが大きすぎる。

    IR（中間表現）はその橋渡し。

      ソース → [フロントエンド] → IR → [最適化] → IR' → [バックエンド] → 機械語

    IRを挟むメリット:
    1. M個の言語 × N個のCPU → M+N個の変換で済む（M×Nではなく）
       例: LLVM は C, C++, Rust, Swift → LLVM IR → x86, ARM, WASM
    2. 言語に依存しない最適化が可能
    3. 新しいターゲットの追加が容易
    """)

    heading("4.2 Three Address Code（三番地コード）")
    p("""
    先に結論: 「4.2 Three Address Code（三番地コード）」は実装と設計判断に直結する。背景とトレードオフを押さえる。
    最も基本的なIR形式。各命令は高々3つのオペランドを持つ。

    ソース: x = (a + b) * (a + b) - 2 * 3

    三番地コード:
      t1 = a + b
      t2 = a + b      ← 共通部分式
      t3 = t1 * t2
      t4 = 2 * 3      ← 定数畳み込み可能
      t5 = t3 - t4
      x  = t5

    特徴:
    - 1命令 = 1演算（複雑な式を分解）
    - 仮想レジスタ（t1, t2...）は無制限に使える
    - 制御フロー: label, goto, if_goto で表現
    """)

    heading("4.3 SSA形式（Static Single Assignment）")
    p("""
    この節の狙い: 「4.3 SSA形式（Static Single Assignment）」を単語暗記ではなく、なぜ必要かまでつながる形で理解する。
    SSA = 各変数は「一度だけ」定義される形式。

    通常のIR:        SSA形式:
      x = 1            x_1 = 1
      x = x + 1        x_2 = x_1 + 1
      y = x * 2        y_1 = x_2 * 2

    分岐がある場合は φ関数（phi関数）を使う:

      if (cond):           SSA形式:
        x = 1                x_1 = 1
      else:                  x_2 = 2
        x = 2              merge:
      y = x                  x_3 = φ(x_1, x_2)  ← どちらかを選ぶ
                              y_1 = x_3

    SSAの利点:
    - def-useチェーンが自明（各変数の定義箇所は1つだけ）
    - 最適化パスが大幅に簡単になる
    - LLVM IR, GCC GIMPLE, V8 Sea of Nodes 全てSSAベース
    """)

    heading("4.4 主要な最適化パス")
    p("""
    この節の狙い: 「4.4 主要な最適化パス」を単語暗記ではなく、なぜ必要かまでつながる形で理解する。
    最適化パスは「IRを等価なより効率的なIRに変換する」もの。

    1. 定数畳み込み（Constant Folding）:
       t1 = 2 * 3  →  t1 = 6   （コンパイル時に計算）

    2. 共通部分式除去（CSE: Common Subexpression Elimination）:
       t1 = a + b       t1 = a + b
       t2 = a + b   →   t2 = t1      （再計算を避ける）

    3. コピー伝播（Copy Propagation）:
       t1 = x           t1 = x
       y = t1 + 1   →   y = x + 1    （中間変数を除去）

    4. 死コード除去（Dead Code Elimination）:
       t1 = x + 1   →   (削除)       （t1が後で使われない場合）

    5. ループ不変式の移動（Loop-Invariant Code Motion, LICM）:
       for i in range(n):
         y = a * b       ← ループ内で変わらない
         x[i] = y + i
       →
       y = a * b          ← ループ外に移動
       for i in range(n):
         x[i] = y + i

    6. インライン展開（Inlining）:
       関数呼び出しを関数本体で置き換える。
       呼び出しオーバーヘッド除去 + 他の最適化の機会が生まれる。

    7. 強度の軽減（Strength Reduction）:
       x * 2  →  x << 1   （乗算をシフトに）
       x * 8  →  x << 3

    実際のコンパイラは50以上の最適化パスを反復適用する。
    """)

    misconception(
        "最適化はコンパイラに任せればいい",
        "コンパイラは「意味を変えない変換」しかできない。"
        "アルゴリズムの選択（O(n^2) vs O(n log n)）やデータ構造の選択は"
        "プログラマの責任。最適化の99%はアルゴリズム選択で決まる。"
    )

    interview("""
    Q: LLVMとは何か？
    A: コンパイラ基盤（フレームワーク）。LLVM IRという共通のSSA形式の
       中間表現を中心に、フロントエンド（Clang等）とバックエンド（x86,
       ARM等）を分離する。最適化パスはIR上で動くので言語非依存。
       Rust, Swift, Julia, Kotlin/Native などが利用している。
    """)


