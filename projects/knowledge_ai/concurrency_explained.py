def chapter4_lock_free():
    title("Chapter 4: ロックフリーデータ構造")

    heading("4-1. CAS（Compare-And-Swap）")
    p("""
    まず全体像: この節では「4-1. CAS（Compare-And-Swap）」を、定義 -> 仕組み -> 実務上の判断の順で整理する。
    ロックフリーの基盤となる原子操作:
      CAS(アドレス, 期待値, 新値)
      → アドレスの現在値が期待値と同じなら新値に更新し、true返却
      → 異なればfalse返却（何もしない）

    ■ なぜCASでロックが不要になるのか？
    楽観的アプローチ: 「競合は稀」と仮定して操作を試み、
    失敗したらリトライ。ロック取得・解放のオーバーヘッドなし。

    ■ CASループパターン（擬似コード）
      while True:
          old = load(addr)
          new = compute(old)
          if CAS(addr, old, new): break

    ■ ハードウェアサポート
    - x86: CMPXCHG命令 + LOCKプレフィクス
    - ARM: LL/SC（Load-Linked / Store-Conditional）
    - Java: Unsafe.compareAndSwapInt → VarHandle (Java 9+)
    """)

    heading("4-2. ABA問題")
    p("""
    この節の狙い: 「4-2. ABA問題」を単語暗記ではなく、なぜ必要かまでつながる形で理解する。
    CASの落とし穴。以下のシナリオ:
    1. スレッドAがアドレスから値'A'を読む
    2. スレッドBが A→B→A と変更（元に戻す）
    3. スレッドAのCAS: 現在値A == 期待値A → 成功！
    しかし値は一度変わっていた。ポインタの場合、
    同じアドレスが別のオブジェクトを指している可能性。

    ■ 対策
    1. タグ付きポインタ: ポインタ+バージョン番号をセットでCAS
       （ABA→A'。バージョンが違うので検出可能）
    2. ハザードポインタ: 使用中のポインタを公開し、
       他スレッドがそのメモリを再利用しないようにする
    3. エポックベース回収: RCU的な手法（Linux Kernel）

    ■ Javaでの対応
    AtomicStampedReference: 参照+スタンプ（int）のペアをCAS
    """)

    heading("4-3. ロックフリー vs Wait-Free")
    p("""
    まず全体像: この節では「4-3. ロックフリー vs Wait-Free」を、定義 -> 仕組み -> 実務上の判断の順で整理する。
    進行保証のレベル:
    1. Obstruction-Free: 他スレッドが全停止すれば進行可能
    2. Lock-Free: 全体として少なくとも1スレッドが進行
       （個々のスレッドはスターブしうる）
    3. Wait-Free: 全スレッドが有限ステップで完了
       （最強だが実装コスト大）

    ■ なぜロックフリーを使うのか？
    - ロックだとスレッドが停止→全体停止のリスク
    - ロックフリーなら停止スレッドがいても他は進行
    - 優先度逆転の問題が発生しない
    - ただし実装が極めて難しい（バグが検出しにくい）
    """)

    heading("4-4. Michael-Scott Queue")
    p("""
    この節の狙い: 「4-4. Michael-Scott Queue」を単語暗記ではなく、なぜ必要かまでつながる形で理解する。
    最も有名なロックフリーFIFOキュー（1996年）。
    Java ConcurrentLinkedQueueの内部実装。

    ■ 構造
    - 単方向リンクリスト + Head/Tailポインタ
    - ダミーノード（sentinel）をHead初期値とする

    ■ Enqueue（末尾追加）
    1. Tailの次をCASで新ノードに設定
    2. TailをCASで新ノードに更新
    ★ 2ステップが原子的でないため、中途半端な状態がありえる
    → 他スレッドが「Tailが遅れている」のを検出し修復（helping）

    ■ Dequeue（先頭取得）
    1. Headの次のノードの値を読む
    2. HeadをCASで次のノードに更新

    ■ helpingの思想
    自分の操作だけでなく、他スレッドの操作も手伝う。
    これがロックフリー設計の重要な設計原則。
    """)

    misconception(
        "ロックフリー=常に高速",
        "CASリトライが頻発すると性能劣化。競合が激しい場合は"
        "ロックベースの方が速いこともある。適材適所"
    )

