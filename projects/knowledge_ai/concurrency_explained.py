def chapter3_sync_primitives():
    title("Chapter 3: 同期プリミティブ")

    heading("3-1. Mutex（相互排他ロック）")
    p("""
    この節の狙い: 「3-1. Mutex（相互排他ロック）」を単語暗記ではなく、なぜ必要かまでつながる形で理解する。
    最も基本的な同期機構。一度に一つのスレッドだけが
    クリティカルセクションに入れる。

    ■ 実装の仕組み
    1. スピンロック: ビジーウェイト（CPUを消費して待機）
       - 短時間のロックに有効（コンテキストスイッチ回避）
       - 長時間保持するとCPU浪費
    2. OSミューテックス: 待機時にスレッドをスリープ
       - カーネルコール必要（コスト高）
       - 長時間ロックに適切
    3. 適応型: 最初スピン→一定時間後スリープ（Java synchronized）

    ■ 比喩
    トイレの鍵。入ったら鍵をかける、出たら開ける。
    他の人は鍵が開くまで待つ。
    """)

    heading("3-2. Semaphore（セマフォ）")
    p("""
    先に結論: 「3-2. Semaphore（セマフォ）」は実装と設計判断に直結する。背景とトレードオフを押さえる。
    Dijkstraが考案。カウンタ付きロック。
    - P操作（wait/acquire）: カウンタ-1、0なら待機
    - V操作（signal/release）: カウンタ+1、待機中スレッドを起床

    ■ 用途
    - バイナリセマフォ（カウンタ=1）: Mutexと同等
    - カウンティングセマフォ: リソースプール管理
      例: DB接続プール最大10本 → 初期値10のセマフォ

    ■ MutexとSemaphoreの違い
    Mutex: 所有権がある。ロックした人だけがアンロックできる
    Semaphore: 所有権なし。誰でもsignalできる
    この違いが優先度逆転への対処（優先度継承）に影響する。
    """)

    heading("3-3. RWLock（読み書きロック）")
    p("""
    この節の狙い: 「3-3. RWLock（読み書きロック）」を単語暗記ではなく、なぜ必要かまでつながる形で理解する。
    読み取りが多く書込みが少ない場合に有効。
    - 読み取りロック: 複数スレッドが同時取得可能
    - 書込みロック: 排他的（読み取りも書込みも不可）

    ■ なぜ有効か？
    設定ファイルの参照が100回、更新が1回のような状況で
    全てMutexにすると99回の読取りが直列化されて無駄。
    RWLockなら読取り同士は並行実行。

    ■ スターベーション問題
    読者が途切れないと書込み者が永遠に待つ。
    対策: 書込み者優先ポリシー or フェアネスキュー
    """)

    heading("3-4. Condition Variable（条件変数）")
    p("""
    先に結論: 「3-4. Condition Variable（条件変数）」は実装と設計判断に直結する。背景とトレードオフを押さえる。
    「ある条件が成立するまで待ちたい」を実現する。
    必ずMutexと組み合わせて使う。

    ■ 3つの操作
    1. wait(mutex): mutexを解放し、通知まで待機
    2. signal(): 待機中のスレッドを1つ起床
    3. broadcast(): 待機中の全スレッドを起床

    ■ Spurious Wakeup（偽の起床）
    OSの実装上、signal無しでwaitから戻ることがある。
    そのため必ずwhileループで条件を再確認する:

      while not condition:
          cv.wait(lock)

    if文ではなくwhile文を使うこと。これは面接で問われる定番。

    ■ 典型パターン: Producer-Consumer
    生産者がキューにデータを入れてsignal。
    消費者はキューが空ならwait。
    bounded buffer（サイズ上限付き）は条件変数2つで実装。
    """)

    misconception(
        "ロックを使えば安全",
        "ロック粒度が粗いと性能低下、細かいとデッドロックリスク増加。"
        "ロックフリー構造やSTM等の代替手段も検討すべき"
    )

    interview("""
    Q: MutexとSemaphoreの違いは？
    A: Mutexは所有権あり（ロック者のみアンロック可）、
       Semaphoreは所有権なし（カウンタベース）。
       Mutexは排他制御、Semaphoreはリソース数制限に適する。

    Q: Condition Variableでwhileループが必要な理由は？
    A: Spurious wakeup対策。OSがsignal無しでwaitを返すことがあり、
       また複数スレッドがbroadcastで起きた場合、先に条件を
       消費されている可能性がある。必ず条件を再確認する。
    """)


