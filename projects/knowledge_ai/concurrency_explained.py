#!/usr/bin/env python3
"""
Concurrency & Parallelism - 深掘り解説
"""
import textwrap

SEP = "=" * 70
THIN = "-" * 70

def title(text):
    print(f"\n{SEP}\n  {text}\n{SEP}")

def heading(text):
    print(f"\n{THIN}\n  {text}\n{THIN}")

def p(text):
    for line in textwrap.dedent(text).strip().split("\n"):
        print(f"  {line}")

def interview(text):
    print()
    print("  【面接向けに整理すると】")
    for line in textwrap.dedent(text).strip().split("\n"):
        print(f"  {line}")

def misconception(wrong, right):
    print(f"\n  ⚠ よくある誤解: {wrong}")
    print(f"  → 正確には: {right}")


# ======================================================================
#  Chapter 1: 並行と並列
# ======================================================================
def chapter1_concurrency_vs_parallelism():
    title("Chapter 1: 並行(Concurrency)と並列(Parallelism)")

    heading("1-1. Rob Pikeの定義")
    p("""
    先に結論: 「1-1. Rob Pikeの定義」は実装と設計判断に直結する。背景とトレードオフを押さえる。
    Rob Pike（Go言語の設計者）の有名な定義:
      - Concurrency（並行性）: 複数のタスクを「構造化」して扱う設計手法
      - Parallelism（並列性）: 複数のタスクを物理的に「同時実行」すること

    ■ 比喩で理解する
    レストランの厨房を想像してほしい:
      - 並行 = 一人のシェフが、パスタを茹でながら肉を焼き、
              ソースも並行して準備する。物理的には一人だが
              タスクを切り替えながら複数の料理を進める。
      - 並列 = 3人のシェフが、それぞれ別の料理を同時に作る。

    並行性は「設計の問題」であり、並列性は「実行の問題」。
    シングルコアCPUでも並行処理はできる（タスク切り替え）。
    並列処理にはマルチコアなど物理的な同時実行能力が必要。
    """)

    misconception(
        "並行と並列は同じ意味",
        "並行は複数タスクの管理構造、並列は物理的同時実行。直交する概念"
    )

    heading("1-2. プロセスとスレッド")
    p("""
    まず全体像:
    - プロセス = 独立した「実行環境の箱」
    - スレッド = その箱の中で動く「実行の流れ」

    ■ プロセス（Process）
    OSが管理する実行中プログラムの単位。典型的に以下を持つ:
    - 独立した仮想メモリ空間
    - ヒープ / グローバル領域 / コード領域
    - ファイルディスクリプタ、ソケット、環境変数
    - 1個以上のスレッド

    重要なのは「独立性」:
    - 別プロセスのメモリは基本的に直接読めない
    - 障害の局所化・セキュリティ分離に強い

    ■ fork() と CoW（Copy-on-Write）
    Unix系ではfork()で親プロセスを複製して子を作る。
    ただしfork直後に全メモリを物理コピーするわけではない:
    - 最初は親子で同じページを読み取り共有
    - どちらかが書いた瞬間に、そのページだけ複製
    これがCoW。forkの実コストを抑える中核最適化。

    ■ プロセス間通信（IPC）
    プロセスは独立空間なので、共有には明示的な通信が必要:
    - パイプ（|）
    - ソケット（TCP/UDP/Unix Domain）
    - 共有メモリ（高速だが同期設計が難しい）
    - メッセージキュー（非同期で疎結合にしやすい）

    ■ スレッド（Thread）
    プロセス内で実際にCPUが実行する最小単位。
    同一プロセス内スレッドは次を共有する:
    - コード、ヒープ、グローバル変数、FD、ソケット
    一方で次はスレッド固有:
    - スタック、レジスタ、プログラムカウンタ

    ■ なぜスレッドを使うのか
    - 軽量に並行性を増やせる（同一プロセス内で完結）
    - ヒープ共有によりデータ受け渡しが速い
    - I/O待ちの間に別スレッドを進め、待ち時間を隠せる

    ただし代償もある:
    - 共有メモリゆえに race condition / deadlock / 可視性問題が起きる
    - つまり「軽いが難しい」。後続章の同期プリミティブが必要になる。

    ■ ユーザースレッド vs カーネルスレッド
    - カーネルスレッド: OSが直接スケジュール
    - ユーザースレッド: ランタイム/ライブラリが管理

    対応モデル:
    - 1:1  多くの一般的スレッドAPI
    - N:1  軽いが1つのブロッキングで全体停止しやすい
    - M:N  ユーザースレッドM個をOSスレッドN個に多重化（Goなど）

    実務での使い分け:
    - 障害隔離・強い安全性が最優先 -> プロセス
    - 同一アプリ内で高速共有しつつ並行処理 -> スレッド
    """)

    heading("1-3. コンテキストスイッチ")
    p("""
    まず全体像: この節では「1-3. コンテキストスイッチ」を、定義 -> 仕組み -> 実務上の判断の順で整理する。
    CPUが実行中のタスクを切り替える操作。なぜコストがかかるのか？

    1. レジスタの退避・復元: 汎用レジスタ、PC、SPなどを保存
    2. TLBフラッシュ: プロセス切り替え時、仮想→物理アドレス変換
       キャッシュが無効化される（スレッド切り替えでは不要）
    3. キャッシュ汚染: 新タスクのデータでL1/L2キャッシュが
       上書きされ、旧タスク再開時にキャッシュミス多発

    ■ コスト目安
    - スレッド切り替え: 数μs
    - プロセス切り替え: 数十μs（TLBフラッシュが主因）
    - syscall: 数百ns〜数μs

    ■ なぜこれが重要か？
    1万スレッドが1msごとに切り替わると、スイッチだけで
    CPU時間の相当部分が消費される。これがC10K問題の本質であり、
    イベント駆動モデル（Chapter 6）が生まれた理由。
    """)

    interview("""
    Q: 並行と並列の違いを説明してください
    A: 並行性はタスクの構造化（設計）、並列性は物理的同時実行。
       1コアでも並行は可能（タイムスライス）。並列にはマルチコア必要。
       Go言語は並行性を言語レベルで支援し、ランタイムが並列実行に変換。

    Q: プロセスとスレッドの使い分けは？
    A: 障害隔離が必要→プロセス（Chrome各タブ、マイクロサービス）
       高速データ共有→スレッド（ゲームエンジン、DBエンジン内部）
       大量軽量タスク→グリーンスレッド/goroutine（Webサーバ）
    """)


