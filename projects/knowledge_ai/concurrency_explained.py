def chapter7_go_actor():
    title("Chapter 7: Go goroutine / CSP / Actor モデル")

    heading("7-1. CSP (Communicating Sequential Processes)")
    p("""
    この節の狙い: 「7-1. CSP (Communicating Sequential Processes)」を単語暗記ではなく、なぜ必要かまでつながる形で理解する。
    Tony Hoare (1978) の理論。Go言語の並行モデルの基盤。

    ■ 核心思想
    「メモリを共有して通信するな。通信してメモリを共有せよ。」
    (Don't communicate by sharing memory;
     share memory by communicating.)

    ■ CSPの要素
    - プロセス: 逐次的に実行される独立した計算単位
    - チャネル: プロセス間の同期的通信路
    - プロセスはチャネルを介してのみデータをやり取り

    ■ なぜ共有メモリより安全か？
    チャネル通信 = データの所有権移転。
    送信側が送ったら、もうそのデータに触らない。
    受信側が新たな所有者。同時アクセスが構造的に排除される。
    """)

    heading("7-2. goroutineとスケジューラ")
    p("""
    まず全体像: この節では「7-2. goroutineとスケジューラ」を、定義 -> 仕組み -> 実務上の判断の順で整理する。
    ■ goroutineとは
    Goランタイムが管理する軽量実行単位。
    - 初期スタック: 数KB（スレッドの1MBに対し1/100以下）
    - 生成コスト: 数百ns（スレッドの数十μsに対し1/100）
    - 100万goroutineも現実的（100万スレッドは非現実的）

    ■ GMP スケジューラ
    - G (Goroutine): 実行されるタスク
    - M (Machine): OSスレッド
    - P (Processor): 論理プロセッサ（GOMAXPROCS個）

    PがGをMに割り当てて実行。Gがブロックすると:
    1. I/Oブロック: netpollerに委譲、Pは別のGを実行
    2. syscallブロック: Mごとブロック、Pは別のMに移動
    3. チャネル待ち: Gをパーク、Pは次のGを実行

    ■ Work Stealing
    PのローカルキューのGが枯渇 → 他のPのキューからGを盗む。
    負荷の均等化を自動で実現。
    """)

    heading("7-3. channel パターン")
    p("""
    まず全体像: この節では「7-3. channel パターン」を、定義 -> 仕組み -> 実務上の判断の順で整理する。
    ■ 基本
    - unbuffered: 送受信が同期（ランデブー）
      送信者は受信者が来るまでブロック。逆も同様
    - buffered: バッファが満杯になるまで送信可能
      producer-consumerパターンに適する

    ■ 頻出パターン
    1. Fan-out/Fan-in: 1つの入力を複数goroutineで処理し結果を集約
    2. Pipeline: stage1 → channel → stage2 → channel → stage3
    3. Done channel: context.Done()でキャンセル伝播
    4. Select: 複数チャネルから最初に準備できたものを処理

    ■ selectの重要性
    selectはCSPの中核。複数の通信操作を非決定的に選択:
      select {
      case msg := <-ch1: handle(msg)
      case ch2 <- data:  ...
      case <-ctx.Done(): return
      }
    タイムアウト、キャンセル、多重通信が統一的に記述可能。
    """)

    heading("7-4. Actor モデル（Erlang / Akka）")
    p("""
    先に結論: 「7-4. Actor モデル（Erlang / Akka）」は実装と設計判断に直結する。背景とトレードオフを押さえる。
    Carl Hewitt (1973)。CSPとは別の並行モデル。

    ■ Actorの3つの能力（メッセージ受信時）
    1. 新しいActorを生成
    2. 他のActorにメッセージを送信
    3. 自身の次の状態を決定

    ■ CSPとの違い
    - CSP: チャネルが通信の中心。プロセスは匿名的
    - Actor: アクター自身がアドレスを持つ。直接メッセージ送信
    - CSP: 同期通信（unbuffered channelがデフォルト）
    - Actor: 非同期通信（メールボックスに蓄積）

    ■ Erlang/OTPの特徴
    - let it crash 哲学: エラー処理よりクラッシュ＋再起動
    - Supervisor tree: 親Actorが子の監視・再起動を担当
      - one_for_one: クラッシュした子だけ再起動
      - one_for_all: 1つクラッシュで全子再起動
      - rest_for_one: クラッシュした子以降を再起動
    - ホットコードスワップ: 無停止でコード更新
    - 99.9999999%可用性（AXD301スイッチ）の実績

    ■ なぜ電話交換機でErlangが使われたか？
    数百万の同時通話（＝Actor）を管理。
    1通話のクラッシュが他に影響しない隔離性。
    これは現代のマイクロサービスと同じ思想。
    """)

    misconception(
        "goroutineはスレッドより常に良い",
        "goroutineは軽量だが、共有メモリアクセス時は依然として"
        "Mutex等が必要。チャネルも競合が激しいと性能劣化する。"
        "適切な粒度設計が重要"
    )

    interview("""
    Q: CSPとActorモデルの違いは？
    A: CSP: チャネル中心、同期通信、プロセス匿名的（Go）
       Actor: アクター中心、非同期通信、直接アドレス指定（Erlang）
       CSPは通信の構造化に強く、Actorは障害隔離に強い。

    Q: goroutineが軽量な理由は？
    A: 数KBの初期スタック（動的成長）、ユーザー空間スケジューリング、
       M:Nモデル（M goroutine : N OSスレッド）、
       Work Stealingで負荷分散。
    """)


