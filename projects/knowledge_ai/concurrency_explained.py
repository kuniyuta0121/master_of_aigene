def chapter2_memory_model():
    title("Chapter 2: メモリモデル")

    heading("2-1. なぜメモリモデルが必要か")
    p("""
    まず全体像: この節では「2-1. なぜメモリモデルが必要か」を、定義 -> 仕組み -> 実務上の判断の順で整理する。
    CPUは高速化のため命令を「リオーダー」する:
      - コンパイラ最適化: 命令の順序入替え
      - CPUパイプライン: Out-of-Order実行
      - ストアバッファ: 書込みを遅延させてスループット向上

    シングルスレッドでは問題ない（結果が同じになるよう保証）。
    しかしマルチスレッドでは他スレッドから「見える順序」が
    コード上の順序と異なりうる。これがバグの温床。

    メモリモデルとは:
    「どの順序でメモリ操作が他スレッドから観測されるか」の規約。
    """)

    heading("2-2. happens-before関係")
    p("""
    この節の狙い: 「2-2. happens-before関係」を単語暗記ではなく、なぜ必要かまでつながる形で理解する。
    happens-before（HB）: 操作Aの結果が操作Bに確実に見える関係。

    ■ HBが成立する典型例:
    1. 同一スレッド内の逐次実行: A → B ならAはBにHB
    2. ロック解放 → 同ロック獲得: unlock HB lock
    3. volatile書込み → 同変数のvolatile読取り（Java）
    4. スレッド開始: start() HB 新スレッドの最初の命令
    5. スレッド終了: 最後の命令 HB join()の返却

    HBがない = 順序保証なし = 読める値が不定 = データ競合。
    """)

    heading("2-3. 一貫性モデルの階層")
    p("""
    まず全体像: この節では「2-3. 一貫性モデルの階層」を、定義 -> 仕組み -> 実務上の判断の順で整理する。
    強い順 ──────────────────────────── 弱い順
    Sequential    TSO         Relaxed
    Consistency   (x86)       (ARM/RISC-V)

    ■ Sequential Consistency（SC）
    Lamportの定義: 全操作がある単一の全順序に従い、
    各スレッド内の順序がその全順序に反映される。
    直感的だが遅い。現代CPUでSCを採用するものはない。

    ■ TSO (Total Store Order) — x86
    ストアバッファの影響のみ:
    - 自分の書込みを他スレッドが即座に見ない可能性がある
    - Store→Load のリオーダーのみ発生
    - 他のリオーダー（Store→Store, Load→Load）は禁止
    実用上、多くのバグは起きにくいが完全ではない。

    ■ Relaxed — ARM, RISC-V
    ほぼ全てのリオーダーが許可される。
    明示的なメモリバリア（fence命令）が必須。
    性能は高いがプログラマの負担大。
    """)

    heading("2-4. Java Memory Model (JMM)")
    p("""
    先に結論: 「2-4. Java Memory Model (JMM)」は実装と設計判断に直結する。背景とトレードオフを押さえる。
    JMM (JSR-133, Java 5以降) の設計思想:
    - ハードウェアの差異を吸収し、プラットフォーム非依存の保証
    - volatile, synchronized, finalに明確なセマンティクス

    ■ volatile
    - 読み書きがHB関係を形成
    - コンパイラ・CPU両方のリオーダーを禁止
    - 可視性は保証するが原子性は保証しない（i++はNG）

    ■ synchronized
    - モニターのロック/アンロックでHBを形成
    - ブロック内の操作の原子性と可視性を両方保証

    ■ final
    - コンストラクタ内のfinalフィールド書込みは、
      オブジェクト参照の公開にHB（安全な公開）

    なぜJMMが重要か？
    x86で動いたコードがARMで壊れる、という問題を防ぐ。
    JMMに従えば、どのCPUでも同じ動作が保証される。
    """)

    misconception(
        "volatileで十分な同期ができる",
        "volatileは可視性のみ保証。複合操作（read-modify-write）には"
        "synchronized/AtomicXxxが必要"
    )

    interview("""
    Q: happens-beforeとは何ですか？
    A: 操作Aの効果が操作Bに確実に見えることを保証する順序関係。
       ロック解放→獲得、volatile書込み→読取りなどで成立。
       HBがなければデータ競合が発生しうる。

    Q: x86とARMでマルチスレッドコードの動作が違う理由は？
    A: メモリ一貫性モデルの違い。x86はTSO（Store→Loadのみ緩い）、
       ARMはRelaxed（ほぼ全リオーダー許可）。同じCコードでも
       ARM上では追加のメモリバリアが必要になる場合がある。
    """)


