    p("""
    先に結論: 「8-2. 2PC (Two-Phase Commit)」は実装と設計判断に直結する。背景とトレードオフを押さえる。
    分散トランザクションの古典的プロトコル。

    ■ Phase 1: Prepare（投票）
    コーディネータ → 全参加者: 「コミットできるか？」
    各参加者: ログ永続化、「Yes」or「No」を返答

    ■ Phase 2: Commit/Abort（決定）
    全員Yes → コーディネータ: 「Commit」を送信
    1つでもNo → コーディネータ: 「Abort」を送信

    ■ 問題点
    1. ブロッキング: コーディネータ障害時、参加者はPrepared状態で
       ロックを保持したまま待ち続ける → リソースが凍結
    2. 単一障害点: コーディネータの可用性に全体が依存
    3. 性能: 2往復の通信 + ログ永続化 → レイテンシ大

    ■ 3PC
    2PCのブロッキング問題を緩和するが、ネットワーク分断に弱く
    実用ではほぼ使われない。
    """)

    heading("8-3. Sagaパターン")
    p("""
    まず全体像: この節では「8-3. Sagaパターン」を、定義 -> 仕組み -> 実務上の判断の順で整理する。
    2PCの代替。長時間トランザクションを複数のローカル
    トランザクションに分割し、失敗時は補償トランザクションで戻す。

    ■ 例: 旅行予約
    1. フライト予約 → 成功
    2. ホテル予約   → 成功
    3. レンタカー予約 → 失敗！
    → ホテル予約キャンセル → フライト予約キャンセル（逆順補償）

    ■ 2つの実装方式
    1. Choreography（振付）: 各サービスがイベントで連鎖
       - 利点: オーケストレータ不要
       - 欠点: フローが追いにくい、サービス増で複雑化
    2. Orchestration（指揮）: 中央のSagaオーケストレータが制御
       - 利点: フロー明確
       - 欠点: オーケストレータが単一障害点になりうる

    ■ Sagaの制約
    - ACIDのIsolationは保証されない（中間状態が見える）
    - 補償トランザクションの設計が難しい
    - べき等性が必須（リトライ時に二重実行防止）
    """)

    heading("8-4. Raft合意アルゴリズム")
    p("""
    この節の狙い: 「8-4. Raft合意アルゴリズム」を単語暗記ではなく、なぜ必要かまでつながる形で理解する。
    Paxosの理解しやすい代替（Diego Ongaro, 2014）。
    etcd, CockroachDB, TiKV等で採用。

    ■ 3つの役割
    - Leader: クライアント要求を受付、ログを複製
    - Follower: Leaderのログを受信・適用
    - Candidate: Leader選出時の候補

    ■ Leader Election（リーダー選出）
    1. Followerがハートビートのタイムアウトを検出
    2. Candidateに遷移、自分に投票、他ノードに投票要求
    3. 過半数の投票を得たらLeaderに
    4. タイムアウトはランダム化（split vote防止）

    ■ Log Replication（ログ複製）
    1. Leaderがエントリをログに追加
    2. 全Followerに複製要求（AppendEntries RPC）
    3. 過半数が確認 → コミット、クライアントに応答
    4. 次のハートビートでコミット情報を伝播

    ■ Safety（安全性）
    - Election Restriction: ログが最新でないCandidateには
      投票しない → コミット済みエントリの喪失を防止
    - 過半数重複: 任意の2つの過半数は1ノード以上重複
      → コミット情報は必ず次のLeaderに引き継がれる

    ■ なぜPaxosでなくRaftか？
    Paxosは正確だが理解困難（Lamport自身も認める）。
    Raftは「理解しやすさ」を設計目標とし、
    Leader選出・ログ複製・安全性を明確に分離。
    """)

    misconception(
        "分散合意があれば何でも解決する",
        "Raftは少数ノード（3〜7）のメタデータ管理向き。"
        "大規模データの全レコードにRaftを適用すると遅い。"
        "用途に応じてSaga・CRDT・イベントソーシング等を使い分ける"
    )

    interview("""
    Q: 2PCとSagaの違いは？
    A: 2PCは強い一貫性（ACID）だがブロッキングリスク。
       Sagaは結果整合性で補償トランザクションにより戻す。
       マイクロサービスではSagaが一般的（サービス自律性を維持）。

    Q: Raftの合意プロセスを説明してください
    A: Leader選出→ログ複製→コミットの3段階。
       過半数確認でコミット。Leaderダウン時はタイムアウト検出→
       ランダム化タイマーで新Leader選出。ログの最新性で安全性確保。
    """)


