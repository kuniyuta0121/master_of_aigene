#  Chapter 5: デッドロック
# ======================================================================
def chapter5_deadlock():
    title("Chapter 5: デッドロック")

    heading("5-1. Coffmanの4条件")
    p("""
    この節の狙い: 「5-1. Coffmanの4条件」を単語暗記ではなく、なぜ必要かまでつながる形で理解する。
    デッドロック発生に必要十分な4条件（1971年、Coffman等）:

    1. 相互排他（Mutual Exclusion）
       リソースは同時に1つのスレッドのみ使用可能

    2. 保持と待機（Hold and Wait）
       リソースを保持したまま、他のリソースを待つ

    3. 横取り不可（No Preemption）
       保持中のリソースを強制的に取り上げられない

    4. 循環待ち（Circular Wait）
       スレッド群が環状にリソースを待ち合う

    4つ全てが同時に成立するとデッドロック発生。
    逆に言えば、1つでも崩せば防げる。
    """)

    heading("5-2. 具体例: 食事する哲学者")
    p("""
    この節の狙い: 「5-2. 具体例: 食事する哲学者」を単語暗記ではなく、なぜ必要かまでつながる形で理解する。
    Dijkstraの古典的問題:
    5人の哲学者が円卓に座り、各自の左右にフォークがある。
    食事には両方のフォークが必要。

    全員が同時に左のフォークを取ると、右のフォークを
    待ち続けて全員がデッドロック。

    ■ 解法例
    1. リソース順序付け: フォークに番号を振り、
       小さい番号から取る（循環待ち防止）
    2. 一括取得: 両フォークを同時に取れるときだけ取る
       （保持と待機の防止）
    3. タイムアウト: 一定時間で諦めてフォークを返す
    """)

    heading("5-3. 検出・予防・回避")
    p("""
    まず全体像: この節では「5-3. 検出・予防・回避」を、定義 -> 仕組み -> 実務上の判断の順で整理する。
    ■ 予防（Prevention）— 4条件の1つを構造的に排除
    - 循環待ち排除: ロック順序を固定（最も実用的）
      全ロックに番号を振り、必ず昇順で取得
    - 保持と待機排除: 全リソースを一度に要求（非効率）

    ■ 回避（Avoidance）— 動的に安全な状態を維持
    - 銀行家のアルゴリズム（Banker's Algorithm）
      各スレッドの最大要求量を事前申告。
      割当後も全員完了可能な「安全状態」か検査。
      安全でなければ割当拒否。
      実用ではほぼ使われない（最大要求量が不明な場合が多い）。

    ■ 検出（Detection）— 発生後に検出して対処
    - Wait-Forグラフ: スレッド間の待ち関係をグラフ化
      循環があればデッドロック検出
    - DB: PostgreSQLは定期的にデッドロック検出を実行し、
      1つのトランザクションをアボートして解決
    """)

    heading("5-4. ライブロックとスターベーション")
    p("""
    まず全体像: この節では「5-4. ライブロックとスターベーション」を、定義 -> 仕組み -> 実務上の判断の順で整理する。
    ■ ライブロック
    デッドロックに似ているが、スレッドは動いている。
    「譲り合い」が永遠に続く状態。
    例: 狭い廊下で二人がすれ違おうと同じ方向に避け続ける。
    対策: ランダムバックオフ（Ethernetの衝突回避と同じ思想）

    ■ スターベーション（飢餓）
    特定のスレッドがリソースを永遠に獲得できない状態。
    原因: 不公平なスケジューリング、優先度の偏り
    対策: フェアネスキュー、エイジング（待ち時間で優先度UP）
    """)

    misconception(
        "ロック順序さえ決めればデッドロックは起きない",
        "ロック順序は循環待ちのみ防止。コールバックやイベント経由で"
        "間接的にロック順序が崩れるケースもある。設計全体での注意が必要"
    )

    interview("""
    Q: デッドロックの4条件とその対策は？
    A: 相互排他・保持待機・横取り不可・循環待ち。全て同時成立で発生。
       実務では「ロック順序固定」（循環待ち排除）が最も効果的。
       DBでは検出+ロールバックが一般的（PostgreSQL等）。

    Q: ライブロックとデッドロックの違いは？
    A: デッドロックはスレッドが停止、ライブロックは動作中だが進行しない。
       ライブロックはランダムバックオフで対策可能。
    """)


