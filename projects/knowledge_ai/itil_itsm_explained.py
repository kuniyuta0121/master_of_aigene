def chapter2_incident_management():
    title("第2章: インシデント管理")

    heading("2-1. インシデント vs 問題 vs 変更")
    p("""
    この3つの区別はITSMの最重要概念:

    インシデント (Incident):
      サービスの「計画外の中断」または「品質の低下」。
      例: Webサイトが表示されない、メールが送れない。
      目的: できるだけ早くサービスを復旧させること。

    問題 (Problem):
      インシデントの「根本原因」、または将来インシデントを
      引き起こす可能性のある状態。
      例: DBサーバーのメモリリークがWebサイトダウンの原因。
      目的: 再発を防止すること。

    変更 (Change):
      ITサービスやインフラへの「追加・修正・削除」。
      例: サーバーのメモリ増設、アプリのバージョンアップ。
      目的: リスクを管理しつつ改善を実施すること。

    レストランの例え:
      インシデント = 客から「料理に髪が入ってる」→ 即座に新品提供
      問題 = なぜ髪が混入したか調査 → 帽子の着用ルール策定
      変更 = 厨房のレイアウト変更 → 事前に影響評価して実施
    """)

    heading("2-2. インシデントのライフサイクル")
    p("""
    検知 → 記録 → 分類 → 優先度設定 → 診断 → エスカレーション
    → 解決 → クローズ

    各ステップの要点:

    検知: 監視ツール (Datadog, PagerDuty等) による自動検知 or
          ユーザーからの申告。自動検知率を高めることが成熟度の指標。

    分類: カテゴリ (ネットワーク/アプリ/ハードウェア等) とサブカテゴリ。
          正確な分類がトレンド分析と自動ルーティングの基盤になる。

    優先度設定: 影響度 × 緊急度 のマトリクスで決定。
      影響度: 影響を受けるユーザー数・ビジネスへの影響
      緊急度: どれだけ早く解決が必要か
      → P1 (Critical), P2 (High), P3 (Medium), P4 (Low)

    診断: ナレッジベース検索 → 過去の類似インシデント確認。

    解決: ワークアラウンド (暫定対処) or 根本解決。
          ワークアラウンドでも「サービス復旧」が最優先。

    クローズ: ユーザー確認後にクローズ。満足度調査も実施。
    """)

    heading("2-3. 重大インシデント対応 (Major Incident)")
    p("""
    P1インシデントには特別なプロセスが発動する:

    1. 専任のインシデントコマンダーを任命
    2. 戦争部屋 (War Room) / ブリッジコール開設
    3. 経営層への即時報告 (15分以内が一般的)
    4. コミュニケーションマネージャーによる定期的なステータス更新
    5. 解決後に PIR (Post-Incident Review) を実施

    PIR (ポストインシデントレビュー):
      SREの「ポストモーテム」と同じ概念。
      「誰が悪いか」ではなく「何が悪かったか」に焦点を当てる
      (Blameless Culture)。
    """)

    heading("2-4. SLA とエスカレーション")
    p("""
    SLA (Service Level Agreement): サービス提供者と顧客の合意書。
      例: P1インシデントは4時間以内に解決、稼働率99.9%

    エスカレーションの2種類:
      機能的エスカレーション: より専門的なチームへ渡す
        L1 (サービスデスク) → L2 (アプリチーム) → L3 (開発/ベンダー)
      階層的エスカレーション: 上位のマネジメントへ報告
        チームリーダー → マネージャー → 部長 → CIO

    エスカレーションのタイミングはSLAに基づいて自動化すべき。
    「30分以内にL1で解決できなければ自動的にL2へ」等のルール。
    """)

    heading("2-5. チケットシステム")
    p("""
    ServiceNow: エンタープライズ向けITSMのデファクト。
      CMDB統合、ワークフロー自動化、AI予測が強み。
      大企業の情シスでは必須スキル。

    Jira Service Management (JSM): Atlassian製。
      開発チーム (Jira Software) との統合が強み。
      DevOpsチームに人気。Confluenceでナレッジ管理。

    選定のポイント: 組織規模、既存ツールとの統合、カスタマイズ性。
    """)

    misconception(
        "インシデント管理は『火消し』だから戦略的ではない",
        "インシデントデータの分析は問題管理・改善活動の最重要インプット。"
        "MTTRやインシデント傾向の可視化は経営レベルの意思決定に直結する"
    )

    interview("""
    Q: インシデントと問題の違いは？
    A: インシデントは「サービスの中断」で、目的は迅速な復旧です。
       問題は「根本原因」で、目的は再発防止です。
       例えばサーバーダウン(インシデント)を再起動で復旧させ、
       並行してメモリリーク(問題)の原因を調査・修正します。
    """)


