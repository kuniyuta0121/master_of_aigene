#!/usr/bin/env python3
"""
ITIL & IT Service Management - 深掘り解説
"""
import textwrap

SEP = "=" * 70
THIN = "-" * 70

def title(text):
    print(f"\n{SEP}\n  {text}\n{SEP}")

def heading(text):
    print(f"\n{THIN}\n  {text}\n{THIN}")

def p(text):
    for line in textwrap.dedent(text).strip().split("\n"):
        print(f"  {line}")

def interview(text):
    print()
    print("  【面接向けに整理すると】")
    for line in textwrap.dedent(text).strip().split("\n"):
        print(f"  {line}")

def misconception(wrong, right):
    print(f"\n  ⚠ よくある誤解: {wrong}")
    print(f"  → 正確には: {right}")


# =====================================================================
#  第1章: ITIL とは何か
# =====================================================================
def chapter1_what_is_itil():
    title("第1章: ITIL とは何か")

    heading("1-1. ITIL の定義")
    p("""
    ITIL (Information Technology Infrastructure Library) とは、
    ITサービスマネジメント (ITSM) のベストプラクティス集である。
    英国政府が1980年代に策定し、現在は Axelos/PeopleCert が管理する。

    身近な例え: レストランの「運営マニュアル」のようなもの。
    料理の作り方（技術）だけでなく、注文の受け方、クレーム対応、
    仕入れ管理、衛生管理など「店舗運営全体」のベストプラクティス集。
    ITILはIT部門の「運営マニュアル」に相当する。
    """)

    heading("1-2. なぜ ITIL が必要か")
    p("""
    ITが「コストセンター」から「ビジネスの成長エンジン」に変わった。
    しかし属人的な運用では以下の問題が起きる:

    - 障害対応が担当者の経験に依存 → 人が辞めるとノウハウ消失
    - 変更作業の影響範囲が不明 → 本番障害が頻発
    - ユーザーの問い合わせが散在 → 対応漏れ、重複対応
    - ITコストの説明ができない → 経営層からの信頼低下

    ITILは「共通言語」と「再現可能なプロセス」を提供する。
    世界中のIT組織が同じ用語・フレームワークで会話できるようになる。
    """)

    heading("1-3. ITIL v3 から ITIL 4 への進化")
    p("""
    ITIL v3 (2011年改訂版):
      サービスライフサイクル (戦略→設計→移行→運用→改善) の5冊構成。
      「プロセス中心」のアプローチ。26のプロセスを定義。
      問題: ウォーターフォール的で、アジャイル/DevOps と相性が悪い。

    ITIL 4 (2019年〜):
      「プロセス」から「プラクティス」へ用語変更。
      アジャイル/DevOps/リーン/クラウドを取り込んだ。
      34のプラクティスを定義 (一般管理14 + サービス管理17 + 技術管理3)。
      硬直的なプロセスではなく、柔軟に組み合わせる思想に転換。
    """)

    heading("1-4. サービスバリューシステム (SVS)")
    p("""
    ITIL 4 の最上位概念。以下の5要素で構成される:

    ┌─────────────────────────────────────────┐
    │  機会/需要 → [SVS] → 価値              │
    │                                         │
    │  1. 指導原則 (Guiding Principles)       │
    │  2. ガバナンス                           │
    │  3. サービスバリューチェーン (SVC)       │
    │  4. プラクティス (34個)                  │
    │  5. 継続的改善                           │
    └─────────────────────────────────────────┘

    SVSの核心: 「需要」を「価値」に変換する仕組み全体。
    技術だけでなく、人・プロセス・パートナー・技術の4つの次元で考える。
    """)

    heading("1-5. サービスバリューチェーン (SVC)")
    p("""
    SVCは6つの活動で構成される運用モデル:

    計画 (Plan) .............. ポートフォリオ・方針・戦略の策定
    改善 (Improve) ........... 継続的な改善活動
    エンゲージ (Engage) ...... ステークホルダーとの関係構築
    設計と移行 (Design & Transition) .. サービスの設計・構築・テスト
    調達/構築 (Obtain/Build) . コンポーネントの調達・開発
    提供とサポート (Deliver & Support) .. サービスの運用・サポート

    重要: v3のライフサイクル（直線的）と異なり、SVCは自由に
    活動を組み合わせる。アジャイルのスプリントにも対応できる。
    """)

    heading("1-6. 7つの指導原則")
    p("""
    ITILのあらゆる意思決定に適用する普遍的な原則:

    1. 価値に着目せよ (Focus on Value)
       → すべての活動は顧客/利害関係者への価値に結びつくべき
    2. 現状から始めよ (Start Where You Are)
       → ゼロからではなく、既存の資産・プロセスを活かす
    3. フィードバックをもとに反復して進化せよ (Progress Iteratively)
       → 大きなビッグバンではなく、小さく試して学ぶ
    4. 協働し、可視性を高めよ (Collaborate and Promote Visibility)
       → サイロを壊し、透明性を確保する
    5. 包括的に考え、取り組め (Think and Work Holistically)
       → 部分最適ではなく全体最適
    6. シンプルかつ実践的であれ (Keep It Simple and Practical)
       → 過剰なプロセスは害悪。最小限で最大の効果を
    7. 最適化し自動化せよ (Optimize and Automate)
       → まず最適化、その後に自動化。順番が重要
    """)

    misconception(
        "ITILは厳格なルールブックで、全プロセスを導入しなければならない",
        "ITILは「フレームワーク」であり「処方箋」ではない。"
        "組織の規模・成熟度に合わせて必要なプラクティスを選択的に採用する"
    )

    interview("""
    Q: ITILとは何ですか？なぜ重要ですか？
    A: ITILはITサービスマネジメントのベストプラクティス集です。
       属人的な運用を「再現可能なプロセス」に変え、ITの価値を
       ビジネスに確実に届ける仕組みを提供します。ITIL 4では
       アジャイルやDevOpsとの統合も考慮されています。
    """)


