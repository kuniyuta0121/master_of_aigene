# =====================================================================
def chapter6_cmdb():
    title("第6章: 構成管理 & CMDB")

    heading("6-1. CI (Configuration Item) とは")
    p("""
    CI (構成アイテム): ITサービスを構成する管理対象の単位。

    CIの例:
    - ハードウェア: サーバー、ネットワーク機器、ストレージ
    - ソフトウェア: OS、ミドルウェア、アプリケーション
    - ドキュメント: SLA文書、運用手順書、ネットワーク図
    - サービス: メールサービス、VPNサービス
    - 人: ベンダー連絡先、責任者

    CIの粒度設計が重要:
    - 粗すぎる → 影響分析ができない
    - 細かすぎる → 管理コストが爆発
    → 「変更管理の対象になるレベル」が適切な粒度の目安
    """)

    heading("6-2. CMDB の設計")
    p("""
    CMDB (Configuration Management Database):
    CIとその関係性を管理するデータベース。

    CMDBが管理する情報:
    - CI属性: 名前、バージョン、所有者、場所、ステータス
    - CI間の関係: 依存関係 (runs on, connects to, used by)

    例: サービスマップ
    [メールサービス]
      └─ depends on → [Exchange Server]
          ├─ runs on → [仮想サーバーVM-001]
          │   └─ hosted on → [物理サーバーSV-001]
          └─ connects to → [Active Directory]

    このマッピングにより:
    - SV-001が故障した場合、メールサービスに影響があると即座に判明
    - Exchange Serverの変更時、影響を受けるサービスを特定できる
    """)

    heading("6-3. 自動ディスカバリ")
    p("""
    手動でCMDBを維持するのは現実的に不可能。
    自動ディスカバリツールが必須:

    エージェント型: 各サーバーにエージェントをインストール。
      詳細な情報を収集可能。管理対象へのアクセスが必要。
    エージェントレス型: ネットワークスキャン、SNMP、WMI等で収集。
      導入が容易だが情報の粒度が粗い。

    主要ツール:
    - ServiceNow Discovery
    - BMC Helix Discovery
    - Device42
    - AWS Config / GCP Cloud Asset Inventory (クラウドネイティブ)

    クラウド時代の課題:
    コンテナやサーバーレスは生成・破棄が頻繁。
    → IaCのコード自体をCI情報のソースとする考え方が台頭。
    """)

    heading("6-4. CMDBの価値と課題")
    p("""
    CMDBの価値:
    - インシデント発生時の影響範囲の即時特定
    - 変更の影響分析の精度向上
    - コンプライアンス監査への対応
    - IT資産の可視化とコスト最適化

    最大の課題: データの鮮度維持。
    CMDBは「腐る」。情報が古くなると誰も信用しなくなり、
    使われなくなるという悪循環に陥る。

    鮮度維持の戦略:
    1. 自動ディスカバリの定期実行 (日次/週次)
    2. 変更管理プロセスとの統合 (変更時にCMDB更新を必須化)
    3. 検証プロセス (月次でサンプリング監査)
    4. 小さく始める: 全CIを登録しようとせず、
       重要サービスの構成から段階的に拡大
    """)

    misconception(
        "CMDBがあればすべての構成情報を完璧に管理できる",
        "CMDBは「完璧」を目指すと必ず失敗する。"
        "「十分に正確で、意思決定に使える」レベルを目指す。"
        "自動ディスカバリと変更プロセスの統合が鮮度維持の鍵"
    )

    interview("""
    Q: CMDBをどう活用しますか？
    A: 主にインシデントの影響分析と変更のリスク評価に使います。
       サービスマッピングにより「このサーバーが落ちたら
       どのサービスに影響するか」を即座に把握できます。
       鮮度維持のため自動ディスカバリと変更プロセスを統合します。
    """)


