def chapter3_problem_management():
    title("第3章: 問題管理")

    heading("3-1. 問題管理の目的")
    p("""
    インシデントの根本原因を特定し、再発を防止すること。
    インシデント管理が「消防署」なら、問題管理は「防火対策課」。

    問題管理は2つのアプローチがある:
      リアクティブ: 発生したインシデントから根本原因を追究
      プロアクティブ: インシデントが起きる前にリスクを特定・排除

    プロアクティブ問題管理の例:
    - インシデントのトレンド分析で「毎月第1月曜にDB遅延」を発見
    - 監視データから「ディスク使用量が毎月3%増加」を検知
    - ベンダーの脆弱性情報を先行的にチェック
    """)

    heading("3-2. 根本原因分析 (RCA) の手法")
    p("""
    5 Why分析: 「なぜ？」を5回繰り返して真因に到達する手法。

    例: Webサイトがダウンした
      なぜ? → APサーバーが応答しなくなった
      なぜ? → メモリが枯渇した
      なぜ? → メモリリークがあった
      なぜ? → コードレビューでメモリ管理が見落とされた
      なぜ? → メモリ関連のレビュー観点がチェックリストになかった
      → 対策: コードレビューチェックリストにメモリ管理項目を追加

    特性要因図 (フィッシュボーン / 石川ダイアグラム):
      「人」「方法」「機械」「材料」「環境」「測定」の6カテゴリで
      原因を網羅的に洗い出す。ブレインストーミングに有効。

    フォルトツリー分析 (FTA):
      トップ事象（障害）から原因をAND/ORゲートで木構造に展開。
      複合的な原因の組み合わせを分析するのに適する。
      航空・原子力など高信頼性が求められる分野で発展した手法。
    """)

    heading("3-3. 既知のエラーデータベース (KEDB)")
    p("""
    KEDB (Known Error Database): 根本原因が判明し、ワークアラウンドが
    確立された問題の記録。

    KEDB のエントリ例:
      問題ID: PRB-001
      症状: 毎週月曜朝にバッチジョブが失敗する
      根本原因: 週末のログローテーションでロックファイルが残る
      ワークアラウンド: 月曜朝にロックファイルを手動削除
      恒久対策: ログローテーションスクリプトの修正 (変更RFC-123)
      ステータス: ワークアラウンド適用中

    KEDBの価値:
    - L1サポートが即座にワークアラウンドを適用できる → MTTR短縮
    - 同じ問題の重複調査を防止
    - 恒久対策の優先度判断材料になる
    """)

    heading("3-4. ワークアラウンド管理")
    p("""
    ワークアラウンド (Workaround): 根本原因が解決されるまでの暫定対処。

    管理のポイント:
    1. 文書化: 手順を明確に記録し、誰でも実行できるようにする
    2. 有効期限: いつまでワークアラウンドで運用するか期限を設定
    3. リスク評価: ワークアラウンド自体が新たなリスクを生まないか確認
    4. 自動化: 繰り返し実行するなら自動化を検討

    危険なパターン: ワークアラウンドが恒久化すること。
    「動いてるから放置」は技術的負債の温床。
    定期的にKEDBをレビューし、恒久対策の優先度を再評価する。
    """)

    misconception(
        "問題管理はインシデントが解決してからやればいい",
        "重大インシデントでは並行して問題管理を開始すべき。"
        "またプロアクティブ問題管理はインシデント発生前に実施する"
    )

    interview("""
    Q: RCAの手法を1つ説明してください。
    A: 5 Why分析です。事象に対して「なぜ？」を繰り返し、
       表面的な原因から真の根本原因に到達します。
       重要なのは「人のミス」で止めず、プロセスや仕組みの
       欠陥まで掘り下げることです。
    """)


