def chapter8_itsm_devops():
    title("第8章: ITSM と DevOps の融合")

    heading("8-1. SRE vs ITIL: 対立ではなく補完")
    p("""
    SRE (Site Reliability Engineering): Googleが提唱したソフトウェア
    エンジニアリングのアプローチで運用を行う手法。

    ITIL ←→ SRE の対応関係:
    - インシデント管理 ←→ On-call, Incident Response
    - 問題管理 ←→ Postmortem (ポストモーテム)
    - 変更管理 ←→ Release Engineering, Progressive Delivery
    - SLA ←→ SLO/SLI/Error Budget
    - CMDB ←→ Service Catalog + IaC

    本質は同じ「サービスの信頼性を管理する」こと。
    ITILはプロセス側から、SREはエンジニアリング側からアプローチ。
    両者を理解する人材が最も価値が高い。
    """)

    heading("8-2. SLA → SLO への進化")
    p("""
    SLA (Service Level Agreement): 契約上の合意。違反するとペナルティ。
    SLO (Service Level Objective): 内部目標。エラーバジェットの基準。
    SLI (Service Level Indicator): 実測値。SLOの判定に使う指標。

    例:
      SLI: 過去30日間のHTTPリクエスト成功率 = 99.95%
      SLO: 成功率 99.9% 以上を維持する (内部目標)
      SLA: 成功率 99.5% を保証する (顧客との契約)

    エラーバジェット: SLO 99.9% の場合、月間約43分のダウンタイムが
    「使える予算」。この予算が残っていれば新機能リリースを許可、
    枯渇したら信頼性改善に集中する。

    この仕組みにより「速度 vs 安定性」のトレードオフを定量化し、
    開発チームと運用チームが共通の判断基準を持てる。
    """)

    heading("8-3. Value Stream Mapping (VSM)")
    p("""
    VSM: アイデアから本番稼働までの全工程を可視化し、
    ムダ (待ち時間、手戻り、手動作業) を特定する手法。
    製造業のリーン生産方式から派生。

    ITSMにおけるVSMの例:
    要件定義(3日) → 開発(5日) → [待ち: CAB承認 2日]
    → テスト(3日) → [待ち: リリースウィンドウ 4日]
    → デプロイ(1日) → 運用開始

    リードタイム: 18日、実作業時間: 12日、待ち時間: 6日
    → プロセス効率 = 12/18 = 67%

    改善ポイント:
    - CAB承認の自動化 → 2日 → 0日
    - CD導入でリリースウィンドウ廃止 → 4日 → 0日
    → リードタイム: 12日、効率100%
    """)

    heading("8-4. 自動化: IaC と ChatOps")
    p("""
    IaC (Infrastructure as Code):
    インフラ構成をコードで管理。Terraform, Ansible, CloudFormation。
    ITSM的な価値:
    - 変更管理: コードレビュー = 変更承認、Git履歴 = 変更記録
    - 構成管理: IaCコード自体がCMDB的な役割を果たす
    - インシデント管理: 環境の再構築が迅速 (MTTR短縮)

    ChatOps:
    SlackやTeams上でbotを通じて運用操作を実行する手法。
    - インシデント管理: /incident create → チケット自動作成
    - 変更管理: /deploy approve → 承認フロー実行
    - 情報共有: 障害状況がチャットに集約 → 可視性向上
    - 監査証跡: チャットログが操作記録になる

    ChatOpsは「協働し、可視性を高めよ」というITIL指導原則の実践。
    """)

    heading("8-5. Platform Engineering との関係")
    p("""
    Platform Engineering: 開発者体験 (DX) を向上させるための
    内部プラットフォームを構築・運営する活動。

    ITSM/ITILとの関係:
    - サービスカタログ → Internal Developer Platform (IDP) のカタログ
    - サービスリクエスト → セルフサービスでの環境構築
    - 変更管理 → Golden Path (推奨パターン) の提供
    - ナレッジ管理 → 開発者ドキュメントポータル

    Platform Engineeringは「ITSM + DevOps + 開発者体験」の統合。
    社内の開発者を「顧客」として、ITILのプラクティスを
    エンジニアリングアプローチで実装したものと言える。
    """)

    misconception(
        "DevOpsを導入すればITILは不要になる",
        "DevOpsは「文化と自動化」、ITILは「プロセスとガバナンス」。"
        "エンタープライズでは両方が必要。ITIL 4はDevOpsとの"
        "共存を前提に設計されている"
    )

    interview("""
    Q: ITSMとDevOpsの関係をどう考えますか？
    A: 対立ではなく補完関係です。DevOpsの自動化・文化を
       ITILのプロセス・ガバナンスの枠組みの中で実践します。
       例えばCI/CDパイプラインを標準変更として承認し、
       SLOベースのエラーバジェットでリリース判断を行います。
    """)


