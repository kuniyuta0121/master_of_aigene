# Chapter 4: コスト管理
# ============================================================
def chapter4_cost():
    title("Chapter 4: コスト管理")

    heading("4-1. コスト見積技法")
    p("""
    精度が低い順に並べる（プロジェクト初期から後期へ）。

    【類推見積（Analogous）】精度: -25%〜+75%
      過去の類似PJの実績を基に見積。
      利点: 早い・安い   欠点: 精度が低い
      例: 「前回の基幹システムは3億だったから今回も3億前後」

    【パラメトリック見積（Parametric）】精度: -15%〜+25%
      統計的な関係式を使う。
      例: 「1画面あたり50万円 × 100画面 = 5,000万円」
      前提: 過去データが十分にあること

    【ボトムアップ見積（Bottom-up）】精度: -5%〜+10%
      WBSの最下層（ワークパッケージ）ごとに見積もり、積み上げる。
      利点: 最も正確   欠点: 時間がかかる
      実務: 詳細設計後に実施する

    【三点見積（Three-point）】
      O, M, P の3点でPERT式計算。上記のどの技法にも組合せ可能。
    """)

    heading("4-2. EVM（Earned Value Management）の全体像")
    p("""
    EVMはプロジェクトの「健康診断」。
    スケジュールとコストの進捗を統合的に測定する。

    なぜEVMが必要か？
    ・「予算の50%を使った」だけでは順調か分からない
    ・「作業の50%が終わった」だけでも不十分
    ・EVMは「使ったお金」と「得た価値」を比較する
    """)

    heading("4-3. EVMの3つの基本指標")
    p("""
    【PV（Planned Value / 計画価値）】
      この時点までに「完了しているはずだった」作業の予算額。
      いわば「計画の進捗を金額で表したもの」。

    【EV（Earned Value / 出来高）】
      実際に「完了した」作業の予算額。
      注意: かかった実費ではなく、完了作業の「予算上の価値」。

    【AC（Actual Cost / 実コスト）】
      実際に「使った」金額。

    身近な例:
      家の建築。予算1000万、工期10ヶ月。
      5ヶ月目の時点で:
      PV = 500万（計画では50%完了のはず）
      EV = 400万（実際には40%しか完了していない）
      AC = 600万（しかし600万も使ってしまった）
      → スケジュール遅延かつコスト超過
    """)

    heading("4-4. EVMの差異指標と効率指標")
    p("""
    【差異指標（Variance）】
      SV（Schedule Variance）= EV - PV
        正 → 前倒し、負 → 遅延
      CV（Cost Variance）= EV - AC
        正 → 予算内、負 → 超過

    【効率指標（Performance Index）】
      SPI（Schedule Performance Index）= EV / PV
        >1 → 前倒し、<1 → 遅延
      CPI（Cost Performance Index）= EV / AC
        >1 → 効率的、<1 → 非効率

    上の例で計算:
      SV = 400 - 500 = -100（遅延）
      CV = 400 - 600 = -200（超過）
      SPI = 400/500 = 0.8（計画の80%ペース）
      CPI = 400/600 = 0.67（1円で0.67円分の価値）
    """)

    heading("4-5. EVMの予測指標")
    p("""
    【EAC（Estimate at Completion / 完了時総見積）】
      プロジェクト完了時の「予想総コスト」。

      計算パターン（状況で使い分ける）:
      ① EAC = BAC / CPI
        → 現在の効率が今後も続く場合（最も一般的）
      ② EAC = AC + (BAC - EV)
        → 今後は計画通りの効率に戻る場合
      ③ EAC = AC + (BAC - EV) / (CPI × SPI)
        → スケジュール遅延もコストに影響する場合

      BAC = Budget at Completion（当初予算総額）

    【ETC（Estimate to Complete / 残作業見積）】
      ETC = EAC - AC（あといくら必要か）

    【TCPI（To-Complete Performance Index）】
      残りの作業をBAC内で終えるために必要な効率
      TCPI = (BAC - EV) / (BAC - AC)
      > 1なら「今まで以上の効率」が必要＝黄色信号
    """)

    misconception(
        "EVMはウォーターフォールでしか使えない",
        "アジャイルでもバーンダウンチャートと組み合わせてEVMは適用可能。"
        "スプリントのストーリーポイント完了率をEVとして計算する"
    )

    interview("""
    Q: プロジェクトのコスト状況をどう報告しますか？
    A: EVMを使います。PV/EV/ACの3指標からCPI・SPIを算出し、
       CPI<1ならコスト超過、SPI<1ならスケジュール遅延と判断。
       EAC=BAC/CPIで完了時総コストを予測し、対策を提案します。
    """)


