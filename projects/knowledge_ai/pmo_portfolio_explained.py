# Chapter 3: スケジュール管理
# ============================================================
def chapter3_schedule():
    title("Chapter 3: スケジュール管理")

    heading("3-1. クリティカルパス法（CPM）")
    p("""
    CPM = Critical Path Method。プロジェクトの最短完了日を求める手法。

    【クリティカルパスとは】
    ・全タスクの依存関係をネットワーク図にしたとき、
      最も長い経路＝クリティカルパス
    ・この経路上のタスクが1日遅れると、全体が1日遅れる
    ・フロート（余裕）= 0 のタスクの連鎖

    【フロート（スラック）】
    ・トータルフロート: プロジェクト完了に影響なく遅延できる日数
    ・フリーフロート: 直後のタスクに影響なく遅延できる日数
    ・クリティカルパス上のタスクはフロート＝0

    なぜ重要か？
    PMは全タスクを均等に管理する余裕はない。
    CPMにより「ここだけは絶対に遅らせてはいけない」を特定できる。
    """)

    heading("3-2. PERT（Program Evaluation and Review Technique）")
    p("""
    PERTは不確実性を考慮したスケジュール見積手法。

    3点見積: 楽観値(O) + 最頻値(M) + 悲観値(P)
    期待値 = (O + 4M + P) / 6
    標準偏差 = (P - O) / 6

    なぜ3点見積が有効か？
    ・人間は「最頻値」だけで見積もりがち（楽観バイアス）
    ・最悪ケースを明示的に考えることで現実的な見積になる
    ・標準偏差を使えば「90%の確率で○日以内」と言える
    """)

    heading("3-3. ガントチャートとマイルストーン")
    p("""
    【ガントチャート】
      横軸=時間、縦軸=タスク のバーチャート。
      利点: 直感的、進捗が一目で分かる
      欠点: 依存関係が見えにくい（矢印で補う）

    【マイルストーン】
      所要期間ゼロの「チェックポイント」。
      例: 要件定義完了、結合テスト開始、本番リリース

      マイルストーンの設定ポイント:
      ・フェーズの境界に置く
      ・ステークホルダーへの報告タイミングに合わせる
      ・多すぎると形骸化するので、重要なものだけ
    """)

    heading("3-4. CCPM（クリティカルチェーン・プロジェクトマネジメント）")
    p("""
    CCPM = TOC（制約理論）をプロジェクト管理に適用したもの。
    エリヤフ・ゴールドラットが提唱。

    従来のCPMの問題点:
    ・各タスクに「安全マージン」が入っている
    ・パーキンソンの法則: 余裕があると使い切ってしまう
    ・学生症候群: 期限直前まで着手しない

    CCPMのアプローチ:
    1. 各タスクの見積から安全マージンを外す（50%確率の見積）
    2. 外したマージンを「プロジェクトバッファ」としてまとめる
    3. リソース競合も考慮（クリティカルチェーン）
    4. バッファ消費率で進捗を管理

    比喩: 各人がポケットにお金を隠すのではなく、
          チーム全体の「予備費」としてプールするイメージ。
    """)

    heading("3-5. リソース平準化と山崩し")
    p("""
    【リソース平準化（Resource Leveling）】
      リソース制約を超えないようにスケジュールを調整する。
      結果: プロジェクト期間が延びることがある。

    【リソース山崩し（Resource Smoothing）】
      フロートの範囲内でリソースの山を均す。
      結果: プロジェクト期間は変わらない。

    違い: 平準化は「期間延長OK」、山崩しは「期間厳守」。
    """)

    misconception(
        "クリティカルパスは1本だけ",
        "複数のクリティカルパスが存在する場合がある。"
        "その場合リスクは更に高い（どちらが遅れても全体に影響）"
    )

    interview("""
    Q: スケジュール遅延のリスクにどう対処しますか？
    A: まずCPMでクリティカルパスを特定し、重点管理します。
       見積にはPERTの3点見積を使い楽観バイアスを排除します。
       CCPMのバッファ管理を導入し、個別タスクの安全マージンを
       プロジェクトバッファに集約することで、全体最適化します。
    """)


