def chapter6_portfolio():
    title("Chapter 6: ポートフォリオ管理")

    heading("6-1. ポートフォリオ vs プログラム vs プロジェクト")
    p("""
    この3層構造を理解することがPMO理解の基盤。

    【プロジェクト】一時的な取り組み。開始と終了がある。
      例: 顧客管理システムの構築

    【プログラム】関連する複数プロジェクトの集合。
      個別PJの合計以上の「便益」を得るために統合管理する。
      例: DXプログラム（顧客管理PJ + データ基盤PJ + AI活用PJ）

    【ポートフォリオ】組織の戦略目標を達成するための
      プログラム・プロジェクト・その他作業の集合。
      「何に投資し、何をやらないか」を決める。

    比喩:
    ・ポートフォリオ = 投資信託のポートフォリオ
      （株式/債券/不動産のバランスを決める）
    ・プログラム = 個別の株式銘柄グループ
    ・プロジェクト = 個別銘柄の売買
    """)

    heading("6-2. 戦略整合性")
    p("""
    ポートフォリオ管理の最重要概念＝戦略整合性（Strategic Alignment）。

    すべてのPJは企業戦略に紐づくべき。
    「なぜこのPJをやるのか？」→「戦略目標Xの達成に貢献するから」

    戦略整合性がないPJ = ゾンビプロジェクト
    ・始めた理由が曖昧
    ・やめる判断ができない
    ・リソースを食い潰す

    PMOの重要な役割: ゾンビPJを特定し、中止を提言すること。
    これは政治的に難しいが、組織にとって最も価値がある。
    """)

    heading("6-3. 投資配分: Run / Grow / Transform")
    p("""
    IT投資を3つのバケットに分類する有名なフレームワーク。

    【Run（維持）】既存システムの維持・保守
      目安: 投資全体の60-70%（多くの企業の現実）
      例: 基幹システム保守、セキュリティパッチ適用

    【Grow（成長）】既存事業の拡大
      目安: 15-25%
      例: ECサイトの機能追加、CRM導入

    【Transform（変革）】新規事業・破壊的イノベーション
      目安: 10-15%
      例: AI新サービス、ブロックチェーン実証実験

    問題: 多くの企業はRunが80%超で、Transformに投資できない。
    ポートフォリオ管理の目標: Runを効率化し、Grow/Transformの
    比率を高めること。クラウド移行がRunの削減策として注目される理由。
    """)

    heading("6-4. ポートフォリオバランスとパイプライン管理")
    p("""
    【ポートフォリオバランス】
      以下の観点でバランスを取る:
      ・リスク: 高リスク案件ばかりにしない
      ・時間軸: 短期回収と長期投資を混ぜる
      ・事業領域: 特定事業に偏らない
      ・技術: レガシーと新技術を組み合わせる

    【パイプライン管理】
      プロジェクトの「候補 → 評価 → 承認 → 実行 → 完了」
      を流れ（パイプライン）として管理する。

      評価基準の例:
      ・戦略整合性スコア
      ・ROI / NPV / IRR（投資対効果）
      ・リスクスコア
      ・リソース必要量
      ・依存関係の複雑さ

      NPV（正味現在価値）:
        将来のキャッシュフローを現在価値に割り引いた合計。
        NPV > 0 なら投資価値あり。
      IRR（内部収益率）:
        NPV = 0 となる割引率。ハードルレートより高ければGo。
    """)

    misconception(
        "全てのプロジェクトを承認して並行実行すべき",
        "リソースは有限。ポートフォリオ管理の本質は「やらないことを決める」。"
        "少数の戦略的PJに集中した方が成功率は高い"
    )

    interview("""
    Q: ポートフォリオ管理とプロジェクト管理の違いは？
    A: プロジェクト管理は「1つのPJを成功させる」こと。
       ポートフォリオ管理は「組織の戦略目標に対して、どのPJに
       投資し、どのPJをやめるかを判断する」ことです。
       Run/Grow/Transformの投資配分で全体最適を図ります。
    """)


