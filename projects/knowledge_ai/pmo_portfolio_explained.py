# Chapter 5: リスク管理
# ============================================================
def chapter5_risk():
    title("Chapter 5: リスク管理")

    heading("5-1. リスクとは何か")
    p("""
    リスク＝「不確実な事象で、発生するとプロジェクトの目標に
    プラスまたはマイナスの影響を与えるもの」。

    重要: リスクには「良いリスク（機会）」もある。
    例: 新技術が想定より早く成熟→工数削減の機会

    リスク ≠ 問題（Issue）
    リスク: まだ起きていない  問題: もう起きている
    リスク管理: 事前対応      問題管理: 事後対応
    """)

    heading("5-2. リスク特定の手法")
    p("""
    【ブレインストーミング】
      チーム全員で洗い出す。批判なしが原則。
      コツ: カテゴリ（技術/外部/組織/PM）で分けて漏れを防ぐ

    【デルファイ法】
      匿名で専門家に意見を集め、集約→再質問を繰り返す。
      利点: 声の大きい人に引きずられない
      欠点: 時間がかかる

    【チェックリスト法】
      過去のPJのリスクリストを再利用。
      利点: 漏れが少ない  欠点: 新しいリスクを見逃す

    【SWOT分析】
      強み・弱み・機会・脅威の4象限でリスクを構造化。

    【前提条件分析】
      プロジェクト憲章の前提条件を「もし違ったら？」で検証。
    """)

    heading("5-3. 定性的リスク分析")
    p("""
    【確率×影響度マトリクス】
      各リスクを「発生確率」と「影響度」の2軸で評価。

      確率:  高(>70%) / 中(30-70%) / 低(<30%)
      影響:  高(致命的) / 中(重大) / 低(軽微)

      リスクスコア = 確率 × 影響度
      → スコアが高いものから優先対応

    なぜ定性分析を先にやるか？
    ・全リスクに定量分析するのはコストが高い
    ・まず定性で優先順位をつけ、上位だけ定量分析する
    """)

    heading("5-4. 定量的リスク分析")
    p("""
    【モンテカルロ・シミュレーション】
      各タスクの所要期間やコストに確率分布を設定し、
      数千〜数万回のシミュレーションを実行。
      結果: 「90%の確率で○日以内に完了」等が分かる。

      なぜ有効か:
      ・個別リスクの「組み合わせ効果」が分かる
      ・経営に確率ベースで説明できる

    【感度分析（トルネードダイアグラム）】
      「どのリスク要因がプロジェクトに最も影響するか」を可視化。
      棒が長いリスク＝影響が大きい → 優先対応。
    """)

    heading("5-5. リスク対応戦略")
    p("""
    【脅威（マイナスのリスク）への4戦略】
    ① 回避（Avoid）: リスク原因を除去する
       例: 不安定な技術を使わない
    ② 転嫁（Transfer）: 第三者にリスクを移す
       例: 保険をかける、固定価格契約で外注する
    ③ 軽減（Mitigate）: 確率や影響を下げる
       例: プロトタイプで技術検証する
    ④ 受容（Accept）: 対策せず受け入れる
       例: 小さなリスクはコンティンジェンシー予備で対応

    【機会（プラスのリスク）への4戦略】
    ① 活用（Exploit）: 機会を確実にする
    ② 共有（Share）: パートナーと協力して活かす
    ③ 強化（Enhance）: 確率を高める
    ④ 受容（Accept）: 来たら活かす
    """)

    heading("5-6. RAIDログ")
    p("""
    RAID = Risks / Assumptions / Issues / Dependencies

    【Risks】まだ起きていない不確実な事象
    【Assumptions】真と仮定しているが未確認の事項
    【Issues】既に発生している問題
    【Dependencies】他チーム・他PJ・外部への依存

    RAIDログは週次のPJ会議で必ずレビューする。
    PMOが全PJのRAIDを横断的に見ることで、
    「A案件のリスクがB案件にも波及する」を早期発見できる。
    """)

    misconception(
        "リスク管理は計画フェーズで一度やれば十分",
        "リスクは常に変化する。反復的にリスク再評価を行うべき。"
        "少なくとも月次、重要局面では週次で見直す"
    )

    interview("""
    Q: リスク管理のプロセスを説明してください。
    A: 特定→定性分析→定量分析→対応計画→監視の5ステップです。
       特定にはブレスト・チェックリスト・前提条件分析を使い、
       確率×影響マトリクスで優先順位をつけます。上位リスクには
       回避/転嫁/軽減/受容から適切な戦略を選び、RAIDログで
       継続的に追跡します。
    """)


