def chapter2_pm_fundamentals():
    title("Chapter 2: プロジェクトマネジメント基礎")

    heading("2-1. PMBOK 第7版の大転換")
    p("""
    PMBOK 6th → 7th で大きなパラダイムシフトが起きた。

    【6th】プロセスベース: 49のプロセスを覚えて実行する
    【7th】原則ベース: 12の原則と8つのパフォーマンスドメインで
           「なぜそうするか」を理解して適用する

    なぜ変わったか？
    ・アジャイル/ハイブリッドの普及で「1つのプロセス」が合わなくなった
    ・原則ベースなら予測型でもアジャイルでも適用できる
    ・「How」より「Why」を重視する方向へ
    """)

    heading("2-2. PMBOK 7th の12原則")
    p("""
    1. スチュワードシップ: 資源を預かり、責任を持って扱う
    2. チーム: 協力的なチーム環境を作る
    3. ステークホルダー: 効果的に関わり合う
    4. 価値: 価値に焦点を当てる（最重要）
    5. システム思考: 相互作用を認識・評価・対応する
    6. リーダーシップ: 動機づけ・影響・コーチング・学習
    7. テーラリング: 状況に応じてアプローチを調整する
    8. 品質: プロセスと成果物に品質を組み込む
    9. 複雑さ: 複雑さに対処する
    10. リスク: リスク対応を最適化する
    11. 適応力・レジリエンス: 変化に適応する
    12. 変革: 望ましい将来の状態を実現する

    覚え方: 最も重要なのは「4. 価値」。
    プロジェクトの存在意義は価値を届けること。
    他の11原則は全て価値デリバリーを支えるためにある。
    """)

    heading("2-3. 8つのパフォーマンスドメイン")
    p("""
    ① ステークホルダー: 誰のためにやるか
    ② チーム: 誰がやるか
    ③ 開発アプローチとライフサイクル: どうやるか
    ④ 計画: いつ・何を・いくらで
    ⑤ プロジェクト作業: 実行とモニタリング
    ⑥ デリバリー: 成果物を届ける
    ⑦ 測定: 進捗と成果を測る
    ⑧ 不確実性: リスクと曖昧さに対処する

    6thの「10の知識エリア」から「8ドメイン」へ。
    数が減ったのではなく、より統合的な視点になった。
    """)

    heading("2-4. ライフサイクルの選択")
    p("""
    【予測型（Predictive/ウォーターフォール）】
      要件が明確・変更が少ない・規制が厳しい場合
      例: 医療機器、会計システムの法対応

    【適応型（Adaptive/アジャイル）】
      要件が不確実・変更が頻繁・早期フィードバックが必要な場合
      例: 新規Webサービス、AI/ML実験プロジェクト

    【ハイブリッド】
      インフラは予測型、アプリは適応型 等、混合
      現実の大規模PJはほぼハイブリッド

    選択基準: 不確実性の度合いで決める。
    不確実性が高い → 適応型（小さく試して学ぶ）
    不確実性が低い → 予測型（計画通り実行が効率的）
    """)

    heading("2-5. プロジェクト憲章とWBS")
    p("""
    【プロジェクト憲章（Charter）】
      プロジェクトの「出生届」。経営がPMに権限を与える文書。
      含む内容: 目的・スコープ概要・主要ステークホルダー・
              予算概算・マイルストーン・PM任命・前提条件

    【WBS（Work Breakdown Structure）】
      成果物を階層的に分解した図。
      WBSの最下層＝ワークパッケージ（見積可能な単位）

      なぜWBSが重要か？
      ・「漏れ」を防ぐ（全体を可視化するから）
      ・見積の精度が上がる（小さく分けるほど正確）
      ・責任分担が明確になる

      注意: WBSは「作業」ではなく「成果物」で分解する。
      × ～する（動詞）  ○ ～（名詞: 設計書、テスト結果）
    """)

    misconception(
        "WBSはタスク一覧表と同じ",
        "WBSは成果物の階層構造。タスク一覧はWBSから導出する別の成果物"
    )

    interview("""
    Q: PMBOK 7thで何が変わりましたか？
    A: プロセスベース（49プロセス）から原則ベース（12原則+8ドメイン）
       に転換しました。背景はアジャイル/ハイブリッドの普及です。
       「価値」を最上位の原則とし、予測型でも適応型でも適用可能な
       フレームワークになりました。
    """)


