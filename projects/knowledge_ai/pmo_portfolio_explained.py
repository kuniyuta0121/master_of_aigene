#!/usr/bin/env python3
"""
PMO & Portfolio Management - 深掘り解説

PMO（Project Management Office）とポートフォリオ管理の全体像を
「なぜそうなのか」を軸に解説する。
ITコンサルタントが現場で使う実践知識もカバー。
"""
import textwrap

SEP = "=" * 70
THIN = "-" * 70

def title(text):
    print(f"\n{SEP}\n  {text}\n{SEP}")

def heading(text):
    print(f"\n{THIN}\n  {text}\n{THIN}")

def p(text):
    for line in textwrap.dedent(text).strip().split("\n"):
        print(f"  {line}")

def interview(text):
    print()
    print("  【面接向けに整理すると】")
    for line in textwrap.dedent(text).strip().split("\n"):
        print(f"  {line}")

def misconception(wrong, right):
    print(f"\n  ⚠ よくある誤解: {wrong}")
    print(f"  → 正確には: {right}")


