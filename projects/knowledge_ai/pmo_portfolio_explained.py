# Chapter 1: PMOとは何か
# ============================================================
def chapter1_what_is_pmo():
    title("Chapter 1: PMOとは何か")

    heading("1-1. PMOが生まれた背景")
    p("""
    企業が同時に複数プロジェクトを走らせるようになると、
    個々のPMだけでは「全体最適」が取れなくなる。

    例えるなら、料理人（PM）が各自で料理を作っていると、
    食材の取り合い・キッチンの混雑・メニューの重複が起きる。
    そこで「キッチンマネージャー」的な存在＝PMOが必要になる。

    PMOの本質的な価値:
    ・プロジェクト横断の可視化（今どのPJが赤信号か）
    ・標準化（各PMが我流にならないよう方法論を統一）
    ・リソース最適配分（A案件とB案件でエンジニアを取り合わない）
    ・経営への橋渡し（個別PJの状況を経営判断に使える形に加工）
    """)

    heading("1-2. PMOの3類型")
    p("""
    PMIが定義する3つのPMO類型を、権限の強さ順に並べる。

    【支援型PMO（Supportive）】- 権限: 低
      役割: テンプレート提供、ベストプラクティスの共有、研修
      比喩: 図書館の司書。情報を整理して提供するが、
            「この本を読め」とは強制しない。
      適する状況: PMが十分に成熟している組織

    【管理型PMO（Controlling）】- 権限: 中
      役割: 方法論の準拠を求める、レビューゲートを設ける、
            進捗報告フォーマットを統一
      比喩: 交通警察。ルールを定め、違反をチェックする。
            ただし運転（実行）はドライバー（PM）に任せる。
      適する状況: ある程度の標準化が必要な中規模組織

    【指揮型PMO（Directive）】- 権限: 高
      役割: PMOがPMを直接マネジメント。PMはPMO配下。
            プロジェクトの優先順位づけ・リソース配分も直接行う。
      比喩: 軍の司令部。作戦の立案・指揮・人員配置まで行う。
      適する状況: 大規模組織、PMの成熟度にばらつきがある場合
    """)

    misconception(
        "PMOは管理ばかりする官僚組織",
        "良いPMOはPMを支援し、プロジェクト成功率を上げる存在。"
        "管理はあくまで手段であり、目的は価値のデリバリー"
    )

    heading("1-3. PM vs PMO vs PgM の違い")
    p("""
    【PM（Project Manager）】
      1つのプロジェクトの成功に責任を持つ。
      スコープ・スケジュール・コスト・品質を管理。

    【PgM（Program Manager）】
      関連する複数プロジェクトを束ねて、
      「個別PJの合計以上の価値」を生み出す。
      例: 基幹システム刷新プログラム＝インフラPJ＋アプリPJ＋移行PJ

    【PMO】
      組織横断でプロジェクト管理を支援・標準化する組織体。
      PMやPgMが「正しいやり方」で仕事できるようにする。

    PMがプレイヤーなら、PgMは監督、PMOはリーグ運営本部。
    """)

    heading("1-4. PMOの主要機能")
    p("""
    ・方法論の策定と維持（PMBOKベース or アジャイルフレームワーク）
    ・プロジェクト管理ツールの選定・運用
    ・リソース管理（要員プール、スキルマトリクス）
    ・ポートフォリオレポーティング
    ・PM人材の育成・評価
    ・教訓（Lessons Learned）のナレッジ化
    ・プロジェクト監査
    """)

    interview("""
    Q: PMOの役割を説明してください。
    A: PMOは組織横断でプロジェクト管理を支援する組織体です。
       3類型（支援型/管理型/指揮型）があり、組織の成熟度に応じて
       選択します。本質的な価値は、標準化・可視化・リソース最適配分
       を通じて、プロジェクト成功率を組織全体で高めることです。
    """)


