def chapter7_governance():
    title("Chapter 7: ガバナンス・報告")

    heading("7-1. ステージゲートレビュー")
    p("""
    プロジェクトのフェーズ間に「関門（Gate）」を設け、
    次フェーズに進むか判断する仕組み。

    典型的なゲート:
    G0: 構想承認（やる価値があるか）
    G1: 計画承認（計画は妥当か）
    G2: 設計承認（技術的に実現可能か）
    G3: テスト完了（品質基準を満たすか）
    G4: リリース承認（本番投入してよいか）

    各ゲートの判断:
    ・Go（次へ進む）
    ・Kill（中止）
    ・Hold（保留）
    ・Recycle（やり直し）

    なぜゲートが重要か？
    ・「サンクコストの罠」を防ぐ（ここまで来たからやめられない）
    ・早期にKillすればリソースを他に振り向けられる
    """)

    heading("7-2. ダッシュボードとRAGステータス")
    p("""
    【RAGステータス】
      R（Red）: 重大な問題。計画からの大幅逸脱。即時対応必要。
      A（Amber/黄）: 注意が必要。対策を講じないとRedになる。
      G（Green）: 順調。計画通り。

    RAGの運用ポイント:
    ・評価基準を客観的に定義する（CPI<0.9→Red 等）
    ・主観で「大丈夫です」と言わせない仕組みにする
    ・Amberが最も重要（Redになる前に手を打てるから）

    【ダッシュボード設計の原則】
    ・1画面で全体が見える
    ・ドリルダウンで詳細に入れる
    ・RAG + EVM + マイルストーン進捗を統合
    ・更新頻度を明確に（週次 or リアルタイム）
    """)

    heading("7-3. ステアリングコミッティー")
    p("""
    ステアリングコミッティー＝プロジェクトの最高意思決定機関。
    steering = 舵取り。プロジェクトの方向を決める。

    構成: スポンサー、主要ステークホルダー、PM、PMO
    頻度: 月次（大規模PJは隔週）
    議題: 進捗報告・リスク/課題のエスカレーション・意思決定

    PMOの役割:
    ・会議の準備（資料整理・アジェンダ作成）
    ・客観的データの提供（PMの主観に頼らない）
    ・決定事項のフォローアップ
    """)

    heading("7-4. エスカレーションプロセス")
    p("""
    エスカレーション＝自分の権限では解決できない問題を上位に上げること。

    設計のポイント:
    ・いつ上げるか（基準を明確化。例: 遅延5日超でPMO→10日超で
      ステアリングコミッティー）
    ・誰に上げるか（エスカレーションパスを事前定義）
    ・何を添えるか（現状・影響・対策案・推奨を必ずセット）

    よくある失敗: エスカレーションが遅い。
    「自分で何とかしよう」→ 手遅れ → 炎上。
    早めのエスカレーション ＝ 弱さではなく、プロのスキル。
    """)

    heading("7-5. 変更管理（CCB）")
    p("""
    CCB = Change Control Board（変更管理委員会）。
    スコープ・スケジュール・コストの変更を審議・承認する。

    変更管理プロセス:
    1. 変更要求の提出（誰でも出せる）
    2. 影響分析（スコープ/コスト/スケジュール/品質への影響）
    3. CCBでの審議
    4. 承認 or 却下
    5. 承認された変更の実行とベースライン更新

    なぜ厳格な変更管理が必要か？
    ・スコープクリープ（気づいたら要件が膨らんでいる）を防ぐ
    ・「誰が承認したの？」を追跡可能にする
    ・変更のコスト影響を見える化する
    """)

    heading("7-6. 教訓管理（Lessons Learned）")
    p("""
    教訓管理の目的:
    同じ失敗を繰り返さない。成功パターンを横展開する。

    実施タイミング:
    ・各フェーズ終了時（振り返り）
    ・プロジェクト完了時（クロージング）
    ・問題発生時（ホットレビュー）

    PMOの役割:
    ・教訓をナレッジベースに蓄積
    ・新PJ立上げ時に関連する過去の教訓を提供
    ・教訓を方法論やテンプレートに反映

    よくある問題: 教訓を集めるが誰も読まない。
    対策: チェックリストに組み込む。
    ゲートレビューで「過去の教訓を確認したか？」を必須項目にする。
    """)

    misconception(
        "ガバナンスは官僚的で価値を生まない",
        "適切なガバナンスはPJを守る仕組み。問題は過剰なガバナンス。"
        "テーラリング（原則7）で組織・PJ規模に合わせて調整すべき"
    )

    interview("""
    Q: プロジェクトガバナンスをどう設計しますか？
    A: ステージゲート・ステアリングコミッティー・CCB・
       エスカレーションプロセスの4本柱で設計します。
       RAGステータスとEVMで客観的なモニタリングを行い、
       意思決定の迅速化と透明性を両立させます。
       ただしPJ規模に応じたテーラリングが重要です。
    """)


