def chapter_5_explained():
    title("Chapter 5: AIを組み込む (深掘り)")

    heading("RNN から Transformer へ")

    p("""\
    なぜ Transformer を理解する必要があるか:
    TechPulse の AI 機能 (要約、モデレーション、推薦) は
    全て Transformer ベースの LLM を使っています。

    RNN (Recurrent Neural Network) とは:
    テキストを1トークンずつ順番に処理するニューラルネットワークです。
    各ステップで「隠れ状態」を次のステップに渡します。

    イメージ:
    「私 は 猫 が 好き」を処理する場合:
    「私」→ 隠れ状態1 → 「は」→ 隠れ状態2 → 「猫」→ ...

    RNN の問題:
    1. 逐次処理: 「猫」を処理するには「は」の処理が終わるのを待つ必要がある
       → GPU の並列計算能力を活かせない
    2. 勾配消失: 長い文で最初の方の情報が失われやすい
       (LSTM や GRU で緩和されたが、根本的解決ではない)

    Transformer の革新:
    「全トークンを同時に見る」ことで並列処理を可能にした。
    これが Self-Attention メカニズムです。
    """)

    heading("Self-Attention の仕組み")

    p("""\
    Self-Attention は「各トークンが他の全トークンとの関連度を計算する」仕組みです。

    Q (Query), K (Key), V (Value):
    入力の各トークンから、3つのベクトルを計算します。

    直感的理解:
    図書館で考えると:
    - Query: 「私が探しているもの」
    - Key: 「各本のタイトル (検索キー)」
    - Value: 「各本の中身」

    1. Q と K の内積を計算 → 関連度スコア (どの本が関係ありそうか)
    2. スコアを √d_k で割る
    3. Softmax で確率分布にする (合計が1になる)
    4. この確率で V を重み付き平均する (関連する本の内容を混ぜる)

    なぜ √d_k で割るか:
    d_k (キーの次元数) が大きいと、内積の値が大きくなりすぎます。
    大きすぎる値に Softmax を適用すると、
    勾配がほぼ0になり学習が進まなくなります (勾配消失)。
    √d_k で割ることでスケールを適切に保ちます。

    Multi-Head Attention:
    1つの Attention だけでなく、複数の「ヘッド」で別々に Attention を計算し、
    結果を結合します。
    各ヘッドは「異なる関係性」を捉えます:
    - ヘッド1: 文法的な関係 (主語-動詞)
    - ヘッド2: 意味的な関係 (同義語)
    - ヘッド3: 位置的な関係 (近くの単語)
    """)

    heading("Positional Encoding")

    p("""\
    Transformer は全トークンを同時に処理するため、
    「どのトークンが何番目か」の位置情報を持ちません。

    RNN は逐次処理なので自然に位置情報を持っていましたが、
    Transformer は明示的に位置を教える必要があります。

    sin/cos による Positional Encoding:
    各位置に、sin と cos 関数で計算したベクトルを割り当てます。
    周波数を変えることで、近い位置同士は似たベクトル、
    遠い位置同士は異なるベクトルになります。

    このベクトルを Embedding (単語のベクトル表現) に加算することで、
    モデルが位置情報を活用できるようになります。
    """)

    heading("推薦システム")

    p("""\
    TechPulse のフィードには、ユーザーに合った記事を表示したい。

    協調フィルタリング:
    「似たユーザーが好んだ記事を推薦する」
    ユーザーAとBが多くの記事に同じ「いいね」をしているなら、
    Aが読んでBが読んでいない記事をBに推薦する。
    利点: 記事の内容を分析しなくてもいい
    欠点: Cold Start (新規ユーザー/新規記事はデータがない)

    コンテンツベース:
    「ユーザーが好んだ記事と似た内容の記事を推薦する」
    記事の特徴量 (タグ、トピック、Embedding) で類似度を計算。
    利点: Cold Start に強い (記事の内容があればいい)
    欠点: 「いつも同じ系統」になりやすい (セレンディピティがない)

    Embedding とコサイン類似度:
    Embedding はテキストを高次元ベクトルに変換したものです。
    意味が似た文は近いベクトルになります。

    なぜユークリッド距離ではなくコサイン類似度か:
    ユークリッド距離はベクトルの「大きさ」に影響されます。
    長い文と短い文ではベクトルの大きさが異なるため、
    意味が似ていても距離が大きくなります。
    コサイン類似度は「方向」だけを見るため、
    文の長さに依存しません。

    HNSW (Hierarchical Navigable Small World):
    数百万のベクトルから最も近いものを見つける近似最近傍検索。
    厳密な全探索は O(n) で遅すぎるため、
    グラフ構造を使って O(log n) で近似解を見つけます。
    精度は 95%+ で、速度は数百倍速い。
    """)

    heading("RAG (Retrieval-Augmented Generation)")

    p("""\
    なぜ RAG が必要か:
    LLM は学習データにない情報を知りません (知識カットオフ)。
    また、知っている情報でも間違った回答をすることがあります (ハルシネーション)。

    RAG の仕組み:
    1. ユーザーの質問を受け取る
    2. 関連する文書を検索して取得 (Retrieval)
    3. 取得した文書をコンテキストとして LLM に渡す
    4. LLM がコンテキストを基に回答を生成 (Generation)

    Chunking (チャンキング):
    長い文書をそのまま Embedding するのではなく、
    適切な長さに分割します。
    - 固定長チャンク: 512トークンずつ分割 (重複あり)
    - セマンティックチャンク: 意味的なまとまりで分割 (段落、セクション)

    BM25:
    テキスト検索の古典的アルゴリズムです。
    TF-IDF の改良版で、単語の出現頻度と文書長を考慮します。
    「Kafka」で検索すると、「Kafka」が多く含まれる文書が上位に来ます。

    Hybrid 検索:
    BM25 (キーワードマッチ) + Dense Retrieval (意味的類似度) を組み合わせ、
    両方の長所を活かします。
    """)

    heading("ガードレールと安全性")

    p("""\
    AIをプロダクションで使うには安全策が不可欠です。

    Prompt Injection:
    ユーザーが悪意ある入力でAIの動作を乗っ取る攻撃。

    Direct Injection:
    ユーザーが直接「以前の指示を無視して...」と入力する。

    Indirect Injection:
    検索結果に含まれるWebページに悪意ある指示が埋め込まれている。
    RAGで取得したコンテキストに攻撃が含まれるケース。

    4層防御:
    Layer 1: 入力サニタイズ (特殊文字エスケープ、長さ制限)
    Layer 2: プロンプト分離 (System/User/Tool の境界を明確に)
    Layer 3: LLM ベース検出器 (入力が攻撃かどうかを分類)
    Layer 4: 出力検証 (構造化出力の強制、PII マスキング)

    RAGAS 評価メトリクス:
    RAGの品質を定量的に測る指標群です。

    Faithfulness (忠実度):
    回答がコンテキストに基づいているか (ハルシネーションしていないか)

    Context Precision (文脈精度):
    検索で取得したコンテキストが正確か (不要な文書が混じっていないか)

    Answer Relevancy (回答関連性):
    回答がユーザーの質問に答えているか
    """)

    interview("""\
    「RAGの品質をどう評価するか？」

    「RAGASフレームワークの主要メトリクスを使います。
    Faithfulnessでハルシネーションがないか確認し、
    Context Precisionで検索精度を評価し、
    Answer Relevancyで回答の適切さを測ります。
    これらを組み合わせたスコアで、チャンキング戦略や
    検索アルゴリズムの改善効果を定量的に判断します。」
    """)


