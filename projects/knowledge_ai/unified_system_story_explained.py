#!/usr/bin/env python3
"""
Unified System Story - 深掘り解説版
====================================
unified_system_story.py の全内容を、専門書レベルの深さで解説。
全ての用語を定義し、「なぜ」を必ず説明し、段階的に理解を積み上げる。

実行: python3 unified_system_story_explained.py
"""
import textwrap

SEP = "=" * 70
THIN = "-" * 70


def title(text):
    print(f"\n{SEP}\n  {text}\n{SEP}")


def heading(text):
    print(f"\n{THIN}\n  {text}\n{THIN}")


def p(text):
    for line in textwrap.dedent(text).strip().split("\n"):
        print(f"  {line}")


def interview(text):
    print()
    print("  【面接向けに整理すると】")
    for line in textwrap.dedent(text).strip().split("\n"):
        print(f"  {line}")


def misconception(wrong, right):
    print(f"\n  ⚠ よくある誤解: {wrong}")
    print(f"  → 正確には: {right}")


# =====================================================================
# Chapter 0: プロローグ - システム全体像 (深掘り)
# =====================================================================
def chapter_0_explained():
    title("Chapter 0: プロローグ - システム全体像 (深掘り)")

    # --- OKR ---
    heading("OKR とは何か")

    p("""\
    OKR は Objectives and Key Results の略です。
    Google が採用して広まった目標設定フレームワークです。

    Objective (目標):
    - 定性的で、チームを鼓舞する方向性
    - 例: 「エンジニアの技術的意思決定を加速する」

    Key Results (主要成果指標):
    - 定量的で、達成度が客観的に測定できる
    - 例: 「月間アクティブ投稿者 10,000人」
    """)

    heading("なぜ KPI ではなく OKR か")

    p("""\
    KPI (Key Performance Indicator) は「現状の健全性を監視する」指標です。
    例えば「サーバー稼働率 99.9%」は KPI として適切です。

    一方 OKR は「挑戦的な変化を起こす」ための仕組みです。
    達成率 70% が理想とされ、100% 達成は「目標が簡単すぎた」と見なされます。

    つまり:
    - KPI: 現状維持を測る → 「売上が先月比で落ちていないか？」
    - OKR: 変化を起こす → 「新市場で利用者を3倍にする」

    TechPulse のような新規プロダクトでは、
    既存指標を監視するのではなく「どう成長するか」を決める必要があるため、
    OKR のほうが適しています。
    """)

    heading("良い OKR と悪い OKR")

    p("""\
    悪い OKR の例:
    - O: 「良いプロダクトを作る」 → 曖昧すぎて方向性がない
    - KR: 「がんばる」 → 測定不可能

    良い OKR の例 (TechPulse):
    - O: 「エンジニアの技術的意思決定を加速する」
    - KR1: 月間アクティブ投稿者 10,000人 (定量・挑戦的)
    - KR2: 記事発見→意思決定の平均時間を 50% 短縮 (行動変容を測定)
    - KR3: AI推薦クリック率 15% 以上 (プロダクト価値の指標)

    KR2 のように「行動変容」を測るのが最も価値のある KR です。
    なぜなら、単なるアクセス数ではなく、
    ユーザーが本当に価値を感じているかを測れるからです。
    """)

    misconception(
        "OKR は達成率100%を目指すもの",
        "70%程度が理想。100%なら目標が保守的すぎる (ムーンショットの考え方)"
    )

    # --- ユーザーストーリー ---
    heading("ユーザーストーリーの書き方")

    p("""\
    ユーザーストーリーとは、機能要件を「ユーザー視点」で書いた短い文です。
    フォーマット:

      「〈ロール〉として、〈したいこと〉をしたい。なぜなら〈理由〉だから。」

    英語では:
      "As a [role], I want [action], so that [benefit]."

    なぜこの形式を使うのか:
    1. 技術ではなくユーザーの目的に集中できる
    2. 「なぜなら」で価値の検証ができる
    3. 小さく分割しやすい (スプリントに収まる単位にする)

    TechPulse の例:
    - US-1: ユーザーとして、技術記事を投稿したい。
            なぜなら、自分の知見を共有して評価を得たいから。
    - US-2: ユーザーとして、興味分野の記事がフィードに表示されてほしい。
            なぜなら、自分で探す時間を節約したいから。
    - US-3: ユーザーとして、長い記事のAI要約を読みたい。
            なぜなら、全文読む時間がない時でも要点を掴みたいから。

    悪い例:
    - 「Kafka を導入したい」 → 技術手段が目的になっている
    - 「DBを最適化したい」 → ユーザー視点がない
    """)

    # --- アーキテクチャ全体像 ---
    heading("アーキテクチャ図の各コンポーネント")

    p("""\
    TechPulse の全体像を1つずつ解説します。

    ┌─────────────────────────────────────────────────┐
    │  ユーザー (ブラウザ / モバイル)                    │
    └──────────────┬──────────────────────────────────┘
                   ↓
    ┌──────────────┴──────────────────────────────────┐
    │  CDN (Content Delivery Network)                  │
    │  静的ファイル (JS, CSS, 画像) をエッジで配信      │
    │  → ユーザーに近いサーバーから返すので高速          │
    └──────────────┬──────────────────────────────────┘
                   ↓
    ┌──────────────┴──────────────────────────────────┐
    │  API Gateway                                     │
    │  全リクエストの入口。認証、レート制限、ルーティング  │
    │  → 各マイクロサービスへ振り分ける                  │
    └──────────────┬──────────────────────────────────┘
                   ↓
    ┌────────┬─────┴──────┬────────────────────────────┐
    │ Auth   │ Post       │ User Service               │
    │Service │ Service    │ プロフィール管理             │
    │JWT発行 │ 記事CRUD   │                             │
    └────┬───┘────┬───────┘────┬───────────────────────┘
         ↓       ↓            ↓
    ┌────┴───┐ ┌─┴──────┐ ┌──┴─────┐
    │Entra ID│ │PostgreSQL│ │Redis   │
    │認証基盤│ │メインDB │ │Cache   │
    └────────┘ └──┬──────┘ └────────┘
                  ↓
    ┌─────────────┴────────────────────────────────────┐
    │  Kafka (メッセージブローカー)                      │
    │  サービス間の非同期通信。投稿イベントを配信         │
    └──────┬─────────────────┬─────────────────────────┘
           ↓                 ↓
    ┌──────┴───────┐ ┌───────┴──────────────────────────┐
    │ AI Moderation│ │ Recommendation Engine             │
    │ 不適切投稿検出│ │ パーソナライズ推薦                 │
    │ + Guardrails │ │ + Vector DB (類似記事検索)         │
    └──────────────┘ └──────────────────────────────────┘
    """)

    p("""\
    なぜこの構造なのか:

    1. CDN を最前段に置く理由:
       静的ファイル (JS/CSS/画像) は毎回サーバーに取りに行く必要がない。
       世界中のエッジサーバーにキャッシュすることで、
       日本のユーザーは東京のエッジから、米国のユーザーはバージニアのエッジから
       取得できる。これだけでレイテンシが劇的に改善する。

    2. API Gateway を挟む理由:
       各マイクロサービスが個別に認証やレート制限を実装すると、
       ロジックが重複し、一貫性が保てない。
       Gateway で一元管理することで「横断的関心事」を集約する。

    3. PostgreSQL と Redis を分ける理由:
       PostgreSQL は耐久性のあるデータストア (ディスク書き込み)。
       Redis はインメモリキャッシュ (高速だがメモリ上のみ)。
       読み取りの多いフィードデータは Redis にキャッシュし、
       書き込みは PostgreSQL に永続化する。

    4. Kafka を使う理由:
       投稿が作成されたら、複数のサービスが反応する必要がある:
       - フォロワーのフィード更新
       - AI モデレーション
       - 検索インデックス更新
       - 通知送信
       同期的に全部やると、投稿APIが遅くなる。
       Kafka にイベントを流して、各サービスが非同期に処理すれば、
       投稿APIは「Kafka に書き込んだらすぐ返す」だけで済む。
    """)

    # --- 技術選定 ---
    heading("技術選定の判断基準")

    p("""\
    技術を選ぶとき、「流行っているから」は最悪の理由です。
    判断基準は:

    1. チームの習熟度 (最重要)
       学習コストはプロジェクト遅延に直結する。
       チーム全員が Python を書けるなら、Go の方が性能が出ても
       Python で始めるほうが正解なことが多い。

    2. 要件との適合性
       リアルタイム性が必要 → WebSocket 対応が楽な技術
       AI/ML を使う → Python エコシステムが必須
       マイクロサービス間通信 → gRPC が有力

    3. 運用コスト
       「作る」より「運用する」時間のほうが長い。
       マネージドサービスを使えるか、監視は容易か、
       障害時の復旧手順は明確か。

    4. コミュニティとエコシステム
       ライブラリが豊富か、Stack Overflow で質問が見つかるか、
       採用市場で人材が見つかるか。

    5. 将来の拡張性
       ただし「将来」を過度に考慮すると over-engineering になる。
       今の要件を満たしつつ、極端に狭い選択をしなければ十分。
    """)

    interview("""\
    「技術選定の基準は？」と聞かれたら:

    「まずチームの習熟度を最優先します。次に要件との適合性、
    運用コスト、エコシステムの順で評価します。
    具体例として、TechPulse ではチームが Python に習熟しており、
    AI/ML パイプラインとの統合が必要だったため、
    バックエンドは FastAPI を選択しました。
    フロントエンドは React/Next.js で SSR による SEO 対応と
    パフォーマンスを両立しています。」
    """)


