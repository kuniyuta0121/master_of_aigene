def chapter_4_explained():
    title("Chapter 4: スケールする - 分散システム (深掘り)")

    heading("Consistent Hashing - なぜ普通のハッシュではダメか")

    p("""\
    ユーザーが100万人を突破しました。1台のDBでは無理です。
    データを複数のサーバーに分散させる必要があります。

    単純なアプローチ: hash(key) % N
    キーのハッシュ値を N (サーバー数) で割った余りでサーバーを決める。

    問題: サーバーを追加/削除すると N が変わる
    → ほぼ全てのキーの割り当て先が変わる
    → 大量のデータ移動が発生する

    例: サーバー3台 → 4台にすると、約75%のキーが移動する。

    Consistent Hashing の解決策:
    ハッシュ空間を「リング」として扱う (0 〜 2^32-1)。
    サーバーもキーも、同じハッシュ関数でリング上の位置を決める。
    キーは「時計回りで最初に見つかるサーバー」に割り当てる。

    サーバー追加時:
    リング上に新しいサーバーが1つ加わる。
    影響を受けるのは、そのサーバーと「反時計回りの隣のサーバー」の
    間にあるキーだけ。全体の 1/N 程度しか移動しない。

    仮想ノード:
    物理サーバーが少ないと、リング上の分布が偏ることがある。
    1つの物理サーバーに複数の仮想的な点をリング上に配置すると、
    データが均等に分散される。
    例: 物理3台 × 仮想150点 = リング上に450点で均等分散。

    使われている場所:
    - DynamoDB, Cassandra: データ分散
    - Redis Cluster: ハッシュスロット (16384スロット)
    - CDN: リクエストのルーティング
    """)

    heading("レプリケーション - なぜコピーを持つか")

    p("""\
    シャーディングでデータを分散しましたが、
    1台が壊れたらそのデータが失われます。

    レプリケーションはデータのコピーを複数サーバーに持つ仕組みです。

    目的:
    1. 可用性: 1台が落ちても別のコピーでサービス継続
    2. 読み取り分散: 読み取りをレプリカに分散して負荷軽減
    3. 地理的分散: ユーザーに近いレプリカから読む

    Primary-Replica 方式:
    - Primary (1台): 書き込みを受け付ける
    - Replica (複数台): Primary のデータをコピーして持つ

    同期レプリケーション:
    - Primary が書き込みを受けたら、全 Replica に反映されるまで待つ
    - 利点: 常に一貫性がある
    - 問題: 1台の Replica が遅いと全体が遅くなる

    非同期レプリケーション:
    - Primary は書き込みをすぐ完了させ、Replica への反映は後で行う
    - 利点: Primary が高速
    - 問題: Replica が最新データに追いつくまでの間、
      古いデータ (stale read) を返す可能性がある

    半同期 (Semi-synchronous):
    - 少なくとも1台の Replica に反映されたら完了とする
    - 同期と非同期の中間
    """)

    heading("Raft 合意プロトコル")

    p("""\
    複数のサーバーが「1つの真実」について合意する必要がある場合があります。

    例: 「この書き込みはコミット済みか？」
    3台のサーバーのうち2台が「はい」で1台が「いいえ」なら、どうするか？

    これが合意問題です。Raft はこれを解決するプロトコルです。

    Raft の3つの要素:

    1. Leader Election (リーダー選出):
    - 全ノードは Follower として開始
    - 一定時間 Leader からの通信がないと、Candidate になり選挙を開始
    - 過半数の投票を得たら Leader になる
    - Leader は任期 (term) を持ち、任期が終わると再選挙

    なぜ1人のリーダーが必要か:
    全ノードが勝手に書き込みを受け付けると、
    順序が決まらず矛盾が生じる。
    1人のリーダーが「この順番で書き込む」と決めることで、
    全ノードが同じ順序でログを持てる。

    2. Log Replication (ログ複製):
    - クライアントの書き込みリクエストは Leader が受ける
    - Leader はログエントリを作り、AppendEntries RPC で全 Follower に送信
    - 過半数が記録したら「コミット済み」とする

    なぜ「過半数」か:
    5台中3台が同意すれば、2台が落ちても過半数が残る。
    任意の2つの過半数は必ず1台以上が重なるため、
    コミット済みの情報が失われることはない。
    (数学的に: C(5,3) のどの2つの組み合わせも共通要素を持つ)

    3. Safety (安全性):
    - コミットされたエントリは取り消せない
    - 新しい Leader は必ず過去のコミット済みエントリを全て持つ
    """)

    heading("Paxos との違い")

    p("""\
    Paxos は Raft より前に発明された合意プロトコルです。

    Paxos の2フェーズ:
    Phase 1 (Prepare): Proposer が「提案番号 N で提案したい」と Acceptor に問う
    Phase 2 (Accept): Acceptor が OK なら、Proposer が値を送って合意

    Paxos の問題:
    - 理論的に正しいが、実装が非常に複雑
    - 論文自体が難解 (著者の Lamport も認めている)
    - Multi-Paxos の最適化が複雑

    Raft の設計思想:
    - 「理解しやすさ」を明示的な設計目標にした
    - Leader ベースで直感的
    - 実装の参考になる擬似コードが論文に含まれている

    理論的な能力は同等ですが、実装しやすさで Raft が広く採用されています。
    etcd, CockroachDB, TiDB などが Raft を使っています。
    """)

    heading("FLP 不可能性定理")

    p("""\
    1985年に Fisher, Lynch, Paterson が証明した定理です。

    内容:
    「非同期分散システムにおいて、1台でも故障する可能性があれば、
     決定的な合意アルゴリズムは存在しない」

    これが意味すること:
    - 非同期: メッセージの到着時間に上限がない
    - 決定的: 必ず有限時間内に結果を出す
    → この条件下では「必ず合意に至る」アルゴリズムは不可能

    Raft はどう回避するか:
    タイムアウトを使います。
    一定時間応答がなければ「そのノードは故障した」と判断する。
    これは「同期の仮定」を部分的に導入していることになり、
    FLP の「非同期」前提を緩和しています。

    実用的には「いつかは」合意に至る (活性は確率的)。
    """)

    heading("一貫性モデルのスペクトラム")

    p("""\
    分散システムで「どこまでの正しさを保証するか」は設計判断です。

    線形化可能性 (Linearizability):
    - 最も強い保証
    - 全操作にグローバルな順序がある
    - 書き込みが完了したら、全ノードで即座に見える
    - 実装: 全操作を Leader 経由にする → 性能ペナルティ大
    - 用途: 銀行残高、在庫管理 (絶対に矛盾不可)

    因果一貫性 (Causal Consistency):
    - 因果関係のある操作は順序が保証される
    - 「AさんがBさんのコメントに返信」→ 全ノードでAの後にBの返信が見える
    - 因果関係のない操作は順不同でOK
    - 実装: Vector Clock で因果関係を追跡
    - 用途: SNS のコメント、チャット

    セッション保証 (Session Consistency):
    - 同一セッション内では自分の書き込みが読める (Read Your Writes)
    - 他ユーザーの書き込みは遅延あり
    - 用途: TechPulse のフィード (自分の投稿は即座に見える)

    結果整合性 (Eventual Consistency):
    - 最も弱い保証
    - いつかは全ノードが同じ値を返す (通常は数百ms〜数秒)
    - 実装: 非同期レプリケーション → 性能は最高
    - 用途: いいね数、閲覧カウンター (多少ズレてもOK)
    """)

    heading("Kafka - メッセージングの仕組み")

    p("""\
    なぜメッセージキューが必要か:

    投稿が作成されたら、以下が必要:
    1. フォロワーのフィード更新
    2. 検索インデックス更新
    3. AI モデレーション
    4. 通知送信

    これを全て同期的にやると:
    - 投稿 API のレスポンスが遅くなる (全部終わるまで待つ)
    - 1つが失敗すると全部失敗する
    - サービス間の結合度が高い

    Kafka を使うと:
    - 投稿サービスは Kafka にイベントを書いたらすぐ返す (非同期)
    - 各サービスが独立してイベントを消費する (疎結合)
    - 一時的に消費者がダウンしてもイベントは Kafka に保持される (バッファ)

    Kafka の内部構造:

    Topic: メッセージのカテゴリ (例: "post-created")
    Partition: Topic を分割したもの。各 Partition は順序付きログ。
    Offset: Partition 内のメッセージの連番。
    Consumer Group: 複数の Consumer が Partition を分担して読む。

    Fan-out on Write vs Fan-out on Read:

    Fan-out on Write (Push):
    - 投稿時にフォロワー全員のフィードに書き込む
    - 読み取りは自分のフィードを読むだけ (高速)
    - 問題: フォロワーが100万人いると書き込みが重い

    Fan-out on Read (Pull):
    - フィード読み取り時にフォロー中全員の投稿を集める
    - 書き込みは1回だけ (高速)
    - 問題: 読み取りが遅い

    ハイブリッド (Twitter方式):
    - 一般ユーザーは Fan-out on Write
    - フォロワーが極端に多い有名人は Fan-out on Read
    """)

    heading("配信セマンティクス")

    p("""\
    メッセージが「ちょうど1回」届くことを保証するのは意外と難しいです。

    At-most-once (最大1回):
    - 送信したら確認しない。ロスの可能性あり。
    - 例: UDP でのログ送信

    At-least-once (最低1回):
    - ACK が来なければ再送。重複の可能性あり。
    - 例: Kafka のデフォルト設定

    Exactly-once (ちょうど1回):
    - 重複なし、ロスなし。最も難しい。
    - Kafka では: 冪等 Producer + トランザクション Consumer で実現

    冪等性 (Idempotency) とは:
    同じ操作を何回実行しても結果が変わらないこと。
    例: 「残高を100にセット」は冪等 (何回やっても100)
    例: 「残高に100を加算」は冪等ではない (やるたびに増える)

    At-least-once + 冪等な処理 = 実質的に Exactly-once
    これが実務で最も現実的なパターンです。
    """)

    heading("Little's Law と Amdahl's Law")

    p("""\
    Little's Law:
    L = λ × W

    L: システム内の平均リクエスト数
    λ: 到着率 (リクエスト/秒)
    W: 平均滞在時間 (レイテンシ)

    直感的理解:
    レストランで考えると、
    「店内の客数 = 来店頻度 × 平均滞在時間」

    応用: サーバー何台必要か？
    1秒に1000リクエスト (λ=1000) が来て、
    各リクエストの処理に 0.1秒 (W=0.1) かかるなら、
    L = 1000 × 0.1 = 100 リクエストが常にシステム内にある。
    1サーバーが10並列処理できるなら、10台必要。

    Amdahl's Law:
    Speedup = 1 / ((1-p) + p/s)

    p: 並列化可能な割合
    s: 並列度 (コア数)

    重要な洞察:
    並列化できない部分 (1-p) がボトルネックになる。
    処理の90%が並列化可能 (p=0.9) でも、
    10コアにすると Speedup = 1/(0.1 + 0.9/10) = 5.26 倍
    100コアにしても Speedup = 1/(0.1 + 0.9/100) = 9.17 倍
    → 10%の逐次部分が上限を決めてしまう
    """)

    interview("""\
    「分散システムで最も難しいことは？」

    「一貫性と可用性のトレードオフです。CAP定理により、
    ネットワーク分断時にはどちらかを犠牲にする必要があります。
    TechPulseでは、いいね数には結果整合性、
    ユーザー認証情報には強い一貫性を適用し、
    データの性質に応じて一貫性モデルを使い分けています。
    合意にはRaftプロトコルを採用し、
    過半数コミットにより安全性を保証しています。」
    """)


