# Chapter 1: 1行のコードが実行されるまで (深掘り)
# =====================================================================
def chapter_1_explained():
    title("Chapter 1: 1行のコードが実行されるまで (深掘り)")

    p("""\
    ユーザーがブラウザで「投稿」ボタンを押した。
    その1クリックの裏で、フロントエンドからCPUのシリコンまで、
    何層もの技術が連携して動いています。
    1つずつ、下の層に向かって潜っていきましょう。
    """)

    # --- React ---
    heading("React と Virtual DOM")

    p("""\
    ブラウザ上で動く TechPulse のフロントエンドは React で書かれています。

    React とは:
    Facebook が開発した UI ライブラリです。
    画面を「コンポーネント」という部品の組み合わせで構築します。

    たとえば「投稿ボタン」はこんなコンポーネントです:

      function PostButton({ onClick }) {
        return <button onClick={onClick}>投稿する</button>;
      }

    ユーザーがこのボタンを押すと、onClick ハンドラが呼ばれ、
    内部で fetch() が実行されます。
    """)

    heading("Virtual DOM とは何か")

    p("""\
    ブラウザには DOM (Document Object Model) という仕組みがあります。
    DOM は HTML をツリー構造で表現したもので、
    JavaScript から操作して画面を変更できます。

    問題は、DOM の操作が「重い」ことです。
    DOM を変更するたびに、ブラウザは:
    1. スタイルの再計算
    2. レイアウトの再計算 (Reflow)
    3. 画面の再描画 (Repaint)
    を行います。これは CPU コストが高い処理です。

    React の解決策が Virtual DOM です:

    1. JavaScript のメモリ上に、DOM のコピー (仮想DOM) を持つ
    2. 状態が変わったら、新しい仮想DOM を作る
    3. 古い仮想DOM と新しい仮想DOM を比較 (diff)
    4. 変わった部分だけ、本物の DOM に反映する

    これを Reconciliation (調停) と呼びます。

    なぜこれが速いか:
    - JavaScript 内のオブジェクト比較は高速 (メモリ上の操作)
    - 本物の DOM 操作は最小限に抑えられる
    - バッチ処理で複数の変更をまとめて反映できる
    """)

    misconception(
        "Virtual DOM は常に直接DOM操作より速い",
        "Virtual DOM のオーバーヘッド自体はある。ただし、"
        "開発者が手動で最適な DOM 操作を書く手間を省きつつ、"
        "十分に高速な更新を実現するのが本当の価値"
    )

    # --- DNS ---
    heading("DNS 解決 - ドメイン名から IP アドレスへ")

    p("""\
    fetch("https://api.techpulse.dev/posts") が呼ばれました。
    ブラウザはまず「api.techpulse.dev」の IP アドレスを調べる必要があります。

    なぜか:
    人間はドメイン名 (api.techpulse.dev) のほうが覚えやすいですが、
    ネットワーク機器は IP アドレス (203.0.113.42) で通信します。
    この変換を行うのが DNS (Domain Name System) です。
    """)

    heading("DNS の階層構造")

    p("""\
    DNS は「電話帳」に例えられますが、1冊の電話帳ではありません。
    世界中に分散した階層構造です:

    1. ブラウザキャッシュ
       → 最近アクセスしたドメインは覚えている

    2. OS キャッシュ
       → /etc/hosts や OS レベルの DNS キャッシュ

    3. ルーターキャッシュ
       → 家庭やオフィスのルーターが持つキャッシュ

    4. 再帰 DNS サーバー (Recursive Resolver)
       → ISP やクラウドの DNS サーバー (8.8.8.8 = Google DNS)
       → ここが「電話帳を引く係」

    5. ルート DNS サーバー (Root Name Server)
       → 世界に13系統 (a.root-servers.net 〜 m.root-servers.net)
       → 「.dev はどの TLD サーバーが管理しているか」を知っている

    6. TLD DNS サーバー (Top-Level Domain)
       → .dev, .com, .jp などのトップレベルドメインを管理
       → 「techpulse.dev はどの権威サーバーが管理しているか」を知っている

    7. 権威 DNS サーバー (Authoritative Name Server)
       → techpulse.dev のゾーンを管理
       → 「api.techpulse.dev → 203.0.113.42」という答えを持っている

    この階層を辿る理由:
    世界中の全ドメインを1箇所で管理するのは不可能です。
    階層化することで、各サーバーは自分の担当範囲だけ知っていれば良い。
    """)

    p("""\
    DNS レコードの種類:

    A レコード:     ドメイン → IPv4 アドレス
    AAAA レコード:  ドメイン → IPv6 アドレス
    CNAME レコード: ドメイン → 別のドメイン名 (エイリアス)
    MX レコード:    メールサーバーの指定
    TXT レコード:   任意のテキスト (SPF, DKIM 等の検証用)

    実務でよくあるパターン:
    api.techpulse.dev → CNAME → alb-123.ap-northeast-1.elb.amazonaws.com
    → A → 52.x.x.x

    CloudFront や ALB を使う場合、CNAME が挟まることが多いです。
    """)

    # --- TCP ---
    heading("TCP 3-way Handshake")

    p("""\
    DNS で IP アドレスが分かりました。
    次は、その相手と通信路を作ります。

    TCP (Transmission Control Protocol) とは:
    「信頼性のある通信」を提供するプロトコルです。
    具体的には:
    - データが届いたことを確認する (ACK)
    - データの順序を保証する (シーケンス番号)
    - データが欠けたら再送する
    - フロー制御で相手を溢れさせない

    UDP との違い:
    UDP は「送りっぱなし」。届いたかどうか確認しない。
    その代わり高速で軽量。動画ストリーミングや DNS クエリに使われる。
    TCP は確実だが、ハンドシェイクの分だけ遅い。
    """)

    heading("なぜ 3 回のやり取りが必要か")

    p("""\
    TCP 接続開始時は、3 回のメッセージ交換が必要です:

    1. クライアント → サーバー: SYN (接続要求)
       「こんにちは、通信したいです。私のシーケンス番号は X です」

    2. サーバー → クライアント: SYN-ACK (接続承認 + 自分の要求)
       「了解、私のシーケンス番号は Y です。あなたの X を受け取りました」

    3. クライアント → サーバー: ACK (確認)
       「あなたの Y を受け取りました。通信開始しましょう」

    なぜ 2 回ではダメか:
    2 回だと、サーバーは「クライアントが自分の SYN-ACK を受け取ったか」
    確認できません。通信は双方向なので、
    双方が「相手からデータを受け取れる」ことを確認する必要があります。

    シーケンス番号とは:
    TCP はデータをバイト単位で管理します。
    シーケンス番号は「このデータは何バイト目から始まるか」を示します。
    これにより、パケットが到着順序が入れ替わっても、
    正しい順序に並べ直すことができます。

    初期シーケンス番号はランダムに決めます。
    これはセキュリティ上の理由で、予測されると
    TCP セッションハイジャック攻撃が可能になるためです。
    """)

    misconception(
        "3-way handshake は 1.5 RTT かかる",
        "クライアント視点では約 1 RTT です。"
        "SYN を送り、SYN-ACK を受け取った時点でクライアントは接続確立とみなし、"
        "ACK と同時にデータ送信を開始できます (TCP Fast Open ならさらに速い)"
    )

    # --- TLS ---
    heading("TLS 1.3 ハンドシェイク")

    p("""\
    TCP で通信路ができましたが、まだ平文通信です。
    HTTPS にするには、TLS (Transport Layer Security) で暗号化します。

    TLS が提供するもの:
    1. 機密性 (Confidentiality): 通信内容を盗み見できない
    2. 完全性 (Integrity): 途中で改ざんされていないことを検証
    3. 認証 (Authentication): 本当にそのサーバーか確認

    TLS 1.3 の流れ:

    1. ClientHello (クライアント → サーバー):
       - 対応 TLS バージョン
       - 対応暗号スイートの一覧
       - 鍵共有用パラメータ (ECDHE の公開鍵)
       - SNI: どのホスト名に接続したいか
       - ALPN: HTTP/2 を使うか HTTP/1.1 を使うか

    2. ServerHello + Certificate + Finished (サーバー → クライアント):
       - 選択した暗号スイート
       - サーバーの公開鍵
       - サーバー証明書 (このサーバーが本物であることの証明)
       - Finished メッセージ (ハンドシェイクの完了確認)

    3. Finished (クライアント → サーバー):
       - ハンドシェイクの確認
       - ここからアプリケーションデータを送信開始
    """)

    heading("SNI と ALPN")

    p("""\
    SNI (Server Name Indication) とは:
    1つの IP アドレスで複数のドメインをホストしていることがあります。
    例えば同じサーバーで api.techpulse.dev と www.techpulse.dev を動かす場合、
    サーバーは「どちらの証明書を返せばいいか」を知る必要があります。
    SNI はクライアントが「私は api.techpulse.dev に接続したい」と
    最初に伝える仕組みです。

    ALPN (Application-Layer Protocol Negotiation) とは:
    TLS の上で HTTP/2 を使うか HTTP/1.1 を使うかを決める仕組みです。
    クライアントが「h2, http/1.1」(= HTTP/2 優先、HTTP/1.1 も可) と送り、
    サーバーが「h2」(= HTTP/2 で行きましょう) と返します。

    なぜ TLS の中で決めるか:
    TLS ハンドシェイクのついでに決めることで、
    追加の往復なしにプロトコルを合意できるからです。
    """)

    heading("TLS 1.3 が TLS 1.2 より速い理由")

    p("""\
    TLS 1.2: 2-RTT (ハンドシェイクに2往復必要)
    TLS 1.3: 1-RTT (1往復で完了)

    なぜ減ったか:
    TLS 1.3 では、ClientHello の時点で鍵共有パラメータを一緒に送ります。
    TLS 1.2 では、まず暗号方式を合意してから鍵交換する2段階でした。
    1.3 では「この暗号方式で行くと仮定して鍵も先に送る」ことで
    1往復を節約しています。

    さらに 0-RTT 再開:
    以前接続したサーバーとの再接続時、前回の情報を使って
    最初のパケットからアプリケーションデータを送れます。
    ただし、0-RTT データはリプレイ攻撃のリスクがあるため、
    副作用のある操作 (POST リクエスト等) には注意が必要です。
    """)

    # --- HTTP ---
    heading("HTTP/1.1 vs HTTP/2 vs HTTP/3")

    p("""\
    TLS が確立したら、いよいよ HTTP リクエストを送ります。
    HTTP にはいくつかのバージョンがあり、それぞれ大きく違います。
    """)

    heading("HTTP/1.1 の限界")

    p("""\
    HTTP/1.1 は 1997 年に策定されました。

    特徴:
    - テキストベースのプロトコル (人間が読める)
    - keep-alive で TCP 接続を使い回せる
    - パイプライニング (複数リクエストを連続送信) も仕様上はある

    しかし実運用では問題がありました:

    Head-of-Line Blocking (先頭行ブロッキング):
    パイプライニングでは、リクエストを連続送信できますが、
    レスポンスは「リクエストの順番通り」に返す必要があります。
    最初のリクエストの処理が遅いと、後続の全レスポンスが待たされます。

    例: 画像A (5秒), CSS (0.01秒), JS (0.01秒) を順に要求
    → 画像A が返るまで CSS も JS も返せない

    このため、パイプライニングは実質的に普及しませんでした。
    ブラウザは代わりに、1ドメインに対して 6 本の TCP 接続を並列に張り、
    擬似的な並列化をしていました。
    """)

    heading("HTTP/2 のマルチプレクシング")

    p("""\
    HTTP/2 は 2015 年に策定されました。

    最大の改善点: マルチプレクシング

    1つの TCP 接続の中に「ストリーム」という仮想的なチャネルを複数作り、
    リクエストとレスポンスを並行して流せます。

    イメージ:
    HTTP/1.1: 1車線の道路。前の車が止まると全車渋滞。
    HTTP/2:   多車線の高速道路。各車線が独立して流れる。

    その他の改善:
    - バイナリフレーム: テキストではなくバイナリで効率的
    - HPACK: ヘッダ圧縮 (同じヘッダを何度も送らない)
    - サーバープッシュ: サーバーが先回りしてリソースを送信

    HPACK とは:
    HTTP リクエストには毎回同じようなヘッダが含まれます:
    - :method: GET
    - :authority: api.techpulse.dev
    - user-agent: Chrome/...
    これらを静的テーブル + 動的テーブルで圧縮し、
    2回目以降はインデックス番号だけ送ります。
    """)

    heading("HTTP/2 の残る弱点")

    p("""\
    HTTP/2 はアプリケーション層では並列化できましたが、
    下の TCP 層では依然として「1本の接続」です。

    TCP は「順序保証」をするプロトコルなので、
    あるパケットが失われると、後続のデータも到着済みでも
    アプリケーションに渡せません。

    これが TCP レベルの Head-of-Line Blocking です。

    HTTP/2 のストリーム A のパケットが失われると、
    ストリーム B のデータも TCP 層で待たされてしまいます。
    アプリケーション層では独立なのに、TCP 層で詰まるのです。
    """)

    heading("HTTP/3 と QUIC")

    p("""\
    HTTP/3 はこの TCP 起因の問題を根本的に解決します。

    QUIC とは:
    UDP 上に構築された、TCP の機能を持つ新しいトランスポートプロトコルです。
    Google が開発し、IETF で標準化されました。

    なぜ UDP の上にわざわざ作るのか:
    TCP はカーネル (OS) に組み込まれているため、
    プロトコルの変更が非常に難しいです (OS のアップデートが必要)。
    UDP の上にユーザー空間で実装すれば、
    アプリケーションのアップデートだけで改善できます。

    QUIC の主要な改善点:

    1. ストリームの独立性:
       ストリーム A のパケットロスがストリーム B に影響しない。
       TCP の Head-of-Line Blocking を完全に回避。

    2. 接続確立の高速化:
       TLS 1.3 が QUIC に統合されているため、
       TCP + TLS の 2 段階が不要。通常 1-RTT で通信開始。

    3. 0-RTT 接続再開:
       以前の接続情報を使い、最初のパケットから
       アプリケーションデータを送信可能。

    4. コネクション移動:
       TCP は (送信元IP, 送信元ポート, 宛先IP, 宛先ポート) の
       4つ組で接続を識別します。WiFi → 4G に切り替わると
       IP が変わるため、TCP 接続が切れます。
       QUIC は Connection ID で接続を識別するため、
       IP が変わっても論理的に同じ接続を継続できます。
    """)

    interview("""\
    「HTTP/1.1, HTTP/2, HTTP/3 の違いは？」

    「HTTP/1.1 はテキストベースで1リクエストずつ処理が基本でした。
    HTTP/2 はマルチプレクシングにより1接続で複数ストリームを並行処理可能にしましたが、
    TCP の順序保証に起因する Head-of-Line Blocking は残っていました。
    HTTP/3 は UDP 上の QUIC を使うことでストリーム間の独立性を実現し、
    接続確立の高速化、コネクション移動にも対応しています。」
    """)

    # --- TCP 輻輳制御 ---
    heading("TCP 輻輳制御")

    p("""\
    ネットワークは無限の帯域を持っているわけではありません。
    もし全員が「送れるだけ送る」をやると、ルーターのキューが溢れ、
    パケットロスと遅延が発生し、全体が不安定になります。

    TCP はこれを防ぐために「輻輳制御」を持っています。
    送信量を適応的に制御する仕組みです。
    """)

    heading("cwnd (Congestion Window) とは")

    p("""\
    cwnd は「ACK を待たずに送信できるデータ量」を表すウィンドウサイズです。

    イメージ:
    cwnd = パイプの太さ。
    太い → 一度にたくさん送れる。
    細い → 少しずつ慎重に送る。

    cwnd は固定ではなく、ネットワークの状況に応じて動的に変化します。
    """)

    heading("Slow Start")

    p("""\
    接続直後はネットワークの状況が分からないため、
    小さな cwnd で始めて、徐々に増やしていきます。

    動作:
    1. cwnd を小さく始める (初期値は OS により異なる)
    2. ACK が返るたびに cwnd を増やす
    3. 結果として cwnd は指数的に増加する

    名前は "Slow" Start ですが、実際の増加は「指数的」でかなり速いです。
    「いきなり最大速度で送らない」という意味で Slow です。

    ssthresh (Slow Start Threshold):
    cwnd がこの閾値に達すると、Slow Start を終了して
    Congestion Avoidance に切り替えます。
    """)

    heading("Congestion Avoidance")

    p("""\
    Slow Start で「このくらいは送れそう」と分かった後は、
    慎重に cwnd を増やしていきます。

    動作:
    - cwnd を RTT あたり 約 1/cwnd ずつ増加 (線形増加)
    - パケットロスを検出したら cwnd を半減させ、ssthresh を更新

    つまり「ゆっくり増やして、混雑を感じたら急に引く」という挙動です。
    これを AIMD (Additive Increase, Multiplicative Decrease) と呼びます。
    """)

    heading("CUBIC と BBR")

    p("""\
    CUBIC (Linux デフォルト):
    - Reno 系の AIMD は、高帯域・高遅延環境で帯域を使い切るのが遅い
    - CUBIC は 3 次関数でウィンドウサイズを制御
    - ロス直前のサイズに向かって急速回復し、
      その付近では慎重に探索する
    - 高帯域回線でも効率よく帯域を活用できる

    BBR (Google が開発):
    - 従来方式: パケットロス = 混雑のサイン、と判断
    - BBR: パケットロスではなく、帯域幅と RTT を推定して
      最適な送信レートを計算する
    - バッファブロート問題を避けやすい

    バッファブロートとは:
    途中のルーターが大きなバッファを持っていると、
    パケットはロスしない代わりにキューに溜まり続けます。
    結果として遅延だけが増大する現象です。
    BBR はバッファの充填度に依存しないため、この問題を緩和します。
    """)

    # --- epoll ---
    heading("I/O 多重化: select → poll → epoll")

    p("""\
    サーバーは何万ものクライアントと同時に通信する必要があります。
    「1接続に1スレッド」は、10万接続になるとスレッドが10万必要で非現実的です。

    そこで「1つのスレッドで複数の接続を監視する」仕組みが I/O 多重化です。

    select (1983年):
    - 「これらの FD (ファイルディスクリプタ) の中で、
      読み書き可能なものがあるか？」とカーネルに問い合わせる
    - 問題1: FD 上限が 1024 (コンパイル時に決まる)
    - 問題2: 毎回 FD の集合全体をカーネルにコピーする
    - 問題3: 返ってきた後、どの FD が準備完了か全部チェックする → O(n)

    poll (1997年):
    - select の FD 上限を撤廃 (配列ベース)
    - しかし毎回全 FD をスキャンする O(n) の問題は同じ

    epoll (2002年, Linux):
    - カーネルに FD を「登録」しておく (epoll_ctl)
    - イベントがあった FD だけを返す (epoll_wait)
    - アクティブな接続数に比例 → O(1) (FD 総数に依存しない)

    これが Node.js, Nginx, Redis など高性能サーバーの裏側で使われています。
    macOS では kqueue が同等の役割を果たします。
    """)

    # --- CFS ---
    heading("CFS (Completely Fair Scheduler)")

    p("""\
    パケットがサーバーに届き、epoll がアプリケーションに通知しました。
    しかし、CPU は1つ (コアは複数でも有限) です。
    複数のプロセスが CPU を使いたい場合、誰が先に使うか決める必要があります。

    これが OS のプロセススケジューラの仕事です。

    Linux は CFS (Completely Fair Scheduler) を使います。

    vruntime (仮想実行時間):
    - 各プロセスに「どれだけ CPU 時間を使ったか」を記録
    - ただし、優先度 (nice値) に応じて進む速度が異なる
    - 優先度が高いプロセス → vruntime の進みが遅い → 多くの CPU 時間を得る

    選択アルゴリズム:
    - vruntime が最小のプロセスを次に実行する
    - データ構造: 赤黒木 (Red-Black Tree) → O(log n) で最小値を取得

    なぜ「公平」か:
    全プロセスの vruntime が均等に進むように設計されているため、
    どのプロセスも「公平に」CPU 時間を得られます。
    """)

    # --- CPU パイプライン ---
    heading("CPU パイプライン (5段)")

    p("""\
    プロセスに CPU 時間が割り当てられました。
    命令がパイプラインに流れ込みます。

    CPU は命令を 5 段階で処理します:

    IF (Instruction Fetch):  メモリから命令を取得
    ID (Instruction Decode): 命令を解読、レジスタを読む
    EX (Execute):            演算を実行 (加算、比較等)
    MEM (Memory Access):     メモリの読み書き
    WB (Write Back):         結果をレジスタに書き戻す

    なぜパイプラインにするのか:
    1つの命令が WB をしている間に、次の命令が MEM を、
    その次が EX を、という風に「流れ作業」で処理します。
    1命令あたりの時間は変わらなくても、
    スループット (単位時間あたりの処理数) が向上します。

    イメージ:
    洗濯機 → 乾燥機 → アイロン を1つずつやると3時間。
    2着目を洗濯機に入れつつ1着目を乾燥、とパイプライン化すると
    全体の処理時間が大幅に短縮されます。
    """)

    heading("パイプラインハザード")

    p("""\
    パイプラインは理想的には毎サイクル1命令を完了しますが、
    「詰まる」ことがあります。これをハザードと呼びます。

    1. データハザード (RAW: Read After Write):
       命令 B が命令 A の結果を使いたいが、
       A の結果がまだ WB されていない。
       → フォワーディング (EX の結果を直接次の命令に渡す) で緩和

    2. 制御ハザード (分岐):
       if 文の結果が分かるまで、次にどの命令を実行すべきか不明。
       → 分岐予測 (Branch Prediction) で推測実行

    3. 構造ハザード:
       同じハードウェアリソースを2つの命令が同時に使おうとする。
       → ハードウェアの複製で解決 (ALU を複数用意等)
    """)

    heading("分岐予測")

    p("""\
    if 文があると、条件の結果 (true/false) が分かるまで
    次の命令をフェッチできません。これではパイプラインが止まります。

    解決策: 分岐予測
    CPU が「たぶん true だろう」と推測して、先に命令を実行します。

    2ビット飽和カウンタ:
    - 各分岐命令に 2 ビットの状態を持つ
    - 状態: 強い taken → 弱い taken → 弱い not-taken → 強い not-taken
    - 過去の分岐結果に基づいて予測する
    - ループは「ほとんど taken」なので高精度で予測できる

    予測が外れた場合:
    パイプラインに入れた命令を全て破棄 (フラッシュ) して
    正しい方の命令を最初からやり直します。
    これが性能ペナルティとなるため、分岐予測の精度は重要です。
    現代の CPU は 95%+ の精度を持ちます。
    """)

    # --- キャッシュ ---
    heading("CPU キャッシュと MESI プロトコル")

    p("""\
    CPU がデータにアクセスする速度は、データがどこにあるかで大きく変わります:

    レジスタ:     ~0.3 ns    (1サイクル)
    L1 キャッシュ: ~1 ns      (3-4サイクル)
    L2 キャッシュ: ~4 ns      (10-12サイクル)
    L3 キャッシュ: ~12 ns     (30-40サイクル)
    メインメモリ:  ~100 ns    (200-300サイクル)
    SSD:          ~100,000 ns (= 100μs)

    L1 とメインメモリで 100 倍の差があります。
    だから CPU は頻繁にアクセスするデータをキャッシュに置きます。

    キャッシュライン:
    キャッシュはバイト単位ではなく「ライン」単位 (通常 64 バイト) で管理します。
    配列を順にアクセスすると、最初のアクセスで 64 バイト分がキャッシュに載り、
    次の数回のアクセスはキャッシュヒットになります。
    これが「空間的局所性」を活かした設計です。
    """)

    heading("MESI プロトコル")

    p("""\
    マルチコア CPU では、各コアが自分の L1/L2 キャッシュを持ちます。
    同じデータを複数コアがキャッシュしている場合、
    1つのコアがデータを変更すると、他のコアのキャッシュは古くなります。

    これを解決するのが MESI プロトコルです:

    M (Modified):  自分だけが持っていて、変更済み。メモリと不一致。
    E (Exclusive): 自分だけが持っていて、メモリと一致。
    S (Shared):    複数コアが持っていて、メモリと一致。
    I (Invalid):   無効。使えない。

    例: コア A とコア B が同じアドレスを持つ場合
    1. 両方 S 状態
    2. コア A がデータを変更 → A は M に遷移、B に Invalidate を送信
    3. コア B のキャッシュラインは I に遷移
    4. コア B がそのデータを読もうとすると、A から最新値を取得

    これが「キャッシュコヒーレンシ」の仕組みです。
    False Sharing (偽共有) という問題もあります:
    異なる変数が同じキャッシュラインに載っていると、
    互いに無関係な書き込みで Invalidate が飛び交い、性能が低下します。
    """)

    interview("""\
    「1リクエストの裏で何が起こるか」と聞かれたら:

    「ブラウザがfetchを呼ぶと、まずDNSで名前解決し送信先IPを取得します。
    次にTCP 3-way handshakeで接続を確立し、TLS 1.3で暗号化通信を開始します。
    HTTP/2ならHEADERSフレームとDATAフレームに分かれて送信されます。

    サーバー側では、パケットがNICからカーネルのネットワークスタックを通り、
    epollがアプリケーションに通知します。
    CFSスケジューラがプロセスにCPU時間を割り当て、
    CPU内部ではパイプラインで命令を処理し、
    L1→L2→L3→メモリの階層でデータにアクセスします。」
    """)


