# Chapter 3: データを永続化する (深掘り)
# =====================================================================
def chapter_3_explained():
    title("Chapter 3: データを永続化する (深掘り)")

    # --- 正規化 ---
    heading("正規化 - なぜテーブルを分けるのか")

    p("""\
    データベース設計の基本は「正規化」です。
    データの重複を排除し、更新時の不整合を防ぎます。

    第 1 正規形 (1NF):
    - 各列の値がアトミック (分割不可能) であること
    - 悪い例: tags 列に "Python, Go, Rust" とカンマ区切りで入れる
    - 良い例: post_tags テーブルを別に作り、1行1タグにする

    第 2 正規形 (2NF):
    - 1NF を満たし、かつ
    - 非キー列が複合主キーの一部にのみ依存しないこと
    - 例: (post_id, tag_id, tag_name) → tag_name は tag_id だけで決まる
      → tag_name は tags テーブルに分離すべき

    第 3 正規形 (3NF):
    - 2NF を満たし、かつ
    - 非キー列が他の非キー列に依存しないこと (推移的依存の排除)
    - 例: (user_id, department_id, department_name)
      → department_name は department_id で決まる
      → departments テーブルに分離すべき

    なぜ正規化するか:
    1. データの冗長性排除 → ストレージ節約
    2. 更新異常の防止 → department_name を変えるとき1箇所だけ変更すればいい
    3. データの整合性保証

    非正規化する場合:
    読み取り性能が重要な場合、あえて冗長データを持つこともあります。
    例: フィードの読み込みが遅いので、ユーザー名を投稿テーブルにも持つ。
    ただし、ユーザー名変更時に複数箇所の更新が必要になるトレードオフがあります。
    """)

    # --- B+Tree ---
    heading("B+Tree インデックス")

    p("""\
    PostgreSQL がデータを高速に検索するために使うのが B+Tree インデックスです。

    B-Tree とは:
    ノードに複数のキーを持つ自己平衡探索木です。
    各ノードが最大 M 個のキーを持つ場合、
    子ノードは最大 M+1 個あります。

    B+Tree は B-Tree の改良版:
    1. データはリーフノード (末端) にのみ格納
    2. 内部ノードはキーのコピーだけ持つ (ナビゲーション用)
    3. リーフノード同士がリンクリストで繋がっている

    なぜ B+Tree か:
    1. ディスク I/O を最小化:
       1ノードを1ページ (通常 8KB) に収めると、
       1回のディスク読み込みで数百のキーを比較できる。
       バイナリツリーだと1ノード1キーで、深さが大きくなり I/O 回数が増える。

    2. 範囲検索が高速:
       リーフのリンクリストを辿るだけで済む。
       例: WHERE created_at BETWEEN '2024-01-01' AND '2024-12-31'
       → 開始位置を見つけたら、リーフを順に辿るだけ。

    3. 安定した性能:
       全リーフの深さが同じなので、どのキーにアクセスしても
       同じ回数のディスク I/O で到達できる。
    """)

    # --- Write Path ---
    heading("Write Path: INSERT からディスクまで")

    p("""\
    投稿が作成されました。INSERT 文が実行されます。
    データがディスクに書き込まれるまでの道のりを追いましょう。

    1. SQL パーサー:
       INSERT INTO posts (title, content) VALUES ('Kafka入門', '...')
       → SQL を解析して実行計画を作成

    2. WAL (Write-Ahead Log) への書き込み:
       まず「何をしようとしているか」をログに記録します。
       これが WAL です。

       なぜデータ本体より先にログを書くか:
       書き込みの途中でクラッシュした場合、
       WAL があれば「どこまで完了していたか」が分かります。
       WAL がなければ、データが中途半端な状態で壊れる可能性があります。
       これが WAL の名前の由来: Write-Ahead (事前に書く) Log

    3. Buffer Pool への書き込み:
       実際のテーブルデータはメモリ上の Buffer Pool に書き込みます。
       Buffer Pool はディスク上のページのキャッシュです。

       なぜすぐディスクに書かないか:
       ディスク I/O は遅いからです。
       メモリに書いて「後でまとめてディスクに書く」ほうが効率的。
       この時点ではメモリ上のページは「汚れた (dirty)」状態です。

    4. Checkpoint (チェックポイント):
       定期的に、dirty ページをディスクにフラッシュします。
       チェックポイント完了後、そこまでの WAL は不要になります。

    5. fsync:
       OS の write() はカーネルのページキャッシュに書くだけで、
       まだディスクの永続記憶に到達していません。
       fsync を呼ぶことで、OS がページキャッシュの内容を
       ディスクに書き出すことを保証します。
    """)

    heading("SSD の内部 - FTL")

    p("""\
    fsync でデータがディスク (SSD) に書き込まれます。
    SSD の内部は HDD とは全く異なる仕組みです。

    NAND Flash の特性:
    - 読み取り: ページ単位 (4KB)
    - 書き込み: ページ単位 (4KB)
    - 削除: ブロック単位 (256KB〜1MB) ← これが重要

    つまり、1ページだけ書き換えたくても、
    ブロック全体を消してから書き直す必要があります。

    FTL (Flash Translation Layer):
    SSD のファームウェアが提供する変換層です。
    OS からは通常のブロックデバイスに見えますが、
    内部では以下を行っています:

    1. 論理アドレス → 物理アドレスの変換
    2. ウェアレベリング: 書き込みを均等に分散 (セルの寿命を延ばす)
    3. ガベージコレクション: 不要なブロックを回収

    書き込み増幅 (Write Amplification):
    OS が 4KB 書き込んでも、SSD 内部では GC のために
    ブロック全体の読み取り→書き戻しが発生し、
    実際の NAND 書き込み量がOS要求量の数倍になることがあります。
    """)

    # --- クエリプランナ ---
    heading("クエリプランナ - どうやって検索するか")

    p("""\
    SELECT でデータを読む場合、PostgreSQL のクエリプランナが
    「どうやって検索するか」を決めます。

    EXPLAIN ANALYZE をつけると実行計画が見られます。

    アクセス方式:

    Seq Scan (Sequential Scan):
    - テーブルの全行を順番にスキャンする
    - いつ使われるか: 検索条件が広い場合 (全体の20%以上を読む場合)
    - 小さいテーブルでは Index Scan より速いこともある

    Index Scan:
    - B+Tree インデックスを辿って該当行を見つける
    - いつ使われるか: 選択性が高い場合 (少数の行を返す)
    - インデックスとテーブルの両方を読むため、
      返す行数が多いと Seq Scan より遅くなる

    Bitmap Index Scan:
    - インデックスを使って「該当するページ番号」のビットマップを作り、
      まとめてテーブルを読む
    - 複数のインデックスを AND/OR で組み合わせられる

    JOIN の実行方式:

    Nested Loop Join:
    - 外側テーブルの各行に対して、内側テーブルを検索
    - 内側にインデックスがあれば高速
    - 小テーブル × 大テーブル (インデックスあり) に最適

    Hash Join:
    - 小さいテーブルからハッシュ表を構築
    - 大きいテーブルを走査して、ハッシュ表で照合
    - 等値結合 (= で結合) の場合に非常に高速
    - メモリにハッシュ表が収まる必要がある

    Merge Join:
    - 両テーブルを結合キーでソートしてから突き合わせ
    - ソート済みのデータ (インデックスがある場合) に有効
    - 大量データの等値結合に適する
    """)

    # --- キャッシュ ---
    heading("Redis Cache-Aside パターン")

    p("""\
    フィードの読み込みが遅い。毎回 JOIN を走らせるのは重い。
    Redis でキャッシュを入れます。

    Cache-Aside (Lazy Loading):
    1. アプリが Redis にキーを問い合わせる
    2. キャッシュヒット → Redis から返す (高速)
    3. キャッシュミス → DB から読み、Redis に書き込んでから返す

    なぜ Cache-Aside か:
    - アプリが明示的にキャッシュを制御するため、予測しやすい
    - キャッシュに載るのは実際に読まれたデータだけ

    問題と対策:

    Cache Stampede (雷群問題):
    人気記事のキャッシュが期限切れ → 同時に100リクエストが DB に殺到
    対策: ロックをかけて1つだけ DB に読みに行く (singleflight パターン)

    Cache Penetration (貫通):
    存在しないデータへのリクエストが繰り返される
    → 毎回キャッシュミスして DB に問い合わせる
    対策: 存在しないことも短時間キャッシュする (null キャッシュ)
    対策: ブルームフィルタで存在確認

    Cache Breakdown (崩壊):
    ホットキーのキャッシュが切れた瞬間に大量リクエスト
    対策: ホットキーには有効期限を設定しない、
          またはバックグラウンドで更新する
    """)

    # --- CAP定理 ---
    heading("CAP 定理")

    p("""\
    分散データベースに関する有名な定理です。

    3 つの性質のうち、同時に 2 つまでしか保証できない:

    C (Consistency): 全ノードが同じデータを返す
    A (Availability): 全リクエストに応答を返す
    P (Partition Tolerance): ネットワーク分断に耐える

    なぜ 3 つ同時は無理か:
    ネットワーク分断 (P) が発生した場合を考えます。
    ノード A とノード B が通信できなくなりました。

    - C を優先: B がデータを返すと A と不一致かもしれない → B はエラーを返す
      → Availability を犠牲にしている
    - A を優先: B は持っているデータで応答する (古いかもしれない)
      → Consistency を犠牲にしている

    分断が起きている間は、C と A のどちらかを選ぶしかありません。

    実際には P (分断耐性) は避けられない:
    ネットワークは必ず故障し得ます。
    だから実質的な選択は「CP (一貫性優先)」か「AP (可用性優先)」です。

    CP の例: PostgreSQL, HBase, MongoDB (with majority write concern)
    AP の例: Cassandra, DynamoDB, CouchDB
    """)

    misconception(
        "CAP定理は3つから2つを選ぶ",
        "Partition (分断) は避けられないので、実質は分断時に "
        "Consistency を取るか Availability を取るかの選択。"
        "分断がない通常時は C と A の両方を実現できる"
    )

    # --- ACID ---
    heading("ACID と分離レベル")

    p("""\
    トランザクションが保証する 4 つの性質:

    Atomicity (原子性):
    トランザクション内の操作は「全て成功」か「全て失敗」。
    中途半端な状態にならない。
    例: 送金で「引き落とし」が成功して「入金」が失敗 → 両方ロールバック

    Consistency (一貫性):
    トランザクション前後で、DB が整合性制約を満たす。
    例: 残高がマイナスにならない制約

    Isolation (分離性):
    並行するトランザクションが互いに影響しない。
    ただし、完全な分離は性能が低下するため、分離レベルを選べる。

    Durability (永続性):
    コミットされたデータは永続的に保存される (電源断でも失われない)。
    WAL + fsync がこれを保証する。
    """)

    heading("分離レベル")

    p("""\
    Isolation は性能とのトレードオフがあるため、
    4 段階の分離レベルが定義されています:

    Read Uncommitted:
    他のトランザクションのコミット前のデータが見える (dirty read)。
    ほぼ使われない。

    Read Committed (PostgreSQL デフォルト):
    コミット済みのデータだけ見える。
    ただし、同じクエリを2回実行すると結果が変わり得る (non-repeatable read)。

    Repeatable Read:
    トランザクション開始時点のスナップショットを見る。
    同じクエリは同じ結果を返す。
    ただし、他のトランザクションが INSERT した行が
    見えたり見えなかったりする (phantom read) 問題が理論上ある。

    Serializable:
    全トランザクションが順番に実行されたかのように振る舞う。
    最も強い保証だが、性能ペナルティが大きい。
    """)

    # --- MVCC ---
    heading("MVCC (Multi-Version Concurrency Control)")

    p("""\
    PostgreSQL が Read Committed や Repeatable Read を実現する仕組みです。

    基本原理:
    行を更新するとき、古い行を消さずに新しいバージョンを作ります。
    各トランザクションは自分が「見るべきバージョン」を判断します。

    PostgreSQL の実装:
    各行に以下のメタデータがある:
    - xmin: この行を作成したトランザクション ID
    - xmax: この行を削除/更新したトランザクション ID (0 = まだ有効)

    可視性判定:
    トランザクション TX100 が行を読むとき:
    - xmin < 100 かつコミット済み → 見える (自分より前に作られた)
    - xmin >= 100 → 見えない (自分の後に作られた)
    - xmax > 0 かつ xmax < 100 かつコミット済み → 見えない (削除済み)

    MVCC の利点:
    読み取りがロックを取らない。
    書き込みトランザクションがあっても、読み取りはブロックされない。

    MVCC の問題:
    古いバージョンの行が溜まる → VACUUM で回収する必要がある。
    VACUUM が遅延すると、テーブルが膨張する (テーブル bloat)。
    autovacuum の設定が重要。
    """)

    interview("""\
    「DBの書き込みパスを説明せよ」

    「INSERTが実行されると、まずWAL (Write-Ahead Log) に変更内容を記録し、
    次にBuffer Pool上のページを更新します。WALはfsyncで永続化されます。
    Buffer Poolのdirtyページは定期的なCheckpointでディスクにフラッシュされます。
    この設計により、クラッシュ時もWALからリカバリ可能で、
    通常の書き込みはメモリ上で完結するため高速です。
    SSD内部ではFTLが論理→物理アドレス変換やウェアレベリングを行い、
    書き込み増幅を最小化しています。」
    """)


