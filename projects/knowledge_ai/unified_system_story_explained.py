def chapter_2_explained():
    title("Chapter 2: APIを設計する (深掘り)")

    # --- REST ---
    heading("REST とは何か")

    p("""\
    REST (Representational State Transfer) は、
    Roy Fielding が 2000 年の博士論文で提唱したアーキテクチャスタイルです。

    核心は「リソース指向」です。
    API の URL は「動詞」ではなく「名詞 (リソース)」で設計します。

    悪い例 (動詞ベース):
    POST /createPost        → 動詞が URL に入っている
    GET  /getPostById?id=42 → 動詞が URL に入っている
    POST /deletePost        → POST で削除は意味が不明

    良い例 (リソースベース):
    POST   /posts           → 新しい投稿を作成
    GET    /posts/42        → 投稿 42 を取得
    PUT    /posts/42        → 投稿 42 を更新
    DELETE /posts/42        → 投稿 42 を削除

    HTTP メソッドが「何をするか」を表し、
    URL が「何に対してするか」を表します。
    """)

    heading("Richardson Maturity Model")

    p("""\
    REST の成熟度を 4 段階で表すモデルです:

    Level 0: The Swamp of POX
    - HTTP をトランスポートとしてだけ使う
    - 全リクエストが POST /api に送られ、
      リクエストボディで操作を指定する (SOAP がこれに近い)

    Level 1: リソース
    - URL でリソースを表現する (/posts, /users)
    - ただし HTTP メソッドの使い分けは不十分

    Level 2: HTTP 動詞
    - GET, POST, PUT, DELETE を正しく使い分ける
    - ステータスコードも適切に使う (201 Created, 404 Not Found)
    - 実務ではここで十分なことが多い

    Level 3: HATEOAS (Hypermedia)
    - レスポンスに「次に何ができるか」のリンクを含める
    - クライアントは URL をハードコードせず、
      レスポンスのリンクをたどって操作する
    - 理想的だが、フロントエンドとの契約が曖昧になりがち

    実務では Level 2 を目指すのが現実的です。
    """)

    # --- REST vs GraphQL vs gRPC ---
    heading("REST vs GraphQL vs gRPC")

    p("""\
    API の設計方式は REST だけではありません。
    それぞれの内部動作と使い分けを理解しましょう。

    REST:
    - リソース単位でエンドポイントを作る
    - Over-fetching: 不要なフィールドも返ってくる
    - Under-fetching: 関連データを取るのに複数リクエスト必要
    - キャッシュが容易 (URL ベースでキャッシュ可能)
    - ツールが豊富 (API Gateway, CDN, ブラウザキャッシュ)

    GraphQL:
    - 1つのエンドポイント (/graphql) に全てのクエリを送る
    - クライアントが欲しいフィールドだけ指定できる
    - 1リクエストで関連データも含めて取得可能
    - Schema 定義でフロント・バック間の契約が明確
    - 問題1: キャッシュが難しい (全て POST /graphql なので URL ベースキャッシュ不可)
    - 問題2: N+1 問題 (resolver が個別にDB問い合わせ → DataLoader で解決)
    - 問題3: クエリの複雑度制限 (悪意あるクエリで過負荷になり得る)

    gRPC:
    - Protocol Buffers でスキーマ定義 (.proto ファイル)
    - バイナリシリアライズで高速 (JSON の 2-10 倍)
    - HTTP/2 必須 → 双方向ストリーミング対応
    - マイクロサービス間通信に最適 (型安全、コード自動生成)
    - 問題: ブラウザから直接呼べない (grpc-web が必要)

    使い分け:
    - 外部公開 API → REST (ツール豊富、習熟度が高い)
    - フロント ↔ BFF → GraphQL (柔軟なデータ取得)
    - サービス間通信 → gRPC (高速、型安全)
    """)

    misconception(
        "GraphQL は REST の上位互換",
        "それぞれトレードオフがある。キャッシュのしやすさ、"
        "ツール対応、学習コスト等で REST が優位な場面は多い"
    )

    # --- JWT ---
    heading("JWT (JSON Web Token) の構造")

    p("""\
    TechPulse のユーザー認証には JWT を使います。

    JWT とは:
    ユーザーの認証情報を JSON 形式でエンコードしたトークンです。
    サーバーがセッションを保持する必要がなく (ステートレス)、
    トークン自体に情報が含まれています。

    構造 (3つの部分をドットで連結):
    Header.Payload.Signature

    Header (ヘッダ):
    {
      "alg": "RS256",    ← 署名アルゴリズム
      "typ": "JWT"       ← トークンタイプ
    }
    これを Base64URL エンコードしたもの。

    Payload (ペイロード):
    {
      "sub": "user-123",     ← 誰のトークンか
      "name": "田中太郎",
      "role": "admin",       ← 権限情報
      "iat": 1710000000,     ← 発行日時 (Unix time)
      "exp": 1710003600      ← 有効期限 (Unix time)
    }
    これも Base64URL エンコード。

    Signature (署名):
    RSASHA256(
      base64UrlEncode(header) + "." + base64UrlEncode(payload),
      秘密鍵
    )

    なぜ Base64URL か:
    JSON にはスペースや特殊文字が含まれるため、
    URL やヘッダに安全に含められる形式にエンコードします。
    Base64URL は Base64 から + を - に、/ を _ に置換したものです。

    重要: Payload は暗号化されていない
    Base64 はエンコード (可逆変換) であって暗号化ではありません。
    誰でもデコードして中身を読めます。
    だから機密情報 (パスワード等) を入れてはいけません。
    署名は「改ざんされていないこと」を検証するだけです。
    """)

    # --- OAuth 2.0 ---
    heading("OAuth 2.0")

    p("""\
    OAuth 2.0 は「認可 (Authorization)」のプロトコルです。
    認証 (Authentication: あなたは誰？) とは異なり、
    認可は「あなたは何ができるか？」を扱います。

    よくある使い方:
    「Google でログイン」ボタンを押すと、
    Google があなたの代わりに TechPulse に
    「この人のメールアドレスとプロフィール画像」へのアクセスを許可します。

    Authorization Code Flow (最も安全):

    1. ユーザーがTechPulseの「Googleでログイン」を押す
    2. TechPulseがGoogleの認可エンドポイントにリダイレクト
    3. ユーザーがGoogleでログイン → 許可する
    4. GoogleがTechPulseに「認可コード」を返す (リダイレクト)
    5. TechPulseのサーバーが「認可コード」を使って
       Googleのトークンエンドポイントに「アクセストークン」を要求
    6. アクセストークンで Google API からユーザー情報を取得

    なぜ認可コードを挟むか:
    アクセストークンをブラウザのリダイレクトURL に直接含めると、
    ブラウザの履歴やログに残ってしまうリスクがあります。
    認可コードはサーバー間通信でアクセストークンと交換するため、
    トークンがブラウザに露出しません。
    """)

    # --- RSA ---
    heading("RSA の数学的原理")

    p("""\
    JWT の署名アルゴリズム RS256 は RSA を使います。
    RSA がなぜ安全なのか、数学的に理解しましょう。

    基本原理: 素因数分解の困難性

    大きな素数 p と q を掛けるのは簡単:
      p = 61, q = 53 → n = p × q = 3233

    しかし n = 3233 から p, q を見つけるのは
    (小さい数なら簡単ですが) n が数百桁になると現実的に不可能です。

    鍵の生成:
    1. 大きな素数 p, q を選ぶ (それぞれ 1024 ビット以上)
    2. n = p × q を計算
    3. φ(n) = (p-1)(q-1) を計算 (オイラーのトーシェント関数)
    4. e を選ぶ (通常 65537)。φ(n) と互いに素であること
    5. d を計算: e × d ≡ 1 (mod φ(n))

    公開鍵: (n, e)
    秘密鍵: (n, d)

    署名:
    sig = message^d mod n  (秘密鍵で署名)

    検証:
    message = sig^e mod n  (公開鍵で検証)

    なぜ安全か:
    秘密鍵 d を知るには φ(n) = (p-1)(q-1) が必要。
    φ(n) を知るには n を素因数分解して p, q を求める必要がある。
    n が十分大きければ素因数分解は計算量的に不可能。
    """)

    # --- Rate Limiting ---
    heading("レート制限")

    p("""\
    API を保護するために、単位時間あたりのリクエスト数を制限します。

    Token Bucket アルゴリズム:
    バケツにトークンが入っている。
    リクエストが来るたびにトークンを1つ消費する。
    一定間隔でトークンが補充される。
    バケツが空ならリクエストを拒否 (429 Too Many Requests)。

    特徴:
    - バースト許容: バケツにトークンが溜まっていれば
      短期間に多くのリクエストを受け付ける
    - 実装が簡単

    Sliding Window:
    過去 N 秒間のリクエスト数をカウントし、
    閾値を超えたら拒否する。

    特徴:
    - バースト耐性: 固定ウィンドウの境界でのバーストを防ぐ
    - Token Bucket より実装がやや複雑
    """)

    # --- SQL Injection ---
    heading("SQL Injection と防御")

    p("""\
    SQL Injection は OWASP Top 10 の A03 に位置する深刻な脆弱性です。

    攻撃例:
    ユーザーが検索ボックスに以下を入力:
      ' OR '1'='1

    アプリケーションが文字列結合でSQLを組み立てている場合:
      SELECT * FROM posts WHERE title = '' OR '1'='1'

    '1'='1' は常に true なので、全投稿が返ってしまいます。
    さらに悪意のある入力:
      '; DROP TABLE posts; --

    これでテーブルが削除される可能性があります。

    防御: Prepared Statement (パラメータ化クエリ)
    SQL 文とデータを分離します:
      SELECT * FROM posts WHERE title = ?
    データベースエンジンは ? を「SQLの構文」ではなく
    「データ」として扱うため、SQL として解釈されません。

    なぜこれで防げるか:
    Prepared Statement では、SQL の構文解析が先に行われ、
    後からバインドされた値はリテラル (文字列データ) として処理されます。
    攻撃者が何を入力しても、それは SQL 命令にはなりません。
    """)

    interview("""\
    「API設計で最も重要なことは？」

    「リソース指向の設計、適切なHTTPメソッドとステータスコードの使用、
    そしてセキュリティです。具体的にはPrepared Statementによるインジェクション防止、
    JWTによる認証、レート制限による過負荷防止を実装します。
    RESTかGraphQLかgRPCかはユースケースで選びます。」
    """)


