# 第2章: テスト計画・設計
# ============================================================
def chapter2_test_design():
    title("第2章: テスト計画・設計 ─ 「何をどうテストするか」を決める技術")

    heading("2-1. テスト計画書 (IEEE 829)")
    p("""
    IEEE 829: テストドキュメントの標準規格。
    テスト計画書に何を書くべきかを定義している。

    テスト計画書の主要項目:
    - テスト対象とスコープ: 何をテストし、何をテストしないか
    - テスト戦略: どのレベル・タイプのテストを行うか
    - リソース・スケジュール: 誰が、いつまでに
    - 環境要件: テスト環境の構成
    - 開始基準・終了基準: いつテストを始め、いつ終わるか
    - リスクと対策: テスト実施上のリスク

    なぜ計画が必要か: テストは無限に行える（入力の組み合わせは膨大）。
    限られたリソースで最大の効果を出すには、戦略的な計画が不可欠。
    """)

    heading("2-2. テスト設計技法 ─ ブラックボックス技法")
    p("""
    テスト設計技法: テストケースを体系的に導出する方法論。
    闇雲にテストするのではなく、技法に基づいて効率的にケースを選ぶ。

    ① 同値分割法 (Equivalence Partitioning)
       入力を「同じ振る舞いをするグループ」に分割し、
       各グループから代表値を1つ選ぶ。
       例: 年齢入力(1-120)→ 有効(50)、無効低(0)、無効高(121)
       なぜ有効: 同じグループ内なら結果は同じはずだから。

    ② 境界値分析 (Boundary Value Analysis)
       バグは境界に集中するという経験則に基づく。
       同値クラスの「端」の値をテストする。
       例: 1-120 → 0, 1, 2, 119, 120, 121
       なぜ有効: off-by-oneエラー（±1のミス）は最も多いバグの一つ。

    ③ デシジョンテーブル (Decision Table)
       複数の条件の組み合わせと、それに対する動作を表形式で整理。
       条件が3つなら最大 2^3=8 パターン。
       なぜ有効: 条件の組み合わせ漏れを防げる。ビジネスルールのテストに最適。

    ④ 状態遷移テスト (State Transition Testing)
       システムの「状態」と「イベント」による遷移を図示し、
       各遷移パスをテストする。
       例: 注文状態 → 支払い済 → 発送済 → 配達完了
       なぜ有効: 状態管理のバグ（不正な遷移）を検出できる。

    ⑤ ペアワイズテスト (Pairwise Testing)
       全組み合わせではなく、任意の2パラメータの全組み合わせを網羅。
       例: OS(3種) × ブラウザ(4種) × 解像度(3種) = 36通り
       → ペアワイズなら約10通りで2因子網羅可能。
       なぜ有効: バグの大半は2因子の相互作用で発生する(経験的事実)。
    """)

    misconception(
        "テスト技法を使うと工数が増える",
        "技法を使うことで無駄なテストを減らし、必要十分なケース数に絞れる"
    )

    heading("2-3. テストケースとテストマトリクス")
    p("""
    テストケース: テストの最小実行単位。
    - 前提条件、入力、手順、期待結果を明記する。
    - 曖昧な期待結果は「テストしていないのと同じ」。

    テストマトリクス(トレーサビリティマトリクス):
    要件とテストケースの対応表。
    - 各要件にテストが紐付いているか確認
    - テストされていない要件（カバレッジギャップ）を発見
    - 要件変更時の影響範囲を特定

    アナロジー: テストマトリクスは「チェックリスト付き地図」。
    全ての目的地（要件）に道（テスト）が通っているか一覧で確認できる。
    """)

    interview("""
    テスト設計技法は「効率的にバグを見つける」ための方法論
    同値分割→グループ代表、境界値→端の値、デシジョン→条件組合せ
    ペアワイズは組合せ爆発を抑える実践的手法
    トレーサビリティマトリクスで要件とテストの紐付けを保証する
    """)


