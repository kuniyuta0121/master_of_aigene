# 第3章: テストレベルとタイプ
# ============================================================
def chapter3_test_levels():
    title("第3章: テストレベルとタイプ ─ テストの全体像を掴む")

    heading("3-1. テストレベル（4段階）")
    p("""
    テストレベル: 「いつ、何の粒度でテストするか」の分類。

    ① 単体テスト (Unit Test)
       個々の関数・クラスを独立してテスト。
       開発者が書く。最も速く、最もコストが低い。
       モック/スタブで外部依存を分離する。
       目的: ロジックの正しさを確認。

    ② 結合テスト (Integration Test)
       複数のコンポーネント間の連携をテスト。
       DB接続、API呼び出し、メッセージキューなど。
       目的: コンポーネント間のインターフェースの正しさを確認。

    ③ システムテスト (System Test)
       システム全体を結合した状態でテスト。
       本番に近い環境で、要件仕様に基づいて実施。
       目的: システム全体が要件を満たすか確認。

    ④ 受入テスト (Acceptance Test)
       ユーザーまたは顧客視点でのテスト。
       - UAT (User Acceptance Test): ビジネス要件の確認
       - 契約受入テスト: 契約条件の充足確認
       - 運用受入テスト: 運用手順の確認
       目的: 「このシステムを受け入れてよいか」の判断。

    アナロジー: 家を建てる場合、
    単体=個々の部品(蛇口、スイッチ)の検査
    結合=配管と蛇口を繋いだ時の水漏れ検査
    システム=家全体の機能確認（電気・水道・ガス総合）
    受入=施主が住んでみて問題ないか確認
    """)

    heading("3-2. テストタイプ ─ 機能テスト")
    p("""
    機能テスト: 「何をするか (What)」を検証するテスト。
    仕様通りに動作するかを確認する。

    - 正常系: 正しい入力に対して正しい出力が返るか
    - 異常系: 不正な入力に対して適切にエラー処理するか
    - 境界系: 境界値で正しく動作するか

    機能テストは全テストレベルで実施される。
    """)

    heading("3-3. テストタイプ ─ 非機能テスト")
    p("""
    非機能テスト: 「どのように動くか (How)」を検証するテスト。

    ① 性能テスト: 応答時間、スループットの確認（第5章で詳述）

    ② 負荷テスト: 高負荷時の挙動確認

    ③ セキュリティテスト:
       - 脆弱性スキャン（OWASP ZAPなど）
       - ペネトレーションテスト（侵入テスト）
       - セキュリティコードレビュー

    ④ ユーザビリティテスト:
       実際のユーザーにタスクを実行してもらい、使いやすさを評価。
       ヒューリスティック評価: ニールセンの10原則に基づく専門家評価。

    ⑤ アクセシビリティテスト:
       障害を持つユーザーも利用できるか。WCAG 2.1準拠の確認。
       スクリーンリーダー対応、キーボード操作、色コントラスト比など。
    """)

    heading("3-4. 回帰テスト")
    p("""
    回帰テスト (Regression Test):
    変更によって既存機能が壊れていないか確認するテスト。

    なぜ重要か: コード変更の影響範囲は予測が難しい。
    「直したつもりが別の場所を壊した」を防ぐ。

    回帰テストは自動化の最有力候補。
    理由: 繰り返し実行される、手動だと単調で見落としやすい、
    変更の度に必要になる。

    回帰テストスイートの管理:
    - 全てのテストを毎回実行するのは非現実的な場合がある
    - リスクベースで優先順位をつける
    - 影響分析に基づいて実行範囲を絞る
    """)

    misconception(
        "非機能テストは機能テストの後でよい",
        "性能要件やセキュリティ要件は設計段階から考慮すべき。後から対処すると手戻りが膨大"
    )

    interview("""
    テストレベル: 単体→結合→システム→受入（粒度が大→小→大）
    機能テスト = What(仕様通りか)、非機能テスト = How(品質特性)
    回帰テスト = 変更の副作用検出。自動化最優先の対象
    非機能テストも早期に計画・実施することがシフトレフトの実践
    """)


