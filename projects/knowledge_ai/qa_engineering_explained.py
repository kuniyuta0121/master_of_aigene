# 第5章: 性能テスト
# ============================================================
def chapter5_performance_testing():
    title("第5章: 性能テスト ─ 「遅い」は「壊れている」と同じ")

    heading("5-1. 性能テストの種類")
    p("""
    性能テストは目的によって4種類に分かれる。

    ① 負荷テスト (Load Test)
       予想される通常負荷・ピーク負荷での性能を測定。
       「100人同時アクセスで応答3秒以内か？」
       目的: 性能要件を満たすか確認。

    ② ストレステスト (Stress Test)
       限界を超えた負荷をかけてシステムの振る舞いを確認。
       「1000人同時アクセスで何が起きるか？」
       目的: 限界点の特定と、限界超過時のグレースフルデグラデーション確認。

    ③ スパイクテスト (Spike Test)
       突発的な急激な負荷増を再現。
       「セール開始時に一瞬で10倍のアクセスが来たら？」
       目的: オートスケーリングの反応速度確認。

    ④ 耐久テスト (Endurance/Soak Test)
       長時間(数時間～数日)一定負荷をかけ続ける。
       目的: メモリリーク、DBコネクション枯渇など
       時間経過で顕在化する問題の検出。
    """)

    heading("5-2. 性能テストツール")
    p("""
    ① JMeter (Apache)
       - Java製。GUI/CUI両対応。最も普及。
       - プロトコル対応が豊富(HTTP/JDBC/JMS等)。
       - GUIでシナリオを作り、CUIで大規模実行。
       - 弱点: スクリプトがXMLで可読性が低い。

    ② k6 (Grafana Labs)
       - JavaScript/TypeScriptでシナリオを記述。
       - CLIベース。CI/CDと親和性が高い。
       - Grafana/InfluxDBとの統合が容易。
       - モダンな性能テストの第一選択肢。

    ③ Gatling
       - Scala/Java/Kotlin対応。DSLでシナリオ記述。
       - HTMLレポートが美しい。
       - 高いスループット生成能力。
       - JVM系プロジェクトに適合。
    """)

    heading("5-3. 性能要件の定義")
    p("""
    性能テストの前に、性能要件を明確に定義する必要がある。

    主要な性能指標:
    - 応答時間 (Response Time): リクエストからレスポンスまでの時間
      平均値よりパーセンタイル(P95, P99)が重要。
      「平均200ms、P99で1秒以内」のように定義。
      なぜP95/P99か: 平均では外れ値が隠れる。
      ユーザーの5%/1%が体験する最悪ケースを管理する。

    - スループット (Throughput): 単位時間あたり処理数
      「1000 req/sec を処理できること」

    - 同時接続数 (Concurrent Users): 同時にアクティブなユーザー数
      「同時500ユーザーをサポートすること」

    - リソース使用率: CPU 70%以下、メモリ80%以下 等

    Littleの法則:
    同時接続数 = スループット × 平均応答時間
    例: 100 req/sec × 0.5sec = 50同時接続
    性能要件の整合性チェックに使える。
    """)

    heading("5-4. ボトルネック分析")
    p("""
    性能問題が見つかった後、ボトルネックを特定する。

    ボトルネックの典型的な場所:
    - アプリケーション層: 非効率なアルゴリズム、N+1クエリ
    - データベース層: スロークエリ、ロック競合、インデックス不足
    - ネットワーク層: 帯域不足、レイテンシ
    - インフラ層: CPU/メモリ/ディスクI/Oの飽和

    分析手法:
    - APMツール (Application Performance Monitoring)
      → リクエストのトレーシングでボトルネック箇所を特定
    - プロファイリング: CPU/メモリのホットスポット分析
    - DBスロークエリログ: 遅いクエリの特定と最適化
    - リソースモニタリング: CPU/メモリ/I/Oの時系列監視

    アナロジー: 高速道路の渋滞分析と同じ。
    どこで詰まっているかを特定し、その箇所を拡張する。
    """)

    misconception(
        "性能テストは本番リリース直前にやればよい",
        "性能問題はアーキテクチャに起因することが多く、直前の修正は困難。継続的に測定する"
    )

    interview("""
    性能テスト4種: 負荷/ストレス/スパイク/耐久、目的が異なる
    性能指標はP95/P99で定義。平均値は罠（外れ値を隠す）
    Littleの法則で性能要件の整合性を検証できる
    k6 = モダンな性能テストの有力選択肢（CI/CD親和性）
    """)


