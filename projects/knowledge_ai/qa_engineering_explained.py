def chapter7_quality_process():
    title("第7章: 品質プロセス ─ 組織として品質を作り込む仕組み")

    heading("7-1. CMMI (Capability Maturity Model Integration)")
    p("""
    CMMI: 組織のプロセス成熟度を5段階で評価するフレームワーク。

    レベル1: 初期 ─ プロセスが属人的。英雄的努力で成功する。
    レベル2: 管理 ─ プロジェクト単位でプロセスが定義されている。
    レベル3: 定義 ─ 組織全体で標準プロセスが定義されている。
    レベル4: 定量的管理 ─ プロセスを統計的に管理している。
    レベル5: 最適化 ─ 継続的にプロセスを改善している。

    日本の大手SIerではCMMI レベル3-5を取得している企業が多い。
    ただし形式的な取得に留まり実質が伴わないケースも。
    """)

    heading("7-2. ISO 9001")
    p("""
    ISO 9001: 品質マネジメントシステム(QMS)の国際規格。

    核心はPDCAサイクル:
    Plan: 品質目標と達成計画の策定
    Do: 計画に基づく実行
    Check: 結果の監視・測定
    Act: 改善活動

    ISO 9001はソフトウェア固有ではなく全産業対象。
    ソフトウェアではISO/IEC 90003がISO 9001適用のガイドライン。
    """)

    heading("7-3. アジャイルにおけるQA")
    p("""
    アジャイル開発では品質はスプリント内で確保する。

    ■ Definition of Done (DoD)
      「完了」の定義を明文化。
      例: コードレビュー済、単体テスト通過、結合テスト通過、
      ドキュメント更新、性能基準充足。
      DoDがないと「完了」が曖昧になり品質が低下する。

    ■ 3 Amigos
      開発者・テスター・PO（ビジネス）の3者でストーリーを議論。
      仕様の認識齟齬を早期に解消する。
      各視点からの質問が品質を作り込む。

    ■ BDD (Behavior Driven Development)
      Given-When-Then形式で振る舞いを記述。
      ビジネスとテストの共通言語になる。

    ■ アジャイルQAの原則
      - テストは後工程ではなくスプリント内で完結
      - QAエンジニアはチームの一員（別チームではない）
      - 自動テストが基本。手動は探索的テストに集中
      - 継続的にテストを実行し、フィードバックを早く
    """)

    heading("7-4. リスクベーステスト")
    p("""
    全ての機能を同じ深さでテストするのは非効率。
    リスクの高い部分に重点を置くのがリスクベーステスト。

    リスク = 影響度 × 発生確率

    高リスク: 決済処理、認証機能、個人情報管理
    → 重点的にテスト（複数技法、多くのケース）

    低リスク: 管理画面の色変更、ヘルプテキスト
    → 最小限のテストで十分

    リスク分析はステークホルダーと共同で行う。
    技術的リスクだけでなくビジネスリスクも考慮する。
    """)

    heading("7-5. 探索的テスト")
    p("""
    探索的テスト (Exploratory Testing):
    テスト設計と実行を同時に行うアプローチ。
    事前にテストケースを書かず、テスターの経験と直感で探索する。

    Session-Based Test Management (SBTM):
    探索的テストを構造化する手法。
    - チャーター: テストの目的と範囲を定義（60-90分のセッション）
    - セッション: 集中してテスト実行
    - デブリーフ: 結果をレポートし、次のチャーターを決定

    探索的テストが有効な場面:
    - 新機能の初期調査
    - 自動テストでカバーしにくい領域
    - ドメイン知識を活かした深い検証
    - ユーザビリティの問題発見
    """)

    heading("7-6. レビュー技法")
    p("""
    レビューはテストより早い段階で欠陥を検出する最も費用対効果の高い手法。

    ■ インスペクション (Formal Inspection)
      Fagan Inspectionとも呼ばれる最も厳格なレビュー。
      - 役割: モデレータ、著者、レビューア、記録者
      - 手順: 計画→概要説明→準備→会議→修正→フォローアップ
      - 統計的に欠陥除去効率が最も高い（DRE 60-90%）
      - ただし工数がかかるため、重要なドキュメント/コードに限定。

    ■ ウォークスルー (Walkthrough)
      著者がコードを説明しながらレビューアが質問・指摘。
      インスペクションより軽量。教育目的にも有効。

    ■ ピアレビュー / コードレビュー
      最も日常的なレビュー形式。PR(Pull Request)ベース。
      GitHubのPRレビューが現代の標準的な実施方法。
    """)

    misconception(
        "アジャイルではドキュメントもテスト計画も不要",
        "アジャイルでも品質基準(DoD)は必須。ドキュメントは最小限だが「ゼロ」ではない"
    )

    interview("""
    CMMI = 組織の成熟度5段階、ISO 9001 = QMSのPDCAサイクル
    アジャイルQA: DoD + 3 Amigos + スプリント内完結 + 自動テスト
    リスクベーステスト = 影響度×発生確率で優先度を決定
    探索的テスト = 自動化の補完、SBTMで構造化
    レビューはDRE最高の品質活動。インスペクション > ウォークスルー
    """)


