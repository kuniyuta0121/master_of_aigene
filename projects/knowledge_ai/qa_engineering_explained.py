# 第4章: テスト自動化戦略
# ============================================================
def chapter4_test_automation():
    title("第4章: テスト自動化戦略 ─ 自動化は手段であり目的ではない")

    heading("4-1. テスト自動化ピラミッド")
    p("""
    Mike Cohnが提唱した自動テストの理想的な配分モデル。

          /  E2E  \\       少数・高コスト・遅い・脆い
         /----------\\
        / 結合テスト  \\    中程度
       /--------------\\
      /  単体テスト    \\   大量・低コスト・速い・安定

    底辺(単体テスト): 最も多く。実行が速く、安定している。
    中間(結合テスト): APIテスト、コンポーネント間連携の確認。
    頂点(E2Eテスト): ブラウザ操作など。最小限に留める。

    なぜピラミッド型か:
    - 上に行くほど実行時間が長く、メンテコストが高い
    - 上に行くほどFlaky(不安定)になりやすい
    - 下層で十分検証できるものを上層でテストするのは無駄

    アンチパターン: アイスクリームコーン（逆ピラミッド）
    手動テストやE2Eが大量で、単体テストが少ない状態。
    実行が遅く、フィードバックが遅く、メンテが地獄。
    """)

    heading("4-2. 自動化のROI判断")
    p("""
    全てのテストを自動化すべきではない。ROIで判断する。

    自動化すべきテスト:
    - 繰り返し実行される（回帰テスト）
    - 手動だと時間がかかる（大量データ、多環境）
    - 正確性が必要（計算結果の検証）
    - CIで頻繁に実行したい

    自動化すべきでないテスト:
    - 一度しか実行しない
    - UI/UXの主観的評価
    - 探索的テスト（人間の直感が必要）
    - 頻繁に仕様が変わる部分（メンテコスト > 効果）

    ROI計算の目安:
    自動化コスト = 作成時間 + メンテ時間 × 期間
    手動コスト = 実行時間 × 実行回数
    手動コスト > 自動化コスト なら自動化する価値あり。
    """)

    heading("4-3. E2Eテストツール比較")
    p("""
    ① Selenium
       - 最も歴史が長い。WebDriver標準。
       - 多言語対応(Java/Python/C#/Ruby)
       - 環境構築がやや複雑。WebDriverの管理が必要。
       - 大規模プロジェクト、既存資産がある場合に選択。

    ② Playwright (Microsoft)
       - モダンな設計。自動待機(Auto-wait)機能。
       - Chromium/Firefox/WebKit全対応。
       - APIテストも可能。トレース機能が強力。
       - 新規プロジェクトでの第一選択肢になりつつある。

    ③ Cypress
       - ブラウザ内で実行。デバッグが容易。
       - タイムトラベル（各ステップのスナップショット）。
       - Chromium系のみ（Firefox実験的対応）。
       - フロントエンド開発者に人気。
    """)

    heading("4-4. CI/CDとの統合")
    p("""
    テスト自動化の真価はCI/CDパイプラインとの統合で発揮される。

    典型的なパイプライン:
    1. コードPush → 単体テスト実行（数分以内）
    2. 単体テスト通過 → 結合テスト実行（10-20分）
    3. 結合テスト通過 → E2Eテスト実行（30分-1時間）
    4. 全テスト通過 → ステージング環境にデプロイ

    原則: フィードバックは早く。失敗は早く知る。
    単体テストが失敗したらE2Eは実行しない（時間の無駄）。
    """)

    heading("4-5. Flaky Test対策")
    p("""
    Flaky Test: 同じコードに対して成功/失敗が不安定なテスト。
    CI/CDの信頼性を破壊する最大の敵。

    主な原因と対策:
    - タイミング依存 → 明示的な待機、ポーリング
    - テスト間依存 → テストの独立性確保、順序非依存
    - 外部サービス依存 → モック/スタブに置換
    - テストデータ汚染 → 各テストでデータを初期化
    - リソース競合 → 並列実行時のリソース分離

    運用対策:
    - Flaky Testの検出: 同じテストを複数回実行して検出
    - 隔離: Flakyと判定したテストを隔離スイートに移動
    - 修正の優先度を上げる: 放置すると「テストが信用されなくなる」
    """)

    heading("4-6. テストデータ管理")
    p("""
    テストデータの管理は自動テストの成功を左右する。

    戦略:
    - テスト毎にデータを生成・クリーンアップ（独立性を保証）
    - ファクトリパターン: テストデータ生成を共通化
    - 本番データのマスキング: 個人情報を匿名化して利用
    - シードデータ: マスタデータは共通のシードで投入
    """)

    misconception(
        "テストカバレッジ100%を目指して全てを自動化すべき",
        "ROIが合わないテストの自動化は負債になる。80/20の法則で重要な部分を優先"
    )

    interview("""
    自動化ピラミッド: 単体(多)→結合(中)→E2E(少)が理想
    自動化判断はROI: 繰り返し実行×手動コスト高 = 自動化価値大
    Flaky Test = CI/CDの信頼性を破壊。検出→隔離→修正を即座に
    Playwright = 現在のE2Eテストのデファクトスタンダードになりつつある
    """)


