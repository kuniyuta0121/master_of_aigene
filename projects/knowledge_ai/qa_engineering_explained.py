# 第1章: 品質とは何か
# ============================================================
def chapter1_quality_fundamentals():
    title("第1章: 品質とは何か ─ 「バグがない」だけでは不十分")

    heading("1-1. そもそも品質とは")
    p("""
    「品質」という言葉は曖昧に使われがちだが、ISOで明確に定義されている。

    品質(Quality): 要求事項を満たす度合い。
    つまり「ユーザーが求めていることを、どれだけ満たしているか」が品質。
    速いだけでもダメ、機能が多いだけでもダメ。要求に対する適合度が品質。

    アナロジー: レストランの品質は「高級食材を使っている」ことではなく、
    「お客さんが期待する料理を、期待通りに提供できるか」で決まる。
    ファストフードでも品質は高くなりうる。
    """)

    heading("1-2. ISO 25010 ─ ソフトウェア品質モデル")
    p("""
    ISO 25010は品質を8つの特性に分類した国際標準。
    「品質を語るときの共通言語」として極めて重要。

    ① 機能適合性 (Functional Suitability)
       ソフトウェアが要求された機能を正しく提供するか。
       - 機能完全性: 必要な機能が全て揃っているか
       - 機能正確性: 結果が正確か（計算誤差など）
       - 機能適切性: 目的達成を適切に支援するか

    ② 性能効率性 (Performance Efficiency)
       リソースに対するパフォーマンスの度合い。
       - 時間効率性: 応答時間、処理速度
       - 資源効率性: CPU/メモリ/ディスクの使用効率
       - 容量満足性: 上限値が要求を満たすか

    ③ 互換性 (Compatibility)
       他システムとの共存・情報交換能力。
       - 共存性: 他ソフトウェアと同じ環境で動くか
       - 相互運用性: 他システムとデータ交換できるか

    ④ 使用性 (Usability)
       ユーザーが効果的・効率的に使えるか。
       - 認識性/学習性/操作性/エラー防止/UI快美性/アクセシビリティ

    ⑤ 信頼性 (Reliability)
       指定条件下で機能を維持する度合い。
       - 成熟性: 通常運用での障害頻度
       - 可用性: 使いたいときに使えるか（99.9%等）
       - 障害許容性: 障害時にも動作を継続できるか
       - 回復性: 障害から復旧できるか

    ⑥ セキュリティ (Security)
       情報・データを保護する度合い。
       - 機密性/完全性/否認防止/責任追跡性/真正性

    ⑦ 保守性 (Maintainability)
       修正・改善のしやすさ。
       - モジュール性/再利用性/解析性/修正性/試験性

    ⑧ 移植性 (Portability)
       他の環境への移行のしやすさ。
       - 適応性/設置性/置換性
    """)

    misconception(
        "品質 = バグの少なさ",
        "バグの少なさは機能適合性の一部。性能・使用性・セキュリティなど8特性の総合が品質"
    )

    heading("1-3. 品質コスト (Cost of Quality: CoQ)")
    p("""
    品質にはコストがかかる。しかし「品質を無視するコスト」の方が遥かに高い。

    品質コストは4分類:

    【適合コスト（良い品質のための投資）】
    ① 予防コスト (Prevention Cost)
       - 要件レビュー、設計レビュー、テスト計画、教育訓練
       - 問題が起きる「前」に防ぐコスト

    ② 評価コスト (Appraisal Cost)
       - テスト実施、コードレビュー、インスペクション、監査
       - 問題を「見つける」ためのコスト

    【不適合コスト（品質不良による損失）】
    ③ 内部失敗コスト (Internal Failure Cost)
       - リリース前に見つかった不具合の修正、再テスト
       - 出荷前に見つかったからまだマシ

    ④ 外部失敗コスト (External Failure Cost)
       - 本番障害対応、顧客クレーム対応、賠償、信用失墜
       - 最も高額。1:10:100の法則で知られる

    1:10:100の法則:
    要件段階でバグを防ぐコストを1とすると、
    テスト段階で見つけると10倍、本番で見つかると100倍のコストがかかる。
    """)

    heading("1-4. シフトレフト ─ 品質活動を早期に")
    p("""
    シフトレフト(Shift Left): テストや品質活動を開発の早い段階に移す考え方。

    従来のV字モデルでは、テストは開発の「後」に行われた。
    しかし後工程で見つかるバグほど修正コストが高い。
    だから品質活動を「左」（早い段階）にシフトする。

    具体的なシフトレフト施策:
    - 要件段階: 要件レビュー、受入基準の明確化
    - 設計段階: 設計レビュー、脅威モデリング
    - 実装段階: ペアプログラミング、TDD、静的解析
    - テスト段階: 自動テスト、CI/CDでの即時フィードバック

    シフトライト(Shift Right)もある:
    本番環境での品質監視。カナリアリリース、A/Bテスト、
    本番モニタリング、カオスエンジニアリングなど。
    「左も右も」やるのが現代の品質戦略。
    """)

    interview("""
    品質 = ISO 25010の8特性の総合的な満足度
    品質コスト = 予防/評価（投資）と内部/外部失敗（損失）の4分類
    シフトレフト = 早い段階で品質を作り込み、修正コストを最小化
    要点: 「テストで品質を作る」のではなく「開発プロセス全体で品質を作り込む」
    """)


