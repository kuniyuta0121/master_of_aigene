# 第6章: 品質メトリクス
# ============================================================
def chapter6_quality_metrics():
    title("第6章: 品質メトリクス ─ 測れないものは管理できない")

    heading("6-1. テストカバレッジ")
    p("""
    テストカバレッジ: コードのどれだけがテストで実行されたかの指標。

    ■ C0カバレッジ (Statement Coverage / 命令網羅)
      各命令文が少なくとも1回実行されたか。
      最も基本的だが、分岐の検証が不十分。

    ■ C1カバレッジ (Branch Coverage / 分岐網羅)
      各分岐(if-else)の全方向が実行されたか。
      C0より強力。多くのプロジェクトの目標値はC1で80%程度。

    ■ C2カバレッジ (Condition Coverage / 条件網羅)
      複合条件内の各条件が真/偽の両方で評価されたか。
      例: if (A && B) → A=T/F, B=T/F それぞれ確認。

    ■ MC/DC (Modified Condition/Decision Coverage)
      各条件が独立して判定結果に影響することを確認。
      航空・医療など安全性が最重要の分野で要求される。
      DO-178C (航空ソフトウェア標準) のLevel Aで必須。

    カバレッジの限界:
    「カバレッジ100% ≠ バグなし」。
    カバレッジはテストの十分条件ではなく必要条件。
    テストの「不足」は検出できるが「品質」は保証しない。
    """)

    misconception(
        "カバレッジが高ければ品質が高い",
        "カバレッジは「テストされていないコード」を見つける指標。テスト自体の質は別問題"
    )

    heading("6-2. バグ密度 (Bug Density)")
    p("""
    バグ密度: コード量あたりのバグ数。
    計算: バグ数 ÷ KLOC(1000行単位のコード行数)

    業界目安:
    - 商用ソフトウェア: 1-25 bugs/KLOC
    - 高品質ソフトウェア: 0.5 bugs/KLOC以下
    - NASAレベル: 0.1 bugs/KLOC以下

    用途: プロジェクト間・リリース間の品質比較。
    注意: 言語やドメインで大きく異なるため、絶対値より傾向を見る。
    """)

    heading("6-3. 欠陥検出率 (DDP) と欠陥除去率 (DRE)")
    p("""
    ■ DDP (Defect Detection Percentage)
      テスト工程で検出した欠陥の割合。
      DDP = テストで見つけた欠陥数 ÷ (テスト欠陥 + 本番欠陥) × 100

    ■ DRE (Defect Removal Efficiency)
      各工程での欠陥除去効率。
      DRE = その工程で除去した欠陥数 ÷ その工程に存在した欠陥数 × 100

    DREが高い工程 = 欠陥を効果的に見つけている工程。
    レビューのDREは40-65%、テストは30-50%と言われる。
    両方を組み合わせることで全体のDREを高める。
    """)

    heading("6-4. 信頼度成長曲線とSカーブ")
    p("""
    ■ 信頼度成長曲線
      テスト期間中のバグ発見数の累積推移をグラフ化。
      理想的には曲線が飽和（新しいバグが出なくなる）する。
      飽和 → テスト終了判断の根拠の一つ。
      飽和しない → まだバグが潜んでいる可能性。

    ■ Sカーブ (テスト進捗管理)
      テストケースの消化数を時系列でプロット。
      計画線と実績線を比較して進捗を可視化。
      - 実績が計画より下 → 遅延。原因分析が必要。
      - 急激に上がる → テストを急いでいないか注意。

    これらのメトリクスは「テストをいつ終わらせるか」の判断材料。
    ゼロバグは非現実的なので、リスクベースで判断する。
    """)

    interview("""
    カバレッジ: C0(命令)→C1(分岐)→C2(条件)→MC/DC(独立条件影響)
    C1で80%が多くのプロジェクトの目標値
    バグ密度で品質傾向を追跡、DDPとDREで検出効率を評価
    信頼度成長曲線の飽和 = テスト終了判断の重要な根拠
    """)


