#!/usr/bin/env python3
"""
Leadership & Organization - 深掘り解説

テクニカルリーダーシップ、組織設計、プロダクトマネジメント、
ビジネスコミュニケーションを「なぜそうなるのか」から体系的に解説する。

実行: python leadership_explained.py
依存: Python 3.9+ 標準ライブラリのみ
"""
import textwrap

SEP = "=" * 70
THIN = "-" * 70

def title(text):
    print(f"\n{SEP}\n  {text}\n{SEP}")

def heading(text):
    print(f"\n{THIN}\n  {text}\n{THIN}")

def p(text):
    for line in textwrap.dedent(text).strip().split("\n"):
        print(f"  {line}")

def interview(text):
    print()
    print("  【面接向けに整理すると】")
    for line in textwrap.dedent(text).strip().split("\n"):
        print(f"  {line}")

def misconception(wrong, right):
    print(f"\n  ⚠ よくある誤解: {wrong}")
    print(f"  → 正確には: {right}")


