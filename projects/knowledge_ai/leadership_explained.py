def chapter1_technical_leadership():
    title("Chapter 1: テクニカルリーダーシップ")

    # --- 1.1 Staff+ の役割 ---
    heading("1.1 Staff+ エンジニアとは何か")
    p("""
    まず全体像:
    Staff+ は「個人の生産性が高い人」ではなく、
    「組織の技術的意思決定の質を上げる人」。
    コードを書くことは手段であり、価値の中心は影響範囲にある。

    典型ラダー:
      Junior -> Mid -> Senior -> Staff -> Principal -> Distinguished

    Senior と Staff+ の境界:
    - Senior: 与えられた課題を高品質に解決する
    - Staff+: そもそも何を解くべきかを定義し、複数チームを動かす

    現場で期待される変化:
    - ローカル最適から全体最適へ視点を移す
    - 実装中心から設計・合意形成中心へ比重を移す
    - 単発成果より「再現可能な仕組み」を作る

    たとえ話:
    Senior は優秀な船員、Staff+ は航路を設計する航海士。
    嵐を乗り切るだけでなく、どの港に向かうべきかを示す役割。
    """)

    misconception(
        "Staff+ になるには、もっと難しいコードを書けばいい",
        "Staff+ の価値はコード量ではなく「影響範囲」。\n"
        "  技術選定、アーキテクチャ方針、組織の技術力底上げが本質"
    )

    # Staff+ の4アーキタイプ
    heading("1.2 Staff+ の4アーキタイプ (Will Larson)")
    p("""
    先に結論: 「1.2 Staff+ の4アーキタイプ (Will Larson)」は実装と設計判断に直結する。背景とトレードオフを押さえる。
    Will Larson の著書 "Staff Engineer" に基づく4つの型:

    1. Tech Lead（技術リード）
       - チームの技術方向を導く、最も一般的な Staff+ の形
       - プロジェクトの技術判断、チームの成長支援
       - 日常: 設計レビュー、1on1、技術的ブロッカー解消

    2. Architect（アーキテクト）
       - 組織横断で技術方針を策定
       - システム全体の整合性と進化を担保
       - 日常: ADR作成、技術戦略文書、横断的設計レビュー

    3. Solver（問題解決者）
       - 組織内の最も困難な技術課題に取り組む
       - プロジェクト完了後に次の「火消し」へ移動
       - 日常: 深い技術調査、プロトタイプ、POC

    4. Right Hand（右腕）
       - VP/CTO の技術的代理人
       - 経営判断に技術的視点を提供
       - 日常: 経営会議参加、全社技術イニシアチブの推進

    自分がどの型に近いかを理解することが重要。
    多くの組織では Tech Lead が最も多く、Architect が次に多い。
    """)

    interview("""
    Q: Staff+ エンジニアの役割を説明してください。
    A: Staff+ は IC（Individual Contributor）のシニアトラック。
       3つの軸で価値を出す:
       (1) 技術方向性の策定 (Technical Vision)
       (2) 組織横断の技術課題解決
       (3) チームの技術力の底上げ（メンタリング、設計レビュー）
       コードを書く時間は減るが、1行のコードの影響範囲は格段に広がる。
    """)

    # --- 1.3 IC vs Manager ---
    heading("1.3 IC トラック vs Manager トラック")
    p("""
    この節の狙い: 「1.3 IC トラック vs Manager トラック」を単語暗記ではなく、なぜ必要かまでつながる形で理解する。
    キャリアの分岐点。どちらが「偉い」わけではない。

    IC (Individual Contributor):
      強み: 技術的深さ、実装力、アーキテクチャ設計
      日常: コード、設計、技術調査、レビュー
      評価: 技術的影響度、システム品質

    Manager:
      強み: ピープルマネジメント、チーム構築、採用
      日常: 1on1、採用面接、目標設定、組織調整
      評価: チームのアウトプット、メンバーの成長

    Pendulum（振り子）キャリア:
      IC → Manager → IC と行き来するのも健全。
      Manager を経験した IC は組織力学を理解し、
      IC を経験した Manager は技術的判断ができる。
    """)

    misconception(
        "IC と Manager は一度選んだら戻れない",
        "Pendulum キャリアは多くの企業で推奨されている。\n"
        "  両方を経験することで視野が広がる"
    )

    # --- 1.4 Technical Vision ---
    heading("1.4 Technical Vision の書き方")
    p("""
    先に結論: 「1.4 Technical Vision の書き方」は実装と設計判断に直結する。背景とトレードオフを押さえる。
    Staff+ が書く最も重要なドキュメント。
    1-2年の技術方向性を組織に示す「北極星」。

    構成:
    ┌──────────────────────────────────────┐
    │ 1. Context: ビジネスの方向性、外部環境 │
    │ 2. Current State: 現在のアーキテクチャ │
    │ 3. Target State: あるべき姿            │
    │ 4. Migration Path: 移行ステップ        │
    │ 5. Success Metrics: 成功の測り方       │
    │ 6. Alternatives: 却下した代替案        │
    └──────────────────────────────────────┘

    なぜ必要か:
    - チームが自律的に判断するための「羅針盤」になる
    - 個別の技術判断に一貫性が生まれる
    - ステークホルダーに技術投資の根拠を説明できる

    なぜ「代替案」セクションが重要か:
    → 読者の「Aではダメなの？」という疑問に先回りで答える。
    → 判断プロセスの透明性を確保する。
    """)

    interview("""
    Q: Technical Vision を書いた経験はありますか？
    A: はい。以下の手順で進めました:
       (1) 関係者ヒアリング（PM、SRE、隣接チーム）
       (2) 現状のペインポイントを定量化（障害頻度、デプロイ速度）
       (3) ドラフト → RFC として全社レビュー
       (4) 四半期ごとに見直し（Vision は生き物）
       重要なのは「正解」を書くことではなく、
       チームが同じ方向を向くための合意形成プロセスです。
    """)


