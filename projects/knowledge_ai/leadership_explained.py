def chapter7_decision_and_hiring():
    title("Chapter 7: 技術的意思決定 & 採用")

    heading("7.1 ADR (Architecture Decision Record)")
    p("""
    まず全体像: この節では「7.1 ADR (Architecture Decision Record)」を、定義 -> 仕組み -> 実務上の判断の順で整理する。
    技術的な意思決定を記録するためのドキュメント。

    なぜ必要か:
    → 「なぜこの技術を選んだのか」が時間とともに失われる。
    → 新メンバーが「なぜこうなっているのか」を理解できない。
    → 同じ議論を何度も繰り返すのを防ぐ。

    ADR のテンプレート:
    ┌──────────────────────────────────┐
    │ # ADR-001: [タイトル]              │
    │ Status: Accepted / Superseded    │
    │ Date: 2026-03-16                 │
    │                                  │
    │ ## Context（背景）                │
    │ なぜこの決定が必要になったか       │
    │                                  │
    │ ## Decision（決定）               │
    │ 何を決めたか                      │
    │                                  │
    │ ## Consequences（影響）            │
    │ この決定の結果、何が起きるか       │
    │ （良い影響と悪い影響の両方）       │
    │                                  │
    │ ## Alternatives（代替案）          │
    │ 検討して却下した選択肢とその理由   │
    └──────────────────────────────────┘

    ADR の運用ルール:
    ・一度 Accepted された ADR は編集しない（immutable）
    ・変更したい場合は新しい ADR で Supersede する
    ・ADR はコードリポジトリ内に置く（docs/adr/）
    ・軽量に書く（1ページ以内）
    """)

    # --- 7.2 RFC ---
    heading("7.2 RFC (Request for Comments)")
    p("""
    まず全体像: この節では「7.2 RFC (Request for Comments)」を、定義 -> 仕組み -> 実務上の判断の順で整理する。
    大きな技術変更の前に、組織全体から意見を募るプロセス。

    ADR との違い:
      ADR: 決定を記録する（事後的でもOK）
      RFC: 決定の前に議論する（事前のコンセンサス形成）

    RFC のプロセス:
      1. 著者がドラフトを書く
      2. レビュアーを指名（影響を受けるチーム）
      3. コメント期間（通常1-2週間）
      4. フィードバックを反映
      5. Approve / Reject / Defer

    RFC を書くべき基準:
    ・複数チームに影響する変更
    ・可逆性が低い決定（DBスキーマ、API設計）
    ・大きな工数（2人月以上）を伴う
    ・セキュリティやプライバシーに関わる変更

    RFC の落とし穴:
    ・長すぎる RFC → 誰も読まない（5ページ以内）
    ・コメント期間が長すぎる → 意思決定が遅れる
    ・RFC 疲れ → 小さな変更にも RFC を求めると速度が落ちる
    """)

    misconception(
        "RFC で全員の合意が必要",
        "RFC は「全員の意見を聞く」だが「全員の同意」は不要。\n"
        "  最終決定者（通常はテックリードや Staff+）が判断する。\n"
        "  「Disagree and Commit」（反対しても決まったら全力でやる）"
    )

    # --- 7.3 面接設計 ---
    heading("7.3 技術面接の設計")
    p("""
    この節の狙い: 「7.3 技術面接の設計」を単語暗記ではなく、なぜ必要かまでつながる形で理解する。
    「良い面接」とは「良いエンジニアを見つける面接」ではなく、
    「候補者の能力を正確に評価できる面接」。

    構造化面接の原則:
    (1) 全候補者に同じ質問をする（比較可能性）
    (2) 評価基準を事前に定義する（ルーブリック）
    (3) 複数の面接官で独立に評価（バイアス軽減）

    面接の種類:
      Coding:    アルゴリズム・データ構造（基礎力）
      System Design: 大規模システムの設計（経験・知識）
      Behavioral: 過去の行動から将来を予測（STAR形式）
      Culture Fit: チームとの相性（価値観の一致）

    STAR 形式:
      Situation:  どんな状況だったか
      Task:       何が求められていたか
      Action:     具体的に何をしたか
      Result:     どんな結果になったか

    バイアスへの対策:
    ・ハロー効果: 一つの良い点で全体を高評価してしまう
    ・類似性バイアス: 自分と似た人を高く評価する
    ・対策: ルーブリック、独立評価、構造化面接
    """)

    # --- 7.4 メンタリング ---
    heading("7.4 メンタリングとスポンサーシップ")
    p("""
    まず全体像: この節では「7.4 メンタリングとスポンサーシップ」を、定義 -> 仕組み -> 実務上の判断の順で整理する。
    メンタリング:
      「アドバイスをする」関係。知識・経験の共有。
      メンターは答えを教えるのではなく、考え方を教える。

    スポンサーシップ:
      「チャンスを与える」関係。メンタリングより強力。
      スポンサーは自分の信用を使って、相手に機会を作る。
      例: 重要プロジェクトへのアサイン、昇進推薦、経営層への紹介

    メンタリング vs スポンサーシップ:
      メンター: 「こうするといいよ」（助言）
      スポンサー: 「この人に任せてください」（行動）

    1on1 の効果的な進め方:
    (1) メンティーがアジェンダを設定する
    (2) 直近の課題ではなく、成長の方向性を議論する
    (3) アクションアイテムを明確にする
    (4) 頻度: 隔週30分が最低ライン

    Staff+ エンジニアにとってメンタリングは「余暇」ではない。
    組織の技術力を底上げする本業の一部。
    """)

    interview("""
    Q: メンタリングの経験を教えてください。
    A: Junior エンジニアのメンターを1年間務めました。
       最初の3ヶ月: 技術的なペアプロとコードレビュー
       中期: 設計力を鍛えるため、小規模プロジェクトのリード経験を積ませた
       結果: 半年で独立してPRを出せるレベルに成長
       学び: 答えを教えるより、質問を投げかけて自分で考えさせる方が効果的
    """)


