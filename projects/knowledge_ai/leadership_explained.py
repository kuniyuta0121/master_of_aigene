def chapter8_business_and_communication():
    title("Chapter 8: ビジネス & コミュニケーション")

    heading("8.1 ユニットエコノミクス")
    p("""
    この節の狙い: 「8.1 ユニットエコノミクス」を単語暗記ではなく、なぜ必要かまでつながる形で理解する。
    ビジネスの健全性を「1ユーザー/1取引単位」で測る指標群。
    エンジニアも理解すべき理由: 技術投資の優先順位に直結する。

    基本指標:

    CAC (Customer Acquisition Cost) — 顧客獲得コスト
      マーケ費用 / 新規顧客数
      例: 広告費100万円で100人獲得 → CAC = 1万円

    LTV (Lifetime Value) — 顧客生涯価値
      ARPU x 平均継続月数
      例: 月額1,000円 x 平均24ヶ月 = LTV 24,000円

    LTV/CAC 比率:
      > 3.0: 健全（投資回収できている）
      < 1.0: 赤字（獲得するほど損する）

    Payback Period — 回収期間
      CAC / 月間収益
      例: CAC 1万円 / 月1,000円 = 10ヶ月で回収

    Churn Rate — 解約率
      月間解約ユーザー / 月初ユーザー数
      SaaS では月次 < 5% が目安
      1%の改善がLTVに大きく影響する

    なぜエンジニアが知るべきか:
    → 「パフォーマンス改善」の優先度を判断できる。
    → レイテンシ改善がコンバージョン率を上げ、CAC を下げる。
    → 障害の影響を金額で語れる（churn → LTV 損失）。
    """)

    misconception(
        "ユニットエコノミクスは PM やビジネスサイドの仕事",
        "Staff+ エンジニアは技術投資を「ビジネスインパクト」で語る。\n"
        "  「p99 レイテンシを 200ms 改善 → CVR 0.5% 向上 → 年間 X 万円の収益増」"
    )

    # --- 8.2 テクニカルライティング ---
    heading("8.2 テクニカルライティング")
    p("""
    この節の狙い: 「8.2 テクニカルライティング」を単語暗記ではなく、なぜ必要かまでつながる形で理解する。
    Staff+ の影響力は「文章力」に大きく依存する。
    コードは10人が読むが、設計ドキュメントは100人が読む。

    原則:

    1. 読者を定義する
       誰のために書くかで、詳細度と語彙が変わる。
       経営層向け: ビジネスインパクト中心、技術詳細は最小限
       エンジニア向け: 技術的根拠、トレードオフ、実装方針

    2. ボトムライン・ファースト
       最も重要な結論を最初に書く。
       NG: 「調査の結果、A, B, C を検討し、最終的に B を選びました」
       OK: 「B を採用します。理由は X と Y。代替案 A, C の却下理由は以下。」

    3. 1文1意
       1つの文に複数のアイデアを詰め込まない。
       NG: 「A は B だが C もあり、D の観点では E になる」
       OK: 「A は B です。ただし C の制約があります。」

    4. 図を使う
       アーキテクチャ図、フロー図、比較表は文章の10倍伝わる。
       Mermaid、draw.io、ASCII art を活用する。
    """)

    # --- 8.3 エグゼクティブコミュニケーション ---
    heading("8.3 エグゼクティブコミュニケーション")
    p("""
    この節の狙い: 「8.3 エグゼクティブコミュニケーション」を単語暗記ではなく、なぜ必要かまでつながる形で理解する。
    経営層への報告・提案は、エンジニアとの会話とは全く異なる。

    経営層が知りたいこと:
    (1) ビジネスインパクト（売上、コスト、リスク）
    (2) タイムライン（いつ完了するか）
    (3) リソース（何が必要か）
    (4) リスクと緩和策

    経営層が知りたくないこと:
    (1) 技術的詳細（「gRPC vs REST」の議論）
    (2) 苦労話（「大変でした」は不要）
    (3) 不確実な情報を曖昧に伝えること

    BLUF (Bottom Line Up Front):
    「結論から先に言う」フレームワーク。

    例:
      NG: 「現在のシステムは 2018 年に構築され、
           トラフィックが年 40% 増加しており、
           データベースの応答時間が悪化し...
           → アーキテクチャの刷新が必要です」

      OK: 「アーキテクチャの刷新が必要です。
           放置すると 6ヶ月後にピーク時の障害リスクが高まります。
           背景: トラフィック年40%増、DB応答時間が SLO を超過。
           提案: 3段階の移行で、Q3 までに完了。必要リソース: 3名。」

    Amazon の「6ページメモ」:
    → PowerPoint ではなく、6ページの散文メモで提案する。
    → 会議冒頭の20分で全員が黙読 → 議論。
    → なぜ: スライドは曖昧さを許すが、文章は論理を強制する。
    """)

