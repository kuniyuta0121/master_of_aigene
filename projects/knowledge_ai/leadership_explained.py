def chapter2_team_topologies():
    title("Chapter 2: チームトポロジー")

    heading("2.1 なぜチーム構造が重要なのか")
    p("""
    先に結論: 「2.1 なぜチーム構造が重要なのか」は実装と設計判断に直結する。背景とトレードオフを押さえる。
    Conway の法則:
    「システムを設計する組織は、その組織のコミュニケーション構造を
     反映した設計を生み出す」(1967年, Melvin Conway)

    たとえ話: 3チームでコンパイラを作ると、3パスコンパイラができる。
    これは冗談ではなく、実証されている現象。

    なぜ起きるか:
    - チーム間の調整コストが高い → インターフェースで分離しようとする
    - チーム内は密にコミュニケーション → 密結合でも動く
    - 結果: チーム境界 ≒ システム境界 になる

    逆コンウェイ戦略:
    → 望ましいアーキテクチャに合わせてチーム構造を設計する。
    マイクロサービスにしたいなら、先に小さな独立チームを作る。
    """)

    misconception(
        "良いアーキテクチャを設計すれば、チーム構造は関係ない",
        "チーム構造とアーキテクチャは不可分。\n"
        "  アーキテクチャを変えたいなら、先にチーム構造を変える（逆コンウェイ）"
    )

    # --- 2.2 4つのチームタイプ ---
    heading("2.2 4つの基本チームタイプ")
    p("""
    まず全体像: この節では「2.2 4つの基本チームタイプ」を、定義 -> 仕組み -> 実務上の判断の順で整理する。
    Matthew Skelton & Manuel Pais の "Team Topologies" (2019) が定義。

    1. Stream-aligned Team（ストリーム沿いチーム）
       ビジネスの「流れ」に沿って配置。End-to-end のオーナーシップ。
       例: 検索チーム、決済チーム、通知チーム
       全チームの約80%はこのタイプであるべき。

       なぜ「ストリーム」か:
       → ユーザー価値の流れ（ストリーム）に沿うことで、
         チーム内で企画→開発→運用が完結し、引き渡しのムダが消える。

    2. Platform Team（プラットフォームチーム）
       Stream-aligned チームの認知負荷を軽減するためのチーム。
       セルフサービスの API・ツール・ドキュメントを提供。
       例: CI/CD基盤、クラウドインフラ、データ基盤

       なぜ必要か:
       → 各 Stream チームが個別にインフラを構築すると、
         知識の重複と品質のばらつきが生まれる。

    3. Enabling Team（支援チーム）
       他チームの技術力向上を期間限定で支援。
       コーチング・ペアプロ・トレーニング。
       例: SRE支援、セキュリティ支援

       なぜ期間限定か:
       → 永続的な依存関係を作らないため。
         スキル移転が完了したら離れる。

    4. Complicated-subsystem Team（複雑サブシステムチーム）
       高度な専門知識が必要な領域を担当。
       例: ML推論エンジン、暗号化基盤、動画エンコーダ
       このチームがないと、Stream チームの認知負荷が爆発する。
    """)

    # --- 2.3 認知負荷 ---
    heading("2.3 認知負荷とチームサイズ")
    p("""
    先に結論: 「2.3 認知負荷とチームサイズ」は実装と設計判断に直結する。背景とトレードオフを押さえる。
    認知負荷（Cognitive Load）= チームが扱うべき情報量と複雑さ。

    3種類の認知負荷:
      Intrinsic:  業務そのものの複雑さ（ドメイン知識）
      Extraneous: ツール・プロセスの複雑さ（不要な複雑さ）
      Germane:    新しいことを学ぶ負荷（必要な学習）

    チームトポロジーの目的:
    → Extraneous Load を最小化し、Intrinsic に集中させる。
    → Platform Team が Extraneous を引き受ける。

    Dunbar数とチームサイズ:
      5人:  深い信頼関係を築ける上限
      15人: 日常的に協力できる上限
      50人: 顔と名前が一致する上限
      150人: 社会的関係を維持できる上限（Dunbar数）

    Amazon の Two-Pizza Team:
    → 2枚のピザで賄える人数（6-8人）を1チームの上限とする。
    → 小さいチームはコミュニケーションコストが低い。
    → n人のチームの通信経路は n(n-1)/2。
       6人 = 15経路、10人 = 45経路、20人 = 190経路。
    """)

    misconception(
        "大きなチームの方が早く成果が出る",
        "Brooks の法則: 遅延したプロジェクトに人を追加すると、さらに遅延する。\n"
        "  新メンバーへの教育コストと通信経路の爆発が原因"
    )

    # --- 2.4 Team API ---
    heading("2.4 Team API")
    p("""
    先に結論: 「2.4 Team API」は実装と設計判断に直結する。背景とトレードオフを押さえる。
    チームを「サービス」として捉え、インターフェースを明示する考え方。

    Team API に含めるもの:
      - チームのミッション（何に責任を持つか）
      - 連絡方法（Slack チャンネル、オフィスアワー）
      - 提供するサービス/API
      - 受け入れるリクエストの種類
      - SLA（応答時間の目安）
      - ドキュメントの場所
      - 今は対応できないこと（スコープ外の明示）

    なぜ有効か:
    → チーム間の期待値を明確にし、暗黙の依存関係を可視化する。
    → 「誰に聞けばいいかわからない」問題を解消する。
    """)

    interview("""
    Q: チーム間の連携を改善した経験は？
    A: Team API を導入しました。
       各チームが「ミッション・提供サービス・連絡方法」を文書化。
       効果: (1) チーム間の問い合わせが30%減少
              (2) 新メンバーのオンボーディング時間が半減
              (3) 責任範囲の曖昧さに起因する障害が激減
    """)


